<h1>Sensory Processing Disorder (SPD)</h1>
<br>
Sensory Processing Disorder (SPD) is a term used to describe children who struggle to correctly perceive the sensory world around them. The sensory difficulties children with SPD experience may find it difficult to tie shoe laces, write, run and many other childhood activities. <br><br>
Occupational therapists can assess the difficulties associated with SPD, determine how these are impacting upon the child’s ability to function at home, in school and socially. The occupational therapist would be able to provide effective treatment aimed at improving function.<br><br>
<h2>Does your child have any of the following difficulties?</h2>
<br>
Sensory Processing Disorder can be present in many forms and can often result in behavioural issues as well as functional difficulties. It is common for children within the Autistic Spectrum to experience many complex sensory difficulties. Children with SPD may experience sensory difficulties relating to one or multiple senses. Some of the symptoms of SPD are listed below:
<ul class="ticks">
<li>Poor posture</li>
<li>Difficulty controlling movements</li>
<li>Poor handwriting</li>
<li>Dislikes loud noises </li>
<li>Difficulty making friends</li>
<li>Difficulty dressing, eating or sleeping</li>
<li>Frequently throws tantrums or gets upset</li>
<li>Easily distracted </li>
<li>Fidgety</li>
</ul>
<h2>How can these difficulties impact on function?</h2>
<br>
The difficulties associated with SPD can have a dramatic effect on a child’s ability to function and complete everyday tasks that many other children complete with ease. Everyday task refer to activities such as tying shoe laces, writing, engaging in sport, running, playing and making friends.<br><br>
<b>Home:</b>
<ul class="ticks">
<li>Trouble sleeping, eating and getting dressed</li>
<li>Difficulty getting ready in time for school</li>
<li>Finds noisy environments overwhelming</li>
</ul>
<b>School:</b>
<ul class="ticks">
<li>Difficulty making friends</li>
<li>Finds the noisy school environment difficulty to deal with</li>
<li>Finds uniform unbearable to wear</li>
</ul>
Our occupational therapist can provide assessment and treatment at home or in school depending on your preference. The OT will work together with all involved to set collaborative goals and attempt to develop the sensory difficulties that your child may be experiencing.<br><br>
<h2>What exactly is Sensory Processing Disorder? </h2>
<br>
The cause of SPD is currently unknown. However research suggests that there may be some hereditary links between parents with the condition and children. However these are unclear and need further research<br><br>
Children with Sensory Processing Disorder tend to fall into one of two categories: Hypersensitivity or Hyposensitivity. The first is a term used to describe children who are overly sensitive, and often struggle with sleeping, dressing or any form of sensory input. Whereas children who are termed Hyposensitive under-react to stimuli that should otherwise cause them some discomfort, such as pain or heat.<br><br>
<b>Hypersensitivity</b>
<ul class="ticks">
<li>Find clothing unbearable</li>
<li>Over sensitive to light</li>
<li>Finds it difficult to cope in loud environments</li>
<li>May act out in environments with a lot going on (such as supermarkets)</li>
<li>Difficulty sleeping</li>
</ul>
<b>Hyposensitivity:</b>
<ul class="ticks">
<li>May show little or no reaction to pain</li>
<li>Under react to loud bangs or noises</li>
<li>Not distracted by extremely bright lights </li>
<li>Has little or no reaction to itchy material or sharp objects</li>
</ul>
<h2>Occupational therapy treatment available for Sensory Processing Disorder</h2>
<br>
An occupational therapist can provide effective assessment of how the symptoms of SPD are impacting upon your child’s ability to function at home and in school. Following this the therapist would provide effective treatment aimed at improving the difficulties associated with SPD
<ul class="ticks">
<li>Improved sensory regulation</li>
<li>Better understanding of the world your child experiences</li>
<li>Improvement in routines and living with SPD</li>
</ul>
<h2>Summary</h2>
<br>
In summary Sensory Processing Disorder (SPD) is a term used to describe children who struggle to correctly perceive the sensory world around them. Occupational therapists can assess the difficulties associated with SPD, determine how these are impacting upon the child’s ability to function at home, in school and socially. Occupational therapists can assess the difficulties associated with SPD, and provide effective treatment to develop sensory processing.<br><br>
