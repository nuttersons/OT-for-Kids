<h1>Down's Syndrome</h1>
<br>
Down's Syndrome is a genetic condition that causes some level of learning disabilities and a range of physical features.  Children with Down's Syndrome are likely to have reduced muscle tone and muscle weakness leading to difficulty in completing occupations both at home and at school.<br><br>
An occupational therapist would be able to provide assessment and treatment for Down's Syndrome, identifying the key learning difficulties and how these are impacting upon both the school and the home environment, identifying how reduced muscle tone is resulting in poor occupational performance and offer treatment to manage these symptoms.<br><br>
<h2>Does your child have any of the following difficulties?</h2>
<br>
Down's Syndrome is typically characterised by facial features such as eyes that slant upwards and outwards, a round shaped face, a small nose and a flat nasal bridge. Down's Syndrome often results in the following difficulties:
<ul class="ticks">
<li>Reduced muscle tone</li>
<li>Hyper-flexibility </li>
<li>Muscle weakness</li>
<li>Poor grip</li>
<li>Poor handwriting</li>
<li>Co-ordination difficulties </li>
<li>Delayed development in fine and gross motor skills </li>
</ul>
An occupational therapist would be able to provide effective treatment and assessment of the level of disability that Down's Syndrome is impacting upon your child. The occupational therapist would work together with you and your child to achieve the goals that you would like to achieve.<br> <br>
<h2>How can these difficulties impact on function?</h2>
<br>
Children with Down's Syndrome often have profound learning difficulties that impact upon function at home, in school and socially. The level of the learning disabilities will depend on the child, varying from profound to mild. Some of these difficulties are listed below:<br><br>
<b>Home:</b>
<ul class="ticks">
<li>Delayed milestones
<ul>
<li>Rolling, sitting, sit to stand, grasping objects, passing objects from one hand to another and various other milestones are delayed due to Down's Syndrome.</li>
</ul>
</li>
<li>Behavioural  issues with siblings </li>
<li>Poor grip</li>
<li>Difficulty carrying food, drink through the house without spilling it</li>
</ul>
<b>School:</b>
<ul class="ticks">
<li>Learning difficulties</li>
<li>Concentration difficulties</li>
<li>Understanding abstract concepts</li>
</ul>
<b>Social/behavioural:</b>
<ul class="ticks">
<li>Bullying</li>
<li>Isolated, lack of friends</li>
</ul>
<h2>What exactly is Down's Syndrome?</h2>
<br>
Down's Syndrome is a genetic disorder that occurs during development. In normal development 23 chromosomes are produced, however in a child with Down's Syndrome an extra chromosome is formed (under chromosome 21) and this is thought to be the cause of the condition.<br><br>
It is diagnosed mainly during childhood and is noticeable through the facial characteristics that are common with the condition. However screening tests during pregnancy can indicate the likelihood of your child being born with Down's Syndrome.<br><br>
<h2>Prevalence:</h2>
<br>
Each year in the UK approximately one in every 1,000 babies are born with Down's Syndrome.<br><br>
<h2>Occupational therapy treatment available for Down's Syndrome </h2>
<br>
An occupational therapist can provide effective treatment and management of the symptoms of Down's Syndrome and offer advice and support to the parents of a child with the condition. Treatment could involve the following:
<ul class="ticks">
<li>Fine motor improvement therapy</li>
<li>Gross motor activity training </li>
<li>Home or school visit</li>
<li>Improved handwriting</li>
<li>Improved social skills </li>
<li>Improved grip and skills involving carrying objects</li>
<li>Increased independence in getting dressed and planning their day<br>
</li>
</ul>
<h2>Summary</h2>
<br>
In summary Down's Syndrome is a genetic condition that causes some level of learning disabilities and a range of physical features. Each year in the UK approximately one in every 1,000 babies are born with Down's Syndrome. An occupational therapist would be able to provide effective treatment and assessment of the level of disability that Down's Syndrome is impacting upon your child.<br><br>
