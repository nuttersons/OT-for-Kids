<h1>Cerebral Palsy</h1>
<br>
Cerebral Palsy is a neurological disorder that occurs during or shortly after birth, it results in movement difficulties and often has secondary complications regarding cognition and perception. Although currently there is no cure for Cerebral Palsy, an occupational therapist can provide effective treatment and management of the symptoms that are directly affecting activity performance.
<br><br>
<h2>Does your child have any of the following difficulties?</h2>
<br>
Cerebral Palsy is an extremely unique condition and symptoms vary greatly from child to child, however common symptoms can be determined, these are:
<ul class="ticks">
<li>Un-coordinated movements</li>
<li>Difficulty planning and executing muscle movements</li>
<li>Spasms </li>
<li>High or low muscle tone</li>
<li>Poor fine motor skills</li>
<li>Difficulty running, catching or throwing</li>
<li>Poor balance</li>
<li>Difficulty getting dressed</li>
<li>Reduced gait</li>
<li>Difficulty concentrating and/or processing information</li>
</ul>
<h2>How can these difficulties impact on function?</h2>
<br>
Occupational therapists aim to improve activity participation, increase health and wellbeing, and enable children to reach their goals, they will work with you and your child in addressing issues that impact across all areas of their life, be it at home, in school or socially, some examples of how Cerebral Palsy impacts upon school life are as follows:<br>
<br>
<b> Home:</b>
<ul class="ticks">
<li>Clumsy</li>
<li>Bumping into walls</li>
<li>Falling often</li>
<li>Difficulty eating </li>
<li>Difficulty dressing </li>
<li>Possible toileting difficulty</li>
<li>Poor concentration</li>
</ul>
<b>School:</b>
<ul class="ticks">
<li>Poor handwriting</li>
<li>Difficulty getting changed for P.E in time</li>
<li>Running/playing outside results in falls</li>
<li>Difficulty in P.E</li>
</ul>
<b>Social/behavioural:</b>
<ul class="ticks">
<li>Child can begin to notice their differences </li>
<li>Isolated, only working with adults</li>
<li>Difficulty building friendships</li>
</ul>
<h2>What exactly is Cerebral Palsy? </h2>
<br>
Cerebral Palsy describes a group of neurological disorders that affect the development of movement and posture, causing activity limitation. It is a non-progressive disease that develops during or soon after birth. The motor disorders of Cerebral palsy are often accompanied by sensory, cognition, communication, perception and/or behavioural disturbances. Cerebral Palsy varies in severity and can at worst result in quadriplegia (the loss of use of all four limbs) but most cases result in spastic Cerebral Palsy, which only affects one side of the body or one limb.<br><br>
<b>Causes:</b><br>
Cerebral palsy is caused by damage to the part of the brain that causes movement, the basal ganglia and the cerebellum. This is usually due to a trauma during birth, premature birth or any form of oxygen deprivation to the child’s brain, such as a reduction in the blood supply to the child (through the mother receiving an infection, or abnormally low blood pressure).<br><br>
<b>Diagnosis:</b><br>
Cerebral Palsy is diagnosed by a paediatrician; they will ask about the child’s history and study the child’s reflexes, posture, motor skills and muscle tone. Further blood tests, MRI or CT scans can be done to reveal further information regarding the diagnosis.<br><br>
<b>Prevalence:</b><br>
It is estimated that 1 in every 400 children in the UK is affected by cerebral palsy. Approximately 1,800 babies are diagnosed with the condition each year.
<br><br>
<h2>Occupational Therapy Treatment available for Cerebral Palsy</h2>
<br>
An occupational therapist can improve the movements, health and happiness of a child with Cerebral Palsy. This is done through a wide range of therapy treatment, following an assessment. Some of the benefits of Occupational Therapy are:
<ul class="ticks">
<li>Improved gross motor skills</li>
<li>Improved fine motor skills</li>
<li>Improved balance</li>
<li>Increased independence to get dressed, feed and toilet themselves</li>
<li>Increased mobility</li>
<li>Increased confidence</li>
</ul>
<h2>Summary</h2>
<br>
In Summary Cerebral Palsy is a neurological disorder that occurs during or shortly after birth, it results in movement difficulties and often has secondary complications regarding cognition and perception. Occupational therapists aim to improve activity participation, increase health and wellbeing, and enable children to reach their goals. Through Occupational Therapy, your child can feel happier, increase their independence and increase their confidence in movement activities.
