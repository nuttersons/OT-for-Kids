<h1>Acquired Head/Brain injury</h1>
<br>
Every 30 minutes, a child or young person will acquire a brain injury. This could be the result of an accident, an illness such as meningitis or encephalitis, a poisoning, a stroke or a brain tumour. <br><br>
Acquired brain Injury or trauma can have profound effects on development of function for a child, and can often take months or years to show. The delay in development is because the area damaged may not have fully developed yet and therefore the extent of the injury is unknown. The brain is a delicate organ, and controls all thoughts, movements and processes that we complete. Any damage to this vital organ can therefore have detrimental effects on how the brain works. <br><br>
Brain damage will then relay into the child’s ability to perform functional occupations. Functional occupations include tying shoe laces, getting dressed, completing sums and many other activities. As all of these activities require a high level of cognitive performance, such as sequencing, organisation, planning, regulating sensory feedback, fine motor control, finger control, and gross motor control, to name just a few, any damage to the areas of the brain responsible for these cognitive abilities can dramatically affect function.
<br><br>
<h2>Does your child have any of the following difficulties?</h2>
<br>
With the brain being such a complex organ, Acquired brain injury will usually affect a particular area of the brain depending upon where the trauma occurs.  For instance, damage to the back of the brain can affect vision, planning, organisation and visual perception skills. Below is a summary of the functional effects that damage can evoke.<br><br>
<h2>Sensory Area</h2>
<br>
The sensory area of the brain is involved in regulating the sensory feedback that your child receives from the world around them. Without this integration of touch, smell, sight, sound and taste, it becomes difficult to navigate and complete the every-day tasks that are required of young children (such as writing, getting dressed and playing). Some examples of sensory damage that an occupational therapist can provide treatment for include:
<ul class="ticks">
<li>Overly sensitive to smells/ unable to smell</li>
<li>Finds clothes sharp or uncomfortable</li>
<li>Struggles to identify differences in temperature </li>
<li>Regulating sensory feedback from body parts</li>
<li>Loss of sensation</li>
<li>Trouble getting dressed, tying shoe laces or other fiddly activities</li>
<li>Finds loud noises over-whelming/ too distracting </li>
<li>Is under-stimulated by sensory input (feels the need to spin/ overload sensory system as a calming method)</li>
</ul>
<h2>Cognitive Area</h2>
<br>
The cognitive area of the brain relates to the area involved in completing complex thought processes, such as planning, problem solving, memory and concentration. Damages to the cognitive area have great effects on function at school and at home. Some examples of difficulties an occupational therapist can help with include:
<ul class="ticks">
<li>Struggles to concentrate </li>
<li>Easily distracted</li>
<li>Poor problem solving skills</li>
<li>Emotional imbalance</li>
<li>Frequently gets upset, or acts out in new or challenging situations</li>
<li>Struggles to be creative and imaginative </li>
<li>Difficulty knowing what to do next in tasks</li>
<li>Struggles to remember and retain information</li>
</ul>
<h2>Motor function, co-ordination and balance area</h2>
<br>
The area of the brain responsible for smooth and continuous movement, good balance, posture and co-ordination may be damaged following acquired brain injury. Damage to the motor function area of the brain can have profound effects on a child’s ability to complete a wide range of activities, some difficulties include:
<ul class="ticks">
<li>Poor posture</li>
<li>Difficulty Co-ordinating movements</li>
<li>Struggles to catch or throw a ball</li>
<li>Jerky/unwanted movements</li>
<li>Poor handwriting</li>
<li>High or low muscle tone</li>
<li>Poor balance </li>
<li>Difficulty balancing on one leg/ across a beam</li>
</ul>
<h2>Visual Area</h2>
<br>
The visual area of the brain is responsible for seeing and interpreting the world around us. Vision is used in most tasks that we complete on a daily basis; any damage to this area can have detrimental effects to a child’s learning and development. An occupational therapist can provide effective management and treatment to improve function following visual impairment. Some examples of how visual impairment affect function:
<ul class="ticks">
<li>Unable to distinguish the difference between 2D and 3D shapes</li>
<li>Poor form recognition (square or circle, short or long)</li>
<li>Finds it difficult to pick out information from a crowded page</li>
<li>Interpreting images</li>
<li>Poor hand-eye co-ordination</li>
<li>Poor handwriting</li>
</ul>
<h2>How can these difficulties impact on function?</h2>
<br>
These lists below are not exclusive but are used rather to provide you with some examples and a guide to understand why your child is behaving the way they are.
<br><br>
<b>Home:</b>
<ul class="ticks">
<li>Difficulty getting dressed</li>
<li>Dislikes playing with construction based toys</li>
<li>Difficulty problem solving</li>
<li>Movement difficulties</li>
<li>Bumps into objects </li>
<li>Frequent falls</li>
<li>Becomes distracted easily</li>
<li>Struggles to play sports</li>
</ul>
<b>School:</b>
<ul class="ticks">
<li>Poor handwriting</li>
<li>Academically behind other classmates<br>
</li>
<li>Finds the sensory feedback from classroom environments overwhelming</li>
<li>Poor posture at school</li>
<li>Difficulty completing P.E or sport activities</li>
<li>Poor concentration</li>
<li>Difficulty making friends</li>
<li>Often angers easily</li>
</ul>
<b>Prevalence:</b> <br><br>
Every year 200,000 children have an acquired brain injury, and account for nearly a third of the people who attend accident and emergency in a year.
