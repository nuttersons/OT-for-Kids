<h1>Dyspraxia or Developmental Co-ordination Disorder (DCD)</h1>
<br>
Developmental Co-ordination Disorder (DCD) or Dyspraxia is a term used to describe a delay in development of, or difficulty co-ordinating motor skills in children that directly affects the child’s ability to perform common everyday activities. Everyday activities refers to the tasks that children complete on a daily basis, and include activities such as writing, playing, drawing, moving, balancing, running, getting dressed and tying shoelaces or completing other fine motor tasks. <br><br>
An occupational therapist can develop these skills, and improve the motor co-ordination of a child with DCD or Dyspraxia. The occupational therapist would use occupations to enhance and develop the underlying skills that gross motor skills compose of.<br><br>
<h2>Does your child have any of the following difficulties?</h2>
<br>
Often described as being clumsy or awkward, children with DCD or Dyspraxia have common difficulties in everyday tasks. A child with DCD or Dyspraxia has the following symptoms: 
<ul class="ticks">
<li>Difficulty going down stairs</li>
<li>Struggling to throw or catch</li>
<li>Unable to/ struggling to run</li>
<li>Difficulty navigating an obstacle course in P.E</li>
<li>Difficulty traversing through forests or rough ground</li>
<li>Poor balance </li>
<li>Tying shoelaces</li>
<li>Completing fine motor tasks</li>
<li>Poor sentence formation </li>
<li>Getting dressed</li>
</ul>
An occupational therapist can help with all of these problems (and others that are not listed) through the use of different treatment techniques. For example, a child who struggles to catch a ball can undergo therapy whereby the skills needed to complete this task are developed and improved through an array of similar tasks so that these developed skills will improve the child’s ability to catch a ball.<br><br>
<h2>How can these difficulties impact on function?</h2>
<br>
DCD can have a dramatic effect on function both at school and home. Children begin to pick up on who is more able to complete activities and who is behind; this could lead to social issues such as bullying and teasing. Some examples of how a child with DCD or Dyspraxia may struggle at home, in school or socially are listed below:<br><br>
<b>Home:</b>
<ul class="ticks">
<li>Clumsy</li>
<li>Spilling drinks and difficulty carrying food</li>
<li>Falling frequently and often bumps into walls</li>
<li>Doesn’t like playing outside, or finds it difficult to catch/throw</li>
</ul>
<b>School:</b>
<ul class="ticks">
<li>Struggling in P.E</li>
<li>Difficulty catching or throwing</li>
<li>Identified to be finding movements hard to complete</li>
<li>Dislikes being trusted to carry objects from one place to another</li>
<li>Poor balance</li>
</ul>
<b>Social/behavioural:</b>
<ul class="ticks">
<li>Teased for their poor ability in lessons</li>
<li>Difficulty competing at the same level of their peers</li>
<li>Low confidence in movement activities</li>
</ul>
<h2>What exactly does DCD or Dyspraxia involve? </h2>
<br>
The exact cause of DCD or Dyspraxia is unknown, it can occur at any stage or development and for many reasons. Children constantly receive input from their senses regarding the outside world as they learn how to interact with it and its objects, a breakdown in communication between the senses on the body and the brain could lead to DCD or Dyspraxia.<br><br>
<b>Diagnosis</b><br>
Dyspraxia or DCD is diagnosed through observation and an analysis of your child’s motor function and movement ability. This is done through your GP or through a referral to a specialist.<br><br>
<b>Prevalence:</b><br>
DCD affects between 5% and 6% of all school-aged children. The ratio of boys to girls varies from 2:1 to 5:1, depending on the age group studied.<br><br>
<h2>Occupational Therapy treatment available for DCD or Dyspraxia</h2>
<br>
An occupational therapist would be able to provide effective treatment and improve the movement co-ordination difficulties associated with DCD or Dyspraxia. Some of the benefits of Occupational Therapy are:
<ul class="ticks">
<li>Improved balance</li>
<li>Improved co-ordination</li>
<li>Improved ability in P.E and other sport based activities</li>
<li>Improved hand eye co-ordination</li>
<li>Core stability improvement</li>
</ul>
Ultimately, the therapist would work collectively with you and your child with the aim of reaching the goals that you/your child would like to achieve.
<br><br>
<h2>Summary</h2>
<br>
In summary Developmental Co-ordination Disorder (DCD) or Dyspraxia is a term used to describe a delay in development of, or difficulty co-ordinating motor skills in children that directly affects the child’s ability to perform common everyday activities. An occupational therapist would be able to provide effective treatment and improve the movement co-ordination difficulties associated with DCD or Dyspraxia.
<br><br>
