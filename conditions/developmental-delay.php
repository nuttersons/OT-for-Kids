<h1>Developmental Delay</h1>
<br>
Developmental delay is a term used to describe when a child is not developing in accordance with the national average. Developmental Delay can have an adverse effect on their ability to perform meaningful activities, such as writing, drawing, tying shoe laces, dressing and playing. An occupational therapist can use activities and games to promote the skills needed to improve your child’s function within these daily activities.
<br><br>
<h2>Does your child have any of the following difficulties?</h2>
<br>
Developmental delay can be identified through comparing how your child is developing against what is considered to be the national averages for development. However, you may have some concerns as a parent regarding the development of your child. Below is a list of indicators that your child may not be developing correctly:
<ul class="ticks">
<li>Difficulty running</li>
<li>Poor P.E score</li>
<li>Difficulty doing up buttons/writing</li>
<li>Behind compared to classmates</li>
<li>Difficulty concentrating for long periods</li>
<li>Struggling to complete everyday tasks</li>
<li>Finds sport difficult/dislikes playing sport</li>
</ul>
One of these symptoms alone does not indicate developmental delay, however, noticing that a child is not performing as well as they should be can indicate an underlying condition or possibly developmental delay and is worth investigating further by speaking to one of our occupational therapists.<br><br>
<h2>How can these difficulties impact on function?</h2>
<br>
Developmental Delay can have an adverse effect on a child’s ability to function both at home, in school, and socially. An occupational therapist would be able to analyse how developmental delay is impacting on these areas and provide you with treatment to help improve your child’s ability across the home, school and social environments.<br><br>
<b>Home:</b>
<ul class="ticks">
<li>Clumsy</li>
<li>Bumping into things</li>
<li>Slow morning routine</li>
<li>Struggling to do things for themselves </li>
</ul>
<b>School:</b>
<ul class="ticks">
<li>Behind in lessons</li>
<li>Struggles in P.E</li>
<li>Un-coordinated</li>
<li>Poor handwriting</li>
</ul>
<b>Social/behavioural:</b>
<ul class="ticks">
<li>Teased for poor ability</li>
<li>Isolated/ without many friends</li>
</ul>
<h2>What exactly is Developmental Delay?</h2>
<br>
Developmental Delay can occur at any age, and to either boys or girls. The cause of which is relatively unknown, however most cases of developmental delay will be alongside a secondary condition such as Down's Syndrome or trauma during pregnancy or birth. Developmental Delay is diagnosed through speaking to your GP or being assessed by a variety of allied health professionals including occupational therapists. Developmental Delay affects around 13% of school aged children.<br><br>
<h2>Occupational therapy Treatment available for Developmental Delay</h2>
<br>
An occupational therapist can assess and treat your child in relation to developmental delay. The occupational therapist will analyse how your child completes occupations and their ability to do so in comparison to what should be expected for a child of their age. Benefits include:
<ul class="ticks">
<li>Increased gross motor skills</li>
<li>Increased fine motor skills</li>
<li>Improved scores at school</li>
<li>Increased independence</li>
<li>Confidence improvement</li>
<li>Able to compete against classmates</li>
</ul>
The occupational therapist would work together with you and your child to reach the goals that they want to achieve; this would include tailoring the therapy to meet your needs. Each of our therapists place your child at the centre of our practice and will adjust the treatment sessions to ensure they are happy with the improvements and activities they are doing at all times.<br><br>
<h2>Summary</h2>
<br>
In summary Developmental delay is a term used to describe when a child is not developing in accordance with the national average. Developmental Delay can have an adverse effect on a child’s ability to function both at home, in school, and socially. An occupational therapist can assess and treat your child in relation to developmental delay. The therapist would work together with you and your child to reach the goals that they want to achieve.<br><br>
