<h1>Rett Syndrome</h1>
<br>
Rett Syndrome is a genetic disorder that begins in early childhood, causing severe physical and mental disability and is predominantly found in girls. Rett Syndrome is due to a faulty gene needed to promote brain development. An occupational therapist would be able to work with a child with Rett syndrome in both the assessment and identification of needs and offer treatment aimed at developing the function in the home and school.<br><br>
<h2>Does your child have any of the following difficulties?</h2>
<br>
Rett Syndrome may go unnoticed for the first few months of childhood, however as the child gets older and development slows, concerns are usually raised and diagnosis begins. Some of the common symptoms of Rett Syndrome are listed below: 
<ul class="ticks">
<li>Slowness in development</li>
<li>Floppiness </li>
<li>Difficulty feeding</li>
<li>Abnormal hand movements</li>
<li>Poor social contact</li>
<li>Difficulty walking</li>
<li>Difficulty running</li>
<li>Unable to hold or carry objects</li>
<li>Poor gross motor skills</li>
<li>Problem solving difficulties</li>
<li>Range of cognitive difficulties</li>
</ul>
<h2>How can these difficulties impact on function?</h2>
<br>
The decrease in ability to move, plan and problem solve successfully can have a dramatic impact upon childhood, especially surrounding completing activities in the home, at school or bonding socially. How Rett syndrome affects function across these areas is listed below:<br><br>
<b>Home:</b>
<ul class="ticks">
<li>Carrying objects</li>
<li>Tying shoe laces</li>
<li>Listening to instructions</li>
<li>Making food and drink</li>
<li>Concentrating </li>
</ul>
<b>School:</b>
<ul class="ticks">
<li>Moving between classes </li>
<li>Poor handwriting</li>
<li>Difficulty cutting </li>
<li>Concentrating </li>
</ul>
<b>Social/behavioural:</b>
<ul class="ticks">
<li>Difficulty making friends</li>
<li>Understanding social cues</li>
<li>Understanding boundaries</li>
</ul>
<h2>What exactly is Rett Syndrome? </h2>
<br>
Children with Rett syndrome undergo four stages of the disease:
<br><br>
<b>Stage 1: early signs and slow development</b>
<ul class="ticks">
<li>Floppiness </li>
<li>Not interested in toys</li>
<li>Walking/crawling awkwardly</li>
<li>Signs of this stage usually appears in the first 6 &ndash; 18 months,</li>
</ul>
<b>Stage 2: Regression</b>
<ul class="ticks">
<li>Occurs between the ages of 1 and 4 years old </li>
<li>May last for weeks or months</li>
<li>Sudden onset of communication difficulties</li>
<li>Distressed for no known reason</li>
<li>Socially uninterested</li>
<li>Difficulty sleeping</li>
<li>Difficulty eating</li>
</ul>
<b>Stage 3 &ndash; plateau </b>
<ul class="ticks">
<li>Plateau following regression, occurring between the age of 2 and 10</li>
<li>Can last for years</li>
<li>Unable to use hands effectively</li>
<li>Teeth grinding</li>
</ul>
<b>Stage 4 &ndash; Decreased movement</b>
<ul class="ticks">
<li>Can last for a significant period of time</li>
<li>Spasticity</li>
<li>Scoliosis (bending of the spine)</li>
<li>Losing the ability to walk </li>
</ul>
Rett syndrome is caused by a faulty gene found on chromosome X. This gene is responsible for a protein that promotes brain development. Rett Syndrome causes this protein to malfunction, and subsequently movement and cognition development and ability are significantly affected. Rett Syndrome occurs mainly in girls due to the extra X chromosome found in their genetic material, and affects 1 in every 12,000 people.<br><br>
<h2>Occupational therapy treatment available for Rett Syndrome</h2>
<br>
An occupational therapist can provide effective assessment and treatment for Rett Syndrome. As Rett Syndrome progresses, the symptoms can vary in complexity and severity, an occupational therapist would be able to identify your child’s occupational needs, and from this provide treatment aimed at improving:
<ul class="ticks">
<li>Balance</li>
<li>Co-ordination</li>
<li>Fine motor skills</li>
<li>Dressing</li>
<li>Organisation</li>
<li>Concentration</li>
<li>Socialisation skills</li>
</ul>
<h2>Summary</h2>
<br>
In summary Rett Syndrome is a genetic disorder that begins in early childhood, causing severe physical and mental disability and is predominantly found in girls. An occupational therapist can provide effective assessment and treatment for Rett Syndrome, improving many aspects such as balance, co-ordination, fine motor, dressing and handwriting.<br><br>
