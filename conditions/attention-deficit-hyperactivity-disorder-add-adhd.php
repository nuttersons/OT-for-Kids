<h1>Attention Deficit (Hyperactivity) Disorder (ADD/ADHD)</h1>
<br>
Occupational therapists can provide effective advice and treatment for children with attention deficit hyperactivity disorder (ADHD). ADHD is a disorder which affects a child’s ability to sustain concentration or multi-task.
<br><br>
<h2>Does your child have any of the following difficulties?</h2>
<br>
ADHD is a condition which directly affects a child’s ability to control their actions, children with ADHD are often impulsive, don’t know their own strength and find it extremely difficult to remain focused. Some examples of the common difficulties of ADHD are listed below:
<ul class="ticks">
<li>Less able to control the amount of force required for a physical activity</li>
<li>Over use of strength</li>
<li>Overflow of movements</li>
<li>Difficulty focusing</li>
<li>Restless</li>
<li>Overactive </li>
<li>Fidgety</li>
<li>Constantly interrupting and talking over people</li>
<li>Struggles to follow rules</li>
<li>Impulsive</li>
<li>Difficulty concentrating</li>
<li>Constantly disrupts other children and doesn’t wait their turn</li>
</ul>
<h2>How can these difficulties impact on function?</h2>
<br>
Occupational therapists will observe your child and analyse how some of the difficulties that they are experiencing directly affect the function of the child within both the home and school environment.  Some examples of how ADHD impacts function are below: 
<br><br>
<b>Home:</b>
<ul class="ticks">
<li>Struggles to follow instruction</li>
<li>Doesn’t like sharing or taking turns</li>
<li>Finds it hard to concentrate on homework</li>
<li>Can be too rough with siblings</li>
<li>Difficulty sleeping</li>
</ul>
<b>School:</b>
<ul class="ticks">
<li>Doesn’t listen in class</li>
<li>Easily distracted</li>
<li>Talks over teacher/ other children</li>
<li>Doesn’t follow rules</li>
<li>Can’t sit still</li>
<li>Struggles to concentrate</li>
<li>Finds accurate movements in P.E difficult</li>
<li>Can be involved in many incidents on the playground</li>
</ul>
These are just some examples to help you identify if your child may have ADHD. An occupational therapist can help manage the symptoms of ADHD, and offer assessment and treatment sessions that improve the function of a child with ADHD.
<br><br>
<h2>What exactly is ADHD? </h2>
<br>
ADHD is a term used to describe a group of behavioural symptoms that include hyper activity, inability to concentrate and impulsiveness. Attention deficit disorder (ADD) is a sub-type of ADHD. There are three main types of ADHD.
<br><br>
<h2>Three types of ADHD </h2>
<b><br>
Predominantly Inattentive Type</b><br>
The child finds it very difficult to organise or finish a task. They find it hard to pay attention to details and find it difficult to follow instructions or conservations <br>
<br>
<b>Predominantly Hyperactive-Impulsive Type</b><br>
The child finds it hard to keep still, fidgeting often, disrupting class. Continually jumping, running or moving around. Restless and impulsive, grabbing objects and speaks at inappropriate times. Difficulty waiting turn and listening<br>
<br>
<b>Combined Type</b><br>
A child who has combined symptoms that present at equal proportions, of those of 1 and 2.<br>
<br>
<b>Cause</b><br>
The cause of ADHD is relatively unknown; however research suggests that the likelihood of ADHD can increase if another family member has been diagnosed with the condition. Research shows that if both parents have been diagnosed with ADHD the child is 4 &ndash; 5 times more likely to be diagnosed with the condition themselves.<br><br>
<b>Diagnosis</b><br>
ADHD is normally diagnosed at a young age, although in some cases in can be diagnosed much later. Most children are diagnosed through their GP<br><br>
<b>Prevalence</b><br>
ADHD affects 2-5% of the school aged population, and is more common in boys than girls.
<br><br>
<h2>Occupational Therapy treatment available for ADHD</h2>
<br>
Occupational therapists are experts at analysing how a condition is impacting upon a person’s ability to perform occupations. A child with ADHD has many difficulties surrounding concentration, memory and multi-tasking. The occupational therapist has a multitude of options and therapy treatments aimed at improving the difficulties associated with ADHD. Some of which are listed below: 
