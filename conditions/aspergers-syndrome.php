<h1>Asperger's Syndrome</h1>
<br>

Asperger’s Syndrome is a medical condition, in which children struggle to understand body language, facial expressions and other under tones to communication, have difficulty in social relationships and often have a lack of imagination or take things too literally. An occupational therapist would be able to help better manage some of these symptoms through therapy.
<br> <br>
<h2>Does your child have any of the following difficulties?</h2>
<br>
Asperger’s syndrome can often be difficult to spot in childhood, and is often mistaken for shyness. However some of the common difficulties associated with Asperger’s Syndrome are listed below:
<ul class="ticks">
<li>Often misinterprets body language and tone of voice</li>
<li>Takes phrases literally and doesn’t understand abstract concepts</li>
<li>Difficulty being creative or using imagination</li>
<li>Difficulty recognising or interpreting other people’s feelings</li>
<li>Difficulty expressing themselves</li>
<li>Using and understanding both verbal and non-verbal language </li>
<li>Expressing emotions </li>
<li>Maintaining friendships </li>
</ul>
<h2>How can these difficulties impact on function?</h2>
<br>
Asperger’s Syndrome can have a direct effect on function both at school and within the home, alongside the identified social issues that this condition presents with. Some examples of how Asperger’s syndrome is impacting upon the home; school or socially are listed below:<br><br>
<b>Home:</b>
<ul class="ticks">
<li>Struggling to communicate between family members and friends</li>
<li>Often misinterprets body language and tone of voice</li>
<li>Takes phrases literally and struggles to grasp concepts outside of logic</li>
<li>Difficulty using imagination during homework</li>
</ul>
<b>School:</b>
<ul class="ticks">
<li>Isolated or struggles in making friends</li>
<li>Difficulty following in lessons that require creative thinking such as Art or philosophy</li>
<li>Finds it hard to follow abstract concepts taught in science and similar subjects</li>
<li>Struggles to play imaginatively and often takes things too literal</li>
</ul>
<b>Social/behavioural:</b>
<ul class="ticks">
<li>Difficulty making friends</li>
<li>Takes jokes and other phrases literally </li>
<li>Can become confused when children say one thing but mean another</li>
</ul>
<h2>What exactly is Asperger’s Syndrome? </h2>
<br>
Asperger’s Syndrome is a branch of the Autistic Spectrum, and is commonly placed in the same medical bracket as that of Autism, due to the commonality between these two conditions. Both conditions result in children having difficulty building relationships, understanding and communicating and as a result are often quite isolated and withdrawn. However children with Asperger’s Syndrome tend to be highly intelligent and are often in the top percentile of their class.<br><br>
<b>Causes and diagnosis:</b><br>
The exact cause of Asperger’s Syndrome is relatively unknown. Research suggests that there is a genetic influence, however many other factors (such as environment and diet) may play a factor, none of which have been scientifically proven to cause Asperger’s Syndrome. Diagnosis usually occurs through seeing your GP, prior to a diagnostic assessment being completed by a specialist.<br><br>
<b>Prevalence:</b><br>
The prevalence of Asperger’s Syndrome is not well established, however estimates range from 1 in 250 to 1 in 5000 children. Three to four times as many males are affected than females.<br><br>
<b>Occupational Therapy Treatment available for Asperger’s Syndrome</b><br>
An occupational therapist can provide effective treatment and management of the symptoms of Asperger’s syndrome.  The treatment sessions will be aimed at improving the function of the child in social situations and learning how to understand and interpret non-verbal communication. Some of the benefits of Occupational Therapy include: 
<ul class="ticks">
<li>Improved social interaction</li>
<li>Increased understanding of body language and non-verbal communication</li>
<li>Increased confidence</li>
<li>Able to notice and manage the symptoms of Asperger’s Syndrome</li>
<li>Chance to discuss any worries or concerns you may have as a parent</li>
</ul>
