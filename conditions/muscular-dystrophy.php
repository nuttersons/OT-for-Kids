<h1>Muscular Dystrophy</h1>
<br>
Muscular Dystrophy is an inherited condition that gradually worsens, causing muscle weakness and other symptoms. There are six different types of Muscular Dystrophy all varying in symptoms and severity, these types are categorised depending upon the age at which the symptoms become prominent.<br><br>
An occupational therapist would be able to provide effective assessment and treatment for a child with Muscular Dystrophy, by first assessing the individual needs of the child, how this is impacting upon function both at home and in school.<br><br>
<h2>Does your child have any of the following difficulties?</h2>
<br>
Muscular Dystrophy varies in severity and complexity depending upon each child. Each child will vary in the presenting symptoms however; the following symptoms are typical symptoms you would expect to see in a child with Muscular Dystrophy
<ul class="ticks">
<li>Difficulty walking/late walker</li>
<li>Finds physical tasks such as climbing stairs hard</li>
<li>Have some form of learning difficulties</li>
<li>Muscle cramps</li>
<li>Muscle weakness</li>
<li>Poor handwriting</li>
<li>Unable to do buttons up or fasten shoes</li>
<li>Muscle stiffness</li>
<li>Difficulty completing sport or physical education</li>
</ul>
<h2>How can these difficulties impact on function?</h2>
<br>
The impact of Muscular Dystrophy can have a dramatic effect on a child’s ability to perform at home, in school and socially. An occupational therapist would be able to not only assess how these difficulties are impacting upon these areas but provide advice and treatment options aimed at increasing independence and happiness.<br><br>
<b>Home:</b>
<ul class="ticks">
<li>Fatigue and its impact upon completing homework/playing at home</li>
<li>Difficulty carrying food or drink</li>
<li>Difficulty getting dressed</li>
<li>Difficulty walking around the home </li>
<li>Difficulty using stairs</li>
</ul>
<b>School:</b>
<ul class="ticks">
<li>Difficulty completing work in school</li>
<li>Learning difficulties</li>
<li>Poor handwriting </li>
<li>Slow at getting changed for P.E</li>
</ul>
<b>Social/behavioural:</b>
<ul class="ticks">
<li>Extra support at school increases child’s awareness of condition</li>
<li>Teasing or bullying</li>
</ul>
<h2>What exactly is Muscular Dystrophy? </h2>
<br>
Muscular Dystrophy is caused by a fault in the gene responsible for muscular structure and function. Muscular Dystrophy is hereditary meaning it is inherited from one or both of your parents. The inherited gene is either recessive or dominant, a dominant gene will always show its characteristics, whereas a recessive gene needs two of the same faulty gene to produce the associated symptoms.<br><br>
<h2>Prevalence:</h2>
<br>
More than 70,000 children and adults in the U.K. have Muscular Dystrophy. Boys are most commonly affected, affecting around 1 in ever y 3,500 children<br><br>
<h2>Occupational therapy treatment available for Muscular Dystrophy</h2>
<br>
An occupational therapist can be able to offer support for families, training and understand to teachers and others that work with your child, and effective assessments and treatments of the condition. Some of the benefits of what OT can provide are listed below:
<ul class="ticks">
<li>Improved handwriting</li>
<li>Improved balance</li>
<li>Increased confidence</li>
<li>Specialised seating and other adaptive equipment</li>
<li>Increased independence</li>
<li>Wheelchair assessments</li>
</ul>
<h2>Summary</h2>
<br>
In summary Muscular Dystrophy is an inherited condition that gradually worsens, causing muscle weakness and other symptoms. An occupational therapist would be able to provide effective assessment and treatment for a child with Muscular Dystrophy, by first assessing the individual needs of the child, how this is impacting upon function both at home and in school. The use of Occupational Therapy for children with Muscular Dystrophy can improve their mental and physical health, improve function at home and in school and provide support and understand to parents and those that work alongside your child.<br><br>
