<h1>Hydrocephalus (Child)</h1>
<br>
Hydrocephalus is a condition in which the fluid surrounding the brain (cerebrospinal fluid) is not regulated properly, leading to excess amounts of cerebrospinal fluid in the blood vessels between brain cells. Poor fluid regulation puts an abnormal amount of high pressure on the brain tissue causing them to become damaged and subsequently affect a child’s ability to function.<br><br>
<h2>Does your child have any of the following difficulties?</h2>
<br>
Hydrocephalus leads to damage of the motor and cognitive areas of the brain, resulting in difficulty planning and executing movements. Some examples of difficulties a child with hydrocephalus may experience are:
<ul class="ticks">
<li>Unable to run/walk smoothly and continually</li>
<li>Difficulty planning movements</li>
<li>Movements look clumsy and uncoordinated</li>
<li>Constant headaches</li>
<li>Feeling sick</li>
<li>Proprioception difficulties</li>
<li>Incontinence </li>
<li>Vision difficulties </li>
<li>Enlarged head</li>
<li>Unusually large shaped head</li>
</ul>
<h2>How can these difficulties impact on function?</h2>
<br>
Hydrocephalus can have a detrimental effect on your child’s ability to function both at home and within the school environment and socially. The unusually large shaped head can also be a concern for children’s social development, as bullying can occur, and making friends can become a challenge.<br><br>
<b>Home:</b>
<ul class="ticks">
<li>Getting dressed</li>
<li>Finding clothes that fit over large head</li>
<li>Difficulty carrying objects around the home without spilling them</li>
<li>Problem solving difficulties</li>
<li>Clumsy, often bumping into things</li>
</ul>
<b>School:</b>
<ul class="ticks">
<li>Difficulty concentrating in lessons</li>
<li>Poor handwriting</li>
<li>Finds sports or P.E difficult</li>
<li>Feeling nauseous can disrupt school work</li>
</ul>
<b>Social/behavioural:</b>
<ul class="ticks">
<li>Teasing and bullying can lower your child’s confidence</li>
</ul>
<h2>What exactly is Hydrocephalus? </h2>
<br>
70% of cases of Hydrocephalus are congenital (developed during pregnancy, at time of birth or shortly after). It is a disease that affects 1 in every 500 births per year, not only this but it is common amongst young children and can occur right through life. The excess CS fluid surrounding the brain damages the delicate brain cells and causes the associated motor and cognitive difficulties. <br><br>
Hydrocephalus can be diagnosed in adults through a CT scan or a MRI scan. These scans will reveal damaged areas of the brain and if there are any obvious causes. To diagnose Normal pressure Hydrocephalus in older adults, the therapist/doctor would assess the patient looking for signs showing difficulty walking, processing mental tasks and bladder symptoms. This alongside brain scans can separate Hydrocephalus from other diseases with similar symptoms such as Alzheimer’s<br><br>
<h2>Occupational therapy treatment available for Hydrocephalus </h2>
<br>
An occupational therapist can provide effective treatment and assessment for a child with hydrocephalus, offering support and advice on managing the symptoms. Some of the benefits of Occupational Therapy are: 
<ul class="ticks">
<li>Improved balance</li>
<li>Increased independence</li>
<li>Help to plan movements</li>
<li>Increased co-ordination</li>
</ul>
<h2>Summary</h2>
<br>
In summary Hydrocephalus is a condition in which the brain is damaged due to excess cerebrospinal fluid (protective fluid around the brain) which results in motor and cognitive planning and execution difficulties. An occupational therapist will provide the person with techniques and strategies and provide adaptive equipment on how to cope with the change in function following Hydrocephalus, these techniques will increase the child’s independence and maximise their ability to complete daily occupations.<br><br>
