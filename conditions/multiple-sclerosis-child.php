<h1>Multiple Sclerosis (Child)</h1>
<br>
Multiple Sclerosis is a neurodegenerative condition which affects the protective shell surrounding nerves. The damage to the protective shell leads to a slower electrical impulse between the central nervous system and the muscles, resulting in a variety of muscular difficulties, fatigue and memory problems. An occupational therapist can provide effective treatment to combat the degeneration of function, working to improve the independence of your child and helping them reach their goals.<br><br>
<h2>Does your child have any of the following difficulties?</h2>
<br>
A child with Multiple Sclerosis will often experience some, if not all of the following symptoms:
<ul class="ticks">
<li>Seizures</li>
<li>Lethargic and slower to perform tasks</li>
<li>Fatigue</li>
<li>Muscle weakness</li>
<li>Unable to carry drinks/heavy objects</li>
<li>Numbness </li>
<li>Pain </li>
<li>Difficulty walking</li>
<li>Poor co-ordination</li>
</ul>
<h2>How can these difficulties impact on function?</h2>
<br>
Occupational therapy not only analyses how the symptoms of multiple sclerosis are impacting your child, but relate it to how they find completing the tasks they have to do on a daily basis, both at school and in the home. Some of the difficulties are:<br><br>
<b>Home:</b>
<ul class="ticks">
<li>Carrying food or drink around the house</li>
<li>Often tired and too fatigued to do homework or after school activities</li>
<li>Clumsy </li>
<li>Extremely fatigued following a days’ activity (at school or outside)</li>
</ul>
<b>School:</b>
<ul class="ticks">
<li>Difficulty concentrating in school due to fatigue</li>
<li>Struggling in P.E or other sport activities</li>
<li>Lethargic and tired,</li>
<li>Difficulty coping with a demanding full day of school</li>
</ul>
<b>Social/behavioural:</b>
<ul class="ticks">
<li>Awareness of difference between themselves and other children</li>
<li>Difficulty playing games / on the playground due to fatigue</li>
</ul>
<h2>What exactly is Multiple Sclerosis? </h2>
<br>
Multiple Sclerosis exists in three types, all of which are progressive. A progressive disorder means that the condition will worsen over time and consequently so will the symptoms, this progression will have a dramatic effect on the child’s ability to function.<br><br>
<h2>The three types of Multiple Sclerosis are:</h2>
<br>
Relapse Remitting <br>
Secondary progressive<br>
Primary progressive<br>
<br>
Relapse remitting means that your child will experience times where the symptoms are worse and therefore are having a great impact on function, followed by times where the symptoms subside and function improves with this cycle containing throughout the child’s life. Secondary progressive is a stage of Multiple Sclerosis that can follow after Relapse remitting, meaning there is a sustained relapse with no sign of a remission. Primary progressive is a continuous degeneration of function, with no remission. The condition slowly worsens, affecting the quality of life of the child dramatically.<br><br>
<h2>Cause and Diagnosis </h2>
<br>
Currently the cause of Multiple Sclerosis is relatively unknown; however research is being conducted and hopes to resolve this issue soon. Diagnosis of Multiple Sclerosis in children is extremely difficult, due to the symptoms having high similarities with other childhood conditions<br><br>
<h2>Prevalence:</h2>
<br>
Multiple Sclerosis in children is rare, but this is thought to the difficulty in diagnosing the condition as a child rather than a low occurrence.<br><br>
<h2>Occupational therapy treatment available for Multiple Sclerosis </h2><br>
An occupational therapist can provide various therapy options for a child with Multiple Sclerosis. Fatigue is usually the biggest threat to participating in occupations, affecting nearly 80% of all children. The occupational therapist can offer advice and support on ways to effectively manage fatigue and other symptoms of Multiple Sclerosis. The benefits of Occupational Therapy are listed below:
<ul class="ticks">
<li>Pacing</li>
<li>Energy conservation </li>
<li>Discuss ways to better manage the challenging school curriculum </li>
<li>Offer advice and education on the available adaptive equipment </li>
<li>Time management and help in accessing facilities</li>
</ul>
<h2>Summary</h2>
<br>
In summary Multiple Sclerosis is a neurodegenerative condition which affects the protective shell surrounding nerves. Occupational therapy not only analyses how the symptoms of multiple sclerosis are impacting your child, but relate it to how they find completing the tasks they have to do on a daily basis, both at school and in the home. An occupational therapist can provide various therapy options for a child with Multiple Sclerosis.<br><br>
