<h1>Spina Bifida</h1>
<br>
Spina bifida is a fault in the development of the spine and spinal cord, often solved through either prenatal surgery or surgery very soon after birth. The health complications depend upon the area of the spine that is affected, but mainly surround both cognitive and mobility dysfunction. Hydrocephalus is closely linked to Spina Bifida and can also be a common occurrence. An occupational therapist can provide effective assessment and treatment for a child with Spina Bifida; the therapist will assess the specific needs of your child and advise a unique treatment plan with the aim of maximising the quality of life of your child.<br><br>
<h2>Does your child have any of the following difficulties?</h2>
<br>
Spina Bifida can have a dramatic effect on function throughout childhood and later life, depending upon the area affected in development, symptoms vary greatly. Below is a list of some of the symptoms of Spina Bifida:
<ul class="ticks">
<li>Short attention span</li>
<li>Difficulty solving problems</li>
<li>Concentration difficulties</li>
<li>Memory difficulties</li>
<li>Difficulty organising themselves</li>
<li>Language difficulties</li>
<li>Poor handwriting</li>
<li>Poor fine motor skills </li>
<li>Difficulty dressing, doing up buttons and undoing zips</li>
<li>Movement difficulties</li>
<li>Scoliosis (curvature of the spine) </li>
<li>Dislocated Joints</li>
</ul>
<h2>How can these difficulties impact on function?</h2>
<br>
Spina Bifida can have a dramatic effect on function and can often result in difficulties at home, in school and socially, depending upon the area which has been poorly developed. Some examples of how Spina Bifida can affect function at home, in school or socially are listed below: <br><br>
Home:
<ul class="ticks">
<li>Difficulty eating, dressing and sleeping</li>
<li>Inability to follow instructions or concentrate for long periods</li>
<li>Forgetful and often doesn’t remember the day’s events or who may be picking them up</li>
</ul>
School:
<ul class="ticks">
<li>Finds it hard to concentrate in class</li>
<li>Difficulty moving between classrooms and participating in P.E</li>
<li>Poor handwriting affecting school work</li>
</ul>
Social/behavioural:
<ul class="ticks">
<li>Difficulty making and keeping friends</li>
<li>Behavioural difficulties/acting out at school due to cognitive dysfunction</li>
</ul>
<h2>What exactly is Spina Bifida?</h2>
<br>
Spina Bifida affects 1 in every 2500 new born babies and is caused by faulty development of the spine during pregnancy. Research has shown that a lack of Folic acid, obesity or smoking during pregnancy can increase the chances of giving birth to a child with Spina Bifida.<br><br>
<h2>Occupational therapy treatment available for Spina Bifida</h2><br>
An occupational therapist can assess the specific difficulties your child with Spina Bifida may be experiencing and provide a tailored treatment session aimed at improving the function of your child across the many fundamental areas of childhood. Below is a list of some of the aspects an occupational therapist can help with:
<ul class="ticks">
<li>Concentration difficulties</li>
<li>Trouble solving problems</li>
<li>Memory difficulties</li>
<li>Poor organisational skills</li>
<li>Poor handwriting</li>
<li>Taking a long time to get dressed</li>
<li>Finds fiddly tasks extremely difficult</li>
<li>Movement difficulties (walking, running, jumping etc.)</li>
</ul>
