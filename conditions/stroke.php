<h1>Stroke</h1>
<br>
Stroke is a neurological condition which is caused by an impairment of blood supply resulting in dysfunction and tissue death in the brain. Depending on the area of the brain struck during stroke, many functional limitations may occur. For a child this can have a dramatic effect on development of skills. An occupational therapist can provide effective assessment and treatment using evidenced based techniques and solutions to improve the functional ability of your child both at school and at home.
<br><br>
<h2>Does your child have any of the following difficulties?</h2>
<br>
Stroke can occur quickly and it is highly important to act as soon as possible. Time is brain. The main signs of a stroke are identified in the anagram FAST &ndash; Face, Arms, Speech and Time, if you notice any instant change in any of these aspects then your child may be experiencing a stroke. Following the initial impact of stroke, the neurological damage done can often be seen once home. Some of the long term symptoms of stroke are:
<ul class="ticks">
<li>Poor fine motor skills </li>
<li>Difficulty jumping</li>
<li>Difficulty processing instructions or spoken word</li>
<li>Word finding/association difficulties</li>
<li>Difficulty walking, moving smoothly</li>
<li>Poor concentration</li>
<li>Difficulty planning and executing movements</li>
</ul>
The list above is a snapshot of what the effects a complex and challenging condition like stroke can be. An occupational therapist would be able to identify the difficulties your child may be experiencing in relation to completing occupations and provide treatment aimed at improving these difficulties.
<br><br>
<h2>How can these difficulties impact on function?</h2>
<br>
Stroke can have a dramatic effect on all aspects of childhood. None more so than the impact on the school, home and social aspects, below are some examples of how Stroke impacts upon these areas:<br><br>
<b>Home:</b>
<ul class="ticks">
<li>Difficulty dressing independently</li>
<li>Trouble eating or feeding themselves</li>
<li>Difficulty concentrating or following instructions</li>
</ul>
<b>School:</b>
<ul class="ticks">
<li>Learning difficulties</li>
<li>Poor concentration </li>
<li>Needing support during lesson times</li>
</ul>
<b>Social/behavioural:</b>
<ul class="ticks">
<li>Stigma attached to being different</li>
<li>Difficulty making friends</li>
</ul>
<h2>What exactly is Stroke?</h2>
<br>
Stroke is caused by a lack of oxygen to the brain, due to a blood clot or leak into delicate brain tissue. There are two types of stroke: haemorrhagic and ischemic. A haemorrhagic stroke is caused by a ruptured blood vessel, however roughly 80% of all stokes are ischemic. The main causes of an ischemic stroke is either a thrombosis (obstruction of a blood vessel by a locally formed blood clot) or an embolism (obstruction of a blood vessel by a clot formed somewhere else. Stroke is the third biggest killer in the U.K.
<br><br>
<h2>Occupational therapy treatment available for Stroke</h2>
<br>
An occupational therapist can assess the individualised needs of your child, discuss with you the implications of a stroke (both short term and long term) and offer effective treatment which can help your child cope with the implications of having a stroke. 
<ul class="ticks">
<li>Motor development</li>
<li>Confidence</li>
<li>Parental understanding of stroke and its long term implication</li>
<li>Cognitive functional improvement</li>
</ul>
