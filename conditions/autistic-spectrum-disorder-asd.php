<h2><a id="1" name="1"></a>Does your child have any of the following difficulties?</h2>
<br>
Autism is what’s known as a ‘spectrum’ disorder, affecting different children in different ways. Children with autism are extremely unique and may experience few or many of the common symptoms below.
<ul class="ticks">
<li>Struggles to understand body language</li>
<li>Finds social situations daunting or unusual</li>
<li>Prefers things to be organised or done a certain way and becomes agitated to change</li>
<li>Has difficulty organising and planning for school work</li>
<li>Doesn’t have many friends at school</li>
<li>Dislikes change in routine and becomes upset if sudden changes occur</li>
<li>Christmas and other holidays are unnerving for your child</li>
<li>Takes pictures and phrases too literally</li>
<li>Needs to know an exact plan of the day’s events and gets upset if things change unexpectedly</li>
<li>Dislikes meeting new people</li>
<li>Little or no awareness of danger</li>
<li>Occasionally runs away from home or school or when out in the community</li>
<li>Distracted by blinds or enjoys moving the head to create flickering sunlight</li>
<li>Finds patterned carpets or floors confusing to navigate across</li>
</ul>
It is also common for a child within the autistic spectrum to be either over-reactive to sensory responses (hypersensitive) or under-reactive (hyposensitive) alongside the usual symptoms above.  It is likely that your child will experience just one type of sensory responses to light, heat, sound smell or taste. However it could be that your child is hypersensitive to light but hyposensitive to touch.
<br><br>
<h2><a id="2" name="2"></a>Hyper-Sensitivity</h2>
<ul class="ticks">
<li>Finds normal background sounds unbearably loud or distracting </li>
<li>Over sensitive to heat, light, sounds or smells</li>
<li>Prefers to focus on the detail rather than the whole </li>
<li>Distorted vision, objects and bright lights can appear to jump around </li>
<li>Likes spicy food</li>
<li>Eats everything (grass, soil, play-dough etc.)</li>
<li>Touch can be painful or uncomfortable </li>
<li>Dislikes wearing shoes, socks or gloves </li>
<li>Struggles with activities like sport</li>
<li>Difficulty stopping quickly or movements overflow</li>
<li>Poor fine motor skills</li>
<li>Moves whole body to look at something</li>
</ul>
<h2><a id="3" name="3"></a>Hypo-Sensitivity</h2>
<ul class="ticks">
<li>Holds others tightly </li>
<li>High pain threshold </li>
<li>A central object is magnified but things on the periphery are blurred </li>
<li>Poor throwing or catching (depth perception) </li>
<li>Under-sensitive to heat, may not recognise when an object is too hot </li>
<li>Poor sense of smell, fail to notice own body odour </li>
<li>Dislikes people with distinct perfumes </li>
<li>Enjoys rocking spinning around at a fast pace</li>
<li>Bumps into people/objects often</li>
<li>Stands too close to others</li>
<li>Finds it hard to navigate rooms </li>
<li>Simple and effective</li>
</ul>
<h2><a id="4" name="4"></a>How can difficulties experienced by children with autism impact on function and routine?</h2>
<br>
Autism can have a profound effect on a child’s routine. The simple tasks that every day involves are difficult and require a high level of attention and effort. Some of the common difficulties that children with autism experience are listed below: 
<br><br>
<b>Home:</b>
<ul class="ticks">
<li>Difficulty understanding instructions</li>
<li>Can be overwhelmed by the smell of dinner cooking</li>
<li>Becomes easily upset if surroundings change (at holidays or if new furniture arrives)</li>
<li>Having guests over can be difficult</li>
<li>Changing perfume or washing powder can result in child becoming agitated</li>
<li>Difficulty sleeping in early hours of morning when the sun rises through the curtains</li>
</ul>
<b>School:</b>
<ul class="ticks">
<li>Classroom environment can become overwhelming and cause distress</li>
<li>Routine changes can be difficult for your child to deal with</li>
<li>Change in teacher can cause significant distress</li>
<li>Can be fixated on displays and or objects within the classroom</li>
<li>Struggles to make friends </li>
<li>Difficulty completing activities in P.E or playground </li>
<li>Finds it hard to navigate corridors, rooms and the school environment</li>
</ul>
An autistic child can find school and home life extremely difficult to comprehend. The occupational therapist will work together with your child to provide effective assessment and treatment aimed at improving quality of life.
<br><br>
<h2><a id="5" name="5"></a>What exactly is Autism? </h2>
<br>
Autism is a term used to describe children and adults who see the world differently to the rest of us; it is medically termed under the Autistic Spectrum. The Autistic Spectrum is a term used to describe both Autism and Asperger’s Syndrome.<br><br>
<b>Causes:</b><br><br>
The cause of Autism is relatively unknown. However it is thought that the combination of a variety of genetic and factors can lead to autism. Genetic mutations from parents to child could make them more vulnerable to developing autism, as well as if there is a older child with autism, the new born becomes 5-6% more likely to be autistic. Although researchers have not yet found any specific genes that relate to autism. <br><br>
Environmental factors are also thought to be a factor, the three main influences determined from research are:
<ul class="ticks">
<li>Smoking in early pregnancy, daily smoking in early pregnancy results in a 40% increase of likelihood in giving birth to an autistic child.</li>
<li>A viral or bacterial infection during pregnancy increases the likelihood of giving birth to an autistic child by 7% </li>
<li>New fathers over 40 are estimated to be six times more likely to father a child with autism</li>
</ul>
Psychological factors are also thought to influence the development of autism. As children develop they learn that other people have minds much like their own, including their own wishes, wants, feelings, thoughts and beliefs. This is termed the theory of mind. Children with autism have a limited or no understanding of this theory; this is thought to be the route of most of their problems.
<br><br>
<h2><a id="6" name="6"></a>Diagnosis</h2>
<br>
The first stage of a diagnosis is to speak to your GP about some of your concerns. Parents tend to notice the signs of autism before a professional is involved. The GP would complete an initial Autism screening tool, such as CHAT (checklist for autism in toddlers), if this shows that your child could have autism, the GP will then refer you onto a specialist in autistic diagnosis. However waiting lists in the NHS are usually lengthy, therefore by seeing a private practitioner, a diagnosis can be given quicker and therefore treatment can start sooner.
<br><br>
<h2><a id="7" name="7"></a>Prevalence:</h2>
<br>
Autism affects around 2% of school aged childhood population as of 2012 (National Centre for Statistics of Health).
<br><br>
<h2><a id="8" name="8"></a>Occupational therapy treatment available for Autism </h2>
<br>
Occupational therapists have the knowledge and skills to provide effective treatment and management of the complex issues that a child with autism experience on a daily basis. Some of which are listed below:
<ul class="ticks">
<li>Sensory Integration </li>
<li>Education and support for living with a child with Autism</li>
<li>Home adaptations to improve sensory feedback</li>
<li>School visits to improve learning environment at school</li>
<li>Social improvement therapy, aimed at improving social understanding and body language</li>
<li>Analysis of routine and offer advice on how this can be adapted to help your child deal with any changes</li>
<li>Adaptive aids and equipment advice, such as weighted jackets for sensory support</li>
</ul>
The OT would be able to assess and provide treatment for your autistic child on a range of their symptoms. The therapist would analyse how your child copes across all areas of their life, be it in school, at home or regarding routines on a daily basis. They would work collaboratively with you in setting goals that both you and your child want to achieve and apply evidenced based techniques to provide treatment aimed at meeting these goals and most importantly improving function in your child in order to make your day to day life easier.
