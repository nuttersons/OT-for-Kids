<h1>Parents</h1>
<br>
OT for Kids work with parents who may be concerned about their child’s development, or who may have been told to contact an occupational therapist following a parents evening at school, or who have noticed that their child is finding tasks difficult compared to their classmates. Our occupational therapists will be able to provide effective assessment and treatment for your child. We offer:<br><br>
<h2>Why choose us?</h2>
<br>
Being a specialised private paediatric Occupational Therapy company, we are highly experienced in regularly working with parents in providing the best care and support for their child. Some of the many benefits of working with us are:
<ul class="ticks">
<li>Experienced Occupational Therapists</li>
<li>Flexible appointments available 7 days a week</li>
<li>Outcome based services</li>
<li>Written quotations available upon request</li>
<li>Individual treatment available in clinic, at home and at school</li>
<li>Both individual or group therapy input available</li>
<li>Detailed individual reports outlining assessment findings, summary of progress made and further recommendations</li>
<li>Detailed home activity programmes provided at monthly reviews</li>
</ul>
OT for Kids also regularly work with private medical insurers in providing effective assessment and treatment. Please check you policy to ensure you are covered if you are using a private medical insurer.
<h2>Services</h2>
<h2>One on One treatment</h2>
<br>
The occupational therapist would be able to provide one on one treatment to your child. The therapist can come out to your home, into school or meet you at one of our clinics, whichever is easiest for you.<br><br>
<h2>Groups</h2>
At OT for Kids, we can provide group services to children with similar difficulties, ages and abilities. The groups we currently offer are:
<ul class="ticks">
<li><a href="/website69b/groups/in-clinic/handwriting-improvement-group.php"><b>Handwriting</b></a> &ndash; a group aimed at children who struggle with handwriting</li>
<li><a href="/website69b/groups/in-clinic/movement-and-co-ordination-group.php"><b>Movement</b></a> &ndash; To improve gross motor skills, for conditions like Developmental Co-ordination Disorder and otherwise movement impaired children</li>
<li><a href="/website69b/services/treatment/confidence-improvement-therapy.php"><b>Confidence</b></a> &ndash; for those with low confidence</li>
<li><a href="/website69b/groups/in-clinic/concentration-and-memory-group.php"><b>Concentration</b></a> &ndash; for children who are easily distracted and often disruptive.</li>
</ul>
