<h2>Discuss a particular case</h2>
<br>
The occupational therapist can arrange to meet yourself to discuss any concerns you may have over a particular case. This could include a home or school visit for safety and access to services, or treatment needed following an accident or injury. We offer the following services<br><br>
<h2>One on One treatment</h2>
<br>
The occupational therapist would be able to provide one on one treatment in the child’s school, at home or in one of our clinics.<br><br>
<h2>Groups</h2>
<br>
At OT for Kids, we can provide group services to children with similar difficulties, ages and abilities. The groups we currently offer are: 
<ul class="ticks">
<li><a href="/website69b/groups/in-clinic/handwriting-improvement-group.php"><b>Handwriting</b></a> &ndash; a group aimed at children who struggle with handwriting</li>
<li><a href="/website69b/groups/in-clinic/movement-and-co-ordination-group.php"><b>Movement</b></a> &ndash; To improve gross motor skills, for conditions like Developmental Co-ordination Disorder and otherwise movement impaired children</li>
<li><a href="/website69b/services/treatment/confidence-improvement-therapy.php"><b>Confidence</b></a> &ndash; for those with low confidence</li>
<li><a href="/website69b/groups/in-clinic/concentration-and-memory-group.php"><b>Concentration</b></a> &ndash; for children who are easily distracted and often disruptive.</li>
</ul>
<h2>Access Audits</h2>
<br>
We would also be able to come out to the child’s school and determine if it is safe and accessible to all children. This is in line with the Disability Discrimination Act 1995.<br><br>
<h2>Home Visits</h2>
<br>
The therapist would be able to come out to a particular child’s home to assess for functional level or to ensure safe living. Following assessment, the therapist can complete treatment in the child’s home.<br><br>
<h2>School Visits</h2>
<br>
The occupational therapist can also complete any assessment and treatment within the school.<br><br>
<h2>Assessments</h2>
<br>
OT for Kids can assess for a range of difficulties, the occupational therapist would use this process to gain a better insight into the difficulties the child is experiencing, determine why this might be and use the assessment as the basis for the therapy treatment sessions.<br><br>
The assessments we provide are:
