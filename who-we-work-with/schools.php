<h1>Schools</h1>
<br>
OT for Kids work alongside schools in providing the best environment for children to learn, grow and develop. We work with a number of schools currently, including supporting children on a one to one basis and working in small groups with the Special Educational Needs Officer (SENCO). These groups have been specifically designed for children who struggle with handwriting, concentrating in class, P.E or sport lessons (movement) and difficulty dressing or using scissors (fine motor skills). These are the services we provide:<br><br>
<h2>Why choose us?</h2>
<br>
OT for Kids regularly work with schools in providing a wide variety of services. We understand what is required and are extremely flexible in working together. See more of the benefits below:
<ul class="ticks">
<li>Experienced Occupational Therapists who regularly work with schools</li>
<li>Flexible in time of groups appointments of space available</li>
<li>All of our services are outcome based and provide clear justification for cost</li>
<li>Written quotations available upon request</li>
<li>10% discount available for year contracts of OT groups</li>
<li>Regular update meetings with SENCO / Key member of staff</li>
<li>Detailed individual reports outlining assessment findings, summary of progress made and further recommendations for school</li>
<li>Detailed appendices accompanying reports to be used as hand-outs with teachers and teaching assistants</li>
<li>Competitive prices to allow access to all schools</li>
<li>Staff training available </li>
</ul>
Along with all these fantastic benefits, we also offer a wide variety of services to schools, below is a list of services we offer to schools:

<h2>One on One treatment</h2>
<br>
The occupational therapist would be able to provide one on one treatment and support in school, working with teachers and teaching assistants in providing therapy that matches the curriculum.<br><br>
<h2>Discuss a particular case</h2>
<br>
The occupational therapist can also arrange to meet yourself, either in school, or at one of our clinics to discuss any concerns you may have over a particular case. This could be something you have observed and are not sure how to address.<br><br>
<h2>Groups</h2>
<br>
At OT for Kids, we can provide group services to children with similar difficulties, ages and abilities. We are able to offer a day of groups, whereby the occupational therapist will come into school to run up to 4 of the following groups:
<ul class="ticks">
<li><b><a href="/website69b/groups/ot-groups-for-schools/concentration-group.php">Concentration Group</a></b> &ndash; for children who are easily distracted and often disruptive.</li>
<li><b><a href="/website69b/groups/ot-groups-for-schools/fine-motor-group.php">Fine Motor Group</a></b> - The fine motor group is aimed at children who are having difficulty cutting, threading and completing tasks that involve the smaller movements of the fingers.</li>
<li><b><a href="/website69b/groups/ot-groups-for-schools/handwriting-group.php">Handwriting Group</a></b> &ndash; a group aimed at children who struggle with handwriting</li>
<li><b><a href="/website69b/groups/ot-groups-for-schools/movement-group.php">Movement Group</a></b> &ndash; To improve gross motor skills, for conditions like Developmental Co-ordination Disorder and otherwise movement impaired children</li>
<li><b><a href="/website69b/groups/ot-groups-for-schools/organisation-group.php">Organisation Group</a></b> - a group designed to improve the ability of children to organise and plan their thought processes, diary, homework schedules and everyday tasks such as packing a bag and money management.</li>
</ul>
These groups are a great opportunity to work together with an occupational therapist and not only provide effective treatment in group settings for children who are behind the curriculum but for also as an opportunity to learn how Occupational Therapy can help both mainstream children and children with special educational needs.<br><br>
<h2>Teacher Training</h2>
<br>
We are also able to offer training sessions for teaching staff. The topics that would be covered is down to the specific school and level of teaching staff. We would be able to come into school to provide this service, this could be education around a particular condition, or how to improve the learning ability of certain children, or any other issue you feel needs addressing.<br><br>
<h2>Access Audits</h2>
<br>
We would also be able to come out to the school and determine if it is safe and accessible to all children, if you are in the process of re-designing the school, having an extension or completing any building work that will change the access to the building we would advise an occupational therapist complete an access audit, this is in line with the Disability Discrimination Act 1995.<br><br>
