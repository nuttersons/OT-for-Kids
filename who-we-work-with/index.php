<h1>Who We Work With</h1>
<br>

OT for Kids provide private Occupational Therapy services to many different individuals and organisations. We commonly provide services to: 
<ul>
<li><a href="/website69b/who-we-work-with/parents.php">Parents</a><br>
We work with parents who may be concerned about how their child is performing at school or home<br>
<br>
</li>
<li><a href="/website69b/who-we-work-with/schools.php">Schools</a><br>
We work with schools in providing both 1:1 support and group therapy, offering handwriting, movement concentration and fine motor improvement groups.<br>
<br>
</li>
<li><a href="/website69b/who-we-work-with/insurers.php">Insurers </a><br>
We work with insurers and insurance companies to provide the necessary assessment and treatment that this child may require. Our reports are always of a high quality and returned as quickly as possible.<br>
<br>
</li>
<li><a href="/website69b/who-we-work-with/case-managers.php">Case managers</a><br>
We work with case managers in ensuring the best level of care and support possible for the child involved.<br>
<br>
</li>
<li><a href="/website69b/who-we-work-with/solicitors.php">Solicitors</a><br>
We work with solicitors when on complex court cases and medico legal reports. All of our expert witnesses are highly experienced within paediatric OT.<br>
<br>
</li>
<li><a href="/website69b/who-we-work-with/organisations.php">Organisations</a><br>
We work together with organisations in providing an overall package of care and treatment for every child.<br>
<br>
</li>
<li><a href="/website69b/who-we-work-with/charities.php">Charities</a><br>
We work with charities in supporting and enabling children back into society and developing the skills they need as they grow.</li>
</ul>
