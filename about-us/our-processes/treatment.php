<h1>Treatment</h1>
<br><h2>Why choose OT for Kids for your child’s treatment?</h2>
<br>
The treatment stage is where the therapist will make effective and important progress with your child. This is where you will start to notice improvements first hand, and see the positive impact that paediatric Occupational Therapy can have for your child.<br>
<br>
Core aspects of all of our interventions:<br>
<br>
<b>Person centred therapy</b> &ndash; we always work together with you and your child to ensure you are at the centre of our therapy.<br>
<br>
<b>Holistic approach</b> &ndash; by taking a holistic approach, we don’t just assess or treat one aspect of your child’s life, we look at how the condition is impacting across all areas, occupations and environments and how we can improve this.<br>
<br>
<b>Motivating therapy sessions</b> &ndash; We understand how important (and sometimes difficult) it is to get your child to be focussed and motivated to do something beneficial for them. Therefore all of our interventions are based around play therapy so that we get the most out of your child and you get the most for your child from our sessions.<br>
<br>
Benefits of Occupational therapy treatment for children include:<br>
<ul>
<li>No waiting lists</li>
<li>Set results</li>
<li>Positive outcomes</li>
<li>Flexible appointments</li>
<li>Professional </li>
<li>Registered therapists with both the Health Professions Council (HPC) and British Association of Occupational Therapy (BAOT) </li>
<li>Criminal Record Bureau checked therapists</li>
<li>Detailed reports with quick turn around</li>
<li>Fully trained and experienced therapists in child therapy<br>
</li>
</ul>
<br>
<h2>Where?</h2>
<br>
Therapy interventions usually take place at home, at school or in one of our clinics, whichever is most convenient for you.  They can be completed on a 1:1 basis or in a group situation. Groups run weekly and are mostly a set length course aimed at improving a particular aspect of your child’s development <br>
<br>
