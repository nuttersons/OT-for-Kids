<h1>Therapy Intervention Planning</h1>
<br>Intervention planning and interventions are terms used by therapists to describe the collaboratively set plan that is needed to help your child attempt to reach their goals. The intervention describes the physical treatment activities that promote the skills needed to reach these goals.<br>
<br>
The overall aim of the intervention stage is to maximise independence and improve the health and wellbeing of your child, ensuring that they reach their goals. It will be based on the occupational needs of your child previously identified through the assessment and problem formulation stage of the Occupational Therapy process.<br>
<br>
During Intervention planning, the therapist will work together with both you and your child to decide what route future treatment sessions will take. For example, a child with dyspraxia would struggle to co-ordinate movements smoothly and effectively, and they may struggle to walk along a balance beam/small wall without falling off. The therapist would choose and discuss with you the most suitable approaches for treatment. This may include a combination of approaches or one individual framework.<br>
<br><br>
<h2>Possible approaches to therapy:</h2>
<br>
<b>Bottom up</b> therapy is an approach that focuses on improving the skills underlying occupations. The theory is that by completing similar occupations that require similar skills the child will acquire and develop the set skills needed to perform well in the identified task. For the child with dyspraxia any balance games or core strengthening activities would improve the underlying strength and balance needed to walk unaided along a wall.<br>
<br>
<b>Top down</b> theory takes the opposite approach and focuses on purely the task and the environment that influence the occupation. The theory is that instead of improving the characteristics of the condition (as in bottom up). Top down theory looks at the surrounding environment, the task itself and how these variables can be graded and adapted to improve occupational performance.<br>
<br>
<b>The Compensatory approach</b> aims to maximise your child’s independence through the use of adaptive aids and equipment by improving the environment in which your child completes the activity. For example, a child struggling with handwriting could benefit from a supportive chair or a sloped writing board because posture plays an important role in handwriting; these adaptive aids help place the child in the correct posture for writing.<br>
<br>
<b>The Restorative approach</b> focuses on improving on the function your child may have lost due to the condition rather than compensating through using the function he/she currently has. The theory is that by focusing on the injured/weaker limb during therapy, the brain will learn to rewire its circuitry to improve the motor planning and execution of the injured limb. This is what is known as neuroplasticity <br>
<br>
<b>Groups</b> At OT for Kids we are able to offer group services. There is a great deal of evidence to support the use of groups in child therapy. The theory is that by placing your child in amongst other children of a similar ability and struggle with completing similar activities, they will be able to mirror behaviours and learn from their peers alongside learning from the fun activities that the lead therapist will provide. Group theory is a great way to boost confidence, self-belief and improve function in a fun, friendly and engaging environment<br><br>
