<h1>Goal Setting</h1>
<br>Goal setting is the next stage in the Occupational Therapy process. Once the problem list has been formulated, our therapist will help both you and your child decide which of these problems are the most important, most urgent and most influential in ensuring your child leads a happy life.<br>
<br>
Once these have been prioritised, collaborative goals will then be set with the aim of improving these main problems and the quality of life for your child.<br>
<br>
For example, your child may find it hard to do up buttons or zips/trouble handwriting or has difficulty concentrating, and you may have noticed that he is not functioning at the same level of his peers. The therapist will then formulate a way to accurately measure the changes that will occur during treatment through what is known as SMART goal setting. <br>
<br>
<b>Specific</b> &ndash; A goal that is clearly defined through achieving a consistent task<br>
<b>Measurable</b> &ndash; A goal that can be measured through performance in activities<br>
<b>Accurate</b> &ndash; A goal that is accurately recorded through outcome based assessments across treatment<br>
<b>Realistic</b> &ndash; A goal that is achievable through hard work<br>
<b>Time-bound</b> &ndash; A set realistic time frame to achieve this goal<br>
<br>
SMART goal setting is used by all professions and successful individuals. They are written and recorded so we do not lose focus on what we want to achieve. They are a reference for the child to see improvements and improve self-esteem and believe that they are no different from anyone else.<br><br>
