<h1>Outcome measurement</h1>
<br>An outcome measurement is simply an evaluation of progress. To ensure that your treatment is precise and clear, every session is recorded and accurately measured in note format and a re-assessment or evaluation of progress is carried out at regular intervals to demonstrate change in your child’s ability over time.<br>
<br>
Every single one of our therapist will use an effective method of re-assessment that will produce a clear, detailed and accurate analysis of the progression your child is making during therapy. This is called outcome measurement. <br>
<br>
For example, a child with developmental co-ordination disorder may have been initially assessed by using the movement ABC assessment tool. This is a standardised tool, recognised by the college of occupational therapists as being an accurate, rigid and reliable way of recording occupational performance. <br>
<br>
Following treatment the therapist will use this tool again (several weeks after the initial assessment) to determine how effective the therapy is on your child’s developments. The results of this standardised test will then be analysed and compared to the national average and to previous results. This comparison will give you and your therapist a clear idea of how the child is progressing in therapy and what areas to address next.<br><br>
