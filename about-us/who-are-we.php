<h1>Who We Are</h1>
<br>
OT for Kids are an independent, private Occupational Therapy company offering high quality Occupational Therapy service to improve the lives of children. <br>
<br>
We believe that every child deserves the right to play, socialise and be given the tools needed to develop their skills so that they may continue to go on to be something truly remarkable. <br><br>
All of our therapists have paediatric Occupational Therapy experience and are 100% committed to providing you with the latest in therapy treatment options, detailed assessments of the difficulties your child may be experiencing and offer support and advice to ease the worries that every parent naturally has about the development of their child. <br><br>
To ensure our work is at the highest standard, all of our therapists are hand-picked and undergo regular appraisals, reviews and reflections on how better they can improve their practice. We provide an extensive range of effective assessment and treatment for a range of conditions and difficulties, and work with a wide range of conditions, problems and organisations.
