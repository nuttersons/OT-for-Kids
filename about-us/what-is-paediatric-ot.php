			  <h1>What  is Paediatric Occupational Therapy?</h1>
		<br>
Paediatric Occupational Therapy encompasses  working with children aged from 0 &ndash; 18, covering a wide range of conditions and  problems to promote their function in relation to activities they complete.  Some of the conditions we work with include Autism, Attention Deficit Hyper  Activity Disorder, Dyspraxia and Stroke, working to improve function in  activities such as writing, dressing, fine motor and gross motor activities and  self-care based activities.<br>
<br>
Our aims are to enable your child to live as  happy and independently as possible. We meet goals that YOU set through  meaningful activities that children enjoy doing.<br> 
<br>
By providing children with therapy based around activities they find  meaningful, they are more likely to make faster improvements.<br>
