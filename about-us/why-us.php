<h1>Why choose OT for Kids</h1>
<br>
OT for Kids are an independent private company and set extremely high standards that all of our therapists adhere too. We will work together with you and your child at all times aiming to provide high quality care, peace of mind and achievement for both you and your child.<br>
<br><br>
<h2>Understanding your child</h2><br>
As a parent you are going to be concerned about the welfare of your child, and wonder why they are not able to do the things that other children can do. One of the first steps of our assessment sessions is to discuss with you the reasons behind the difficulties your child is experiencing. Giving you a better understanding of how your child works and what you can do in the future and be able to professionally communicate to both yourself and at a child-friendly level.<br>
<br><br>
<h2>Effective Occupational Therapy</h2><br>
OT for Kids provides effective Occupational Therapy treatment for your child. Our treatments work and aim to improve your child’s ability both at home and within school. Any treatment sessions and the reasoning behind these will be clearly explained prior to starting any treatment and we are more than happy to answer any questions you may have regarding this.<br>
<br><br>
<h2>In the right Hands</h2><br>
Our occupational therapists are all highly trained and are members of both the Health Professions Council and the British Association of Occupational Therapists. The HPC govern all medical professions to ensure they are working to provide safe and effective treatment and the British Association of Occupational Therapy are a national association of information for occupational therapists to ensure we provide the most current and evidence based therapy.<br>
<br><br>
<h2>Improving your child’s future</h2><br>
At OT for Kids we firmly believe that treating children and improving function at an early age will give your child the greatest chance of enjoying a better quality of life in the future. By addressing their skills whilst they are still young, research has shown that functional improvements will be made sooner than working with adults. This is because the brain is much more malleable at a younger age, and is able to adapt to new situations and learn new skills.<br>
<br><br>
<h2>OT’s who care:</h2><br>
At OT for Kids, we only employ the most enthusiastic and caring therapists who love working in paediatric Occupational Therapy. This offers you a high quality service with people who will go out their way to help your child maximise their potential.<br>
<br><br>
<h2>Looking past a condition</h2><br>
All of our assessments are a complete assessment, we will analyse how the difficulties your child is experiencing is impacting upon the school, the home, socially and across all areas of your child’s life. We will discuss with both yourself and the child what they want to achieve from therapy, set goals from this and do our best to reach these goals.<br>
