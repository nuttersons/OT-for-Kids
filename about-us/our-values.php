<h1>Our Values</h1>
<br>
<h2>Making a difference</h2>
<br>
The backbone of our service is about making positive improvements for the children we treat and the parents, teachers, schools and organisations we work with. Everyone at OT for Kids strives to make a positive impact with every child and every client we come into contact with. In doing this we aim to increase peoples understanding of the benefits of Occupational Therapy.<br><br>
<h2>Passion and enthusiasm</h2>
<br>
