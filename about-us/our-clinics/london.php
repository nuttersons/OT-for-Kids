<h1>London</h1>
<br>
<b>At OT for Kids we have a team of highly skilled Occupational Therapists, who work in and around London, including areas such as: Dartford, Romford, Wembley, Twickenham, and Croydon.  </b>
<br><br>
Our Occupational Therapist will visit your child in their home or school environment to carry out a holistic assessment or provide ongoing treatment. 
<br><br>
At OT for Kids we assess and treat children with a range of difficulties, including:
<ul class="ticks">
	<li>Sensory difficulties </li>
	<li>Handwriting problems</li>
	<li>Concentration difficulties </li>
	<li>Autism</li>
	<li>Dyspraxia </li>
	<li>Cerebral Palsy</li>
</ul>

<h2>Why choose a school based assessment?</h2>
<br>
Through completing an assessment or providing ongoing treatment at school, Our Occupational Therapist is able to:
<ul class="ticks">
<li>Work closely with teaching staff</li>
<li>Observe your child in the natural environment</li>
<li>Make recommendations suitable to the school</li>
</ul>
<br>
A school based assessment or ongoing treatment enables your child to be observed in their natural learning environment. In addition, appropriate strategies and recommendations are made to not only support your child at school, but can often be transferred home. 
<br><br>
<h2>Benefits of home assessment :</h2>
<br>
Having an Occupational Therapy assessment at home provides an excellent opportunity for:
<ul class="ticks">
	<li>Contextual environment  </li>
	<li>Self-care (washing and dressing) observations </li>
	<li>Recommendations appropriate with current lifestyle </li>
	<li>Discussions with parents and key workers</li>
	<li>Observations and advice in improving the environment </li>
	<li>Small aids and adaptation advice</li>
	<li>Seating and wheelchair support</li>
</ul>
