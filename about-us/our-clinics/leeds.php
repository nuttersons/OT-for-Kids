<h1>Leeds</h1>
<br>
<b>OT for Kids have a dynamic team of Occupational Therapists, who work in Leeds and surrounding locations such as; Thornton, Brighouse, Wakefield, Pontefract and Ripley.</b>
<br><br>
Our Occupational Therapist will assess your child in their home or school environment. From this we provide an individually written report summarising the difficulties your child is experiencing and provide further recommendations to improve these.
<br>
<h2>School assessment </h2>
The benefits of having a school based assessment include:
<ul>
<li>Teacher and SENCO Involvement </li>
<li>Observations of child in natural learning environment </li>
<li>Recommendations suitable for school environment </li>
</ul>
Assessment or treatment in school enables our Occupational Therapist to work with your child in a familiar and comfortable environment. A school based assessment also gives our Occupational Therapist the opportunity to work alongside education professionals, ensuring that recommended strategies are accessible for school.
<h2>Benefits of home assessment</h2> 	
When your child participates in home assessment or treatment, our Occupational Therapist are able to directly observe the difficulties your child experiences on a daily basis, and provide recommendations to improve this. Some of the benefits include:
<ul>
<li> Familiar environment </li>
<li>Reduced anxiety for the child</li>
<li>Observations in natural environment</li>
<li>Contextual observation</li>
</ul>
