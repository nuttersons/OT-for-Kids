<h1>Glasgow</h1>
<br>
<b>At OT for Kids we have a team of specialised Occupational Therapists working in Glasgow and surrounding areas such as: Strathaven, Prestwick, Saltcoats, Greenock and Callander.</b>
<br><br>
One of our highly skilled Therapists can complete an assessment at your child’s home or at school. 
<br><br>
At OT for Kids we work with children with a variety of difficulties including:
<ul class="ticks">
<li>Autism and Asperger’s syndrome </li>
<li>Dyspraxia </li>
<li>Concentration </li>
<li>Handwriting </li>
<li>Sensory </li>
</ul>
<h2>Benefits of school assessment</h2>
There are a variety of reasons why your child should have a school based assessment:
<ul class="ticks">
<li>Assessed in day to day environment </li>
<li>Productive discussions with SENCO, teacher and supporting staff</li>
<li>Clear outcomes and objectives for maximising learning environment</li>
<li>Observations completed in natural environment</li>
</ul>
A school based assessment gives our Occupational Therapists the opportunity to observe your child in their natural learning environment, and collaborate with educational staff, ensuring that your child’s needs are met. Alternatively, if you do not wish for our Therapist to visit your child at school we can complete treatment and assessment at home. 
<br>
<h2>Benefits of home assessment </h2>
During a home assessment our skilled Occupational Therapist will visit your child in their home environment. There are many benefits of a home assessment, which include:
<ul class="ticks">
<li>Familiar environment </li>
<li>Contextual assessment </li>
<li>Observations of self-care occupations(washing and dressing)</li>
<li>Discussions with parents and key workers</li>
</ul>
A home assessment gives our Occupational Therapist the opportunity to observe your child in their natural home environment, ensuring all individual recommendations can be implemented with ease. 

