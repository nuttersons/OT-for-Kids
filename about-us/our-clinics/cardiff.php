<h1>OT for Kids Cardiff</h1>
<br>
<b>At OT for Kids we have a team of dedicated Occupational Therapists, who work in Cardiff and surrounding areas such as; Barry, Cowbridge, Merthyr Tydfil, Pontypool, and Magor. </b>
<br><br>
Our Therapists work with an array of children whom all experience varying levels of difficulties:
<ul class="ticks">
<li>Handwriting assessment</li>
<li>Dyspraxia assessment</li>
<li>Concentration assessment</li>
<li>Autism and Asperger’s assessment</li>
<li>Sensory Processing assessment</li>
<li>Seating and wheelchair assessments</li>
</ul>
One of our experienced Occupational Therapists can assess your child at home or at school, and provide advice, support and treatment based on the individual needs of your child.
<br><br>
<h2>School assessment</h2>
Through completing an assessment in school, our Occupational Therapist can: 
<ul class="ticks">
<li>Speak to the SENCO</li>
<li>Help contribute to the EHCP</li>
<li>Observe in class </li>
<li>Provide strategies and recommendations for school</li>
</ul>
During the school based assessment, our Occupational Therapist will observe the difficulties your child experiences and how they are impacting on your child’s education and daily life at school. 
<br><br>
<h2>Benefits of home assessment</h2> 
A home assessment is beneficial to both you and your child for many reasons:
<ul class="ticks">
<li>Familiar environment </li>
<li>Contextual assessment</li>
<li>Open discussions with parents</li>
<li>Child is likely to feel relaxed and at ease </li>
</ul>
<br><br>
In choosing a home assessment our Occupational Therapist can observe your child in a comfortable environment, ensuring a true reflection of the difficulties experienced by your child. On completion of the assessment our Occupational Therapist can then recommend appropriate strategies to meet the needs of your child.

