<h1>Bristol</h1>
<br>
<b>At OT for Kids we have highly skilled Occupational Therapists, who work across Bristol and surrounding locations such as: Newbury, Cheltenham, and Western-Super-Mare. </b>
<br><br>
Here at OT for Kids we work with a variety of children experiencing a range of difficulties: 
<ul class="ticks">
<li>Handwriting </li>
<li>Concentration </li>
<li>Sensory Regulation </li>
<li>Development Co-ordination Disorder</li>
<li>Autism </li>
<li>Dyspraxia</li>
</ul>
If you feel your child would benefit from an assessment our Occupational Therapists can carry out an assessment of your child in their home or school environment. 
<br>
<h2>School assessment</h2>
There are many reasons why you may wish to choose a school assessment for your child:
<ul class="ticks">
<li>SENCO involvement</li>
<li>Recommendations suitable for the school environment </li>
<li>Classroom observations </li>
<li>Discussions with class teacher</li>
<li>Assessment in natural environment</li>
</ul>
By selecting a school assessment for your child our Occupational Therapist can observe the impact the difficulties are having on your child on a daily basis within the school environment. Our Occupational Therapist can then suggest strategies to improve these difficulties and work with the school in implementing these successfully. 
<br>
<h2>Home assessment</h2>
Through assessing a child at home, the assessment will provide a:
<ul class="ticks">
<li>Familiar environment </li>
<li>Reduced anxiety </li>
<li>Self care assessments</li>
<li>Contextual observations </li>
</ul>
During a home assessment our Occupational Therapist has the opportunity to observe your child participating in self-care occupations (i.e. washing and dressing). Our Occupational Therapist will recommend strategies to improve activities of daily living making routines easier and less strenuous for your child. 
<br><br>
On completion of either assessment the Therapist will provide a detailed individual report and provide advice for further treatment. If you feel your child would benefit from an assessment by one of our skilled Therapists, please contact us. 
