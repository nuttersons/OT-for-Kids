<h1>Birmingham</h1>
<br>
<b>At OT for Kids we have a dedicated team of highly skilled Occupational Therapists who cover Birmingham and it's surrounding areas such as; Coventry, Tamworth, Wolverhampton, Kidderminster, and Redditch. </b>
<br><br>
Our Occupational Therapists work with children experiencing a variety of difficulties which impact on daily activities, such as:
<ul class="ticks">
<li>Poor concentration </li>
<li>Poor handwriting </li>
<li>Dyspraxia</li>
<li>Poor Sensory Regulation </li>
<li>Autism </li>
<li>Seating and Wheelchair support</li>
</ul>
If you feel your child would benefit from an assessment our Therapists can complete an assessment in your child's school or home environment. 
<br>
<h2>Home assessment</h2>
<br>
There are several reasons why you should choose a home assessment:
<ul class="ticks">
<li>Familiar environment </li>
<li>Contextual observations</li>
<li>Reduced anxiety</li>
<li>Recommendations appropriate to home life</li>
</ul>
In choosing a home assessment our Occupational Therapist is able to assess activities of self-care i.e. getting washed and dressed and provide strategies to increase independence; increasing your child's confidence and self-esteem. 
<br><br>
However, if you would prefer our Occupational Therapist can assess your child in their school environment.
<br>
<h2>School assessment </h2>
<br>
We can also come out and provide an Occupational Therapy assessment in your child's school. This promotes:
<ul class="ticks">
<li>Teacher involvement </li>
<li>Classroom strategies</li>
<li>Discussions with teaching staff to obtain accurate skill set in school</li>
<li>Observations in natural learning environment </li>
<li>Collaboration between health and education </li>
</ul>
If you select a school assessment our Occupational Therapist can collaborate with your child's teachers, ensuring all identified needs are met. From this our Occupational Therapist will ensure all recommendations are individual and unique to meet the needs of your child. 
<br><br>
On completion of either assessment our Occupational Therapist will provide a detailed report and treatment plan based on the needs of your child containing strategies to overcome identified difficulties.
<br><br>
