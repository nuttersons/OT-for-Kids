<h1>Edinburgh</h1>
<br>
<b>At OT for Kids we have a team of highly skilled and caring Occupational Therapists, who work in Edinburgh and surrounding locations such as; Livingston, West Linton, Berwick-upon-Tweed, Coldstream and Selkirk. </b>
<br><br>
Here at OT for Kids we work with a range of individuals who experience a variety of difficulties, such as:
<ul class="ticks">
<li>Poor attention</li>
<li>Neurological difficulties</li>
<li>Sensory processing difficulties </li>
<li>Physical difficulties</li>
<li>Fine motor difficulties </li>
<li>Handwriting difficulties </li>
</ul>
If you feel your child would benefit from an assessment, one of our skilled Occupational Therapists can visit your child in their home or school environment. 
<h2>School assessment</h2>
There are a number of benefits to being assessed or treated in school. These include:
<ul class="ticks">
<li>SENCO Involvement</li>
<li>Observations in natural learning environment </li>
<li>Refined recommendations for schools </li>
<li>Social observations </li>
</ul>
A school assessment gives our Occupational Therapist the opportunity to observe your child in their learning environment and assess any barriers to their education. Therefore, ensuring all strategies and recommendations are appropriate for the school environment. 
<h2>Home assessment </h2>

Our Occupational Therapist is also able to visit your child at home to complete assessment or treatment. There are many advantages of a home assessment, some of which include: 
<ul class="ticks">
<li>Comfortable environment</li>
<li>Observations completed in natural environment </li>
<li>No travel expenses </li>
</ul>
A home assessment gives our Occupational Therapist the opportunity to observe your child in a relaxed familiar environment. Therefore, gaining a true representation of difficulties experienced by your child. From the assessment our Occupational Therapist can suggest strategies to implement within the home environment
