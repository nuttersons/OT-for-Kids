<h1>Early Development (0-12 months)</h1>
<br>
As children begin the early stages of their life, they will have difficulty understanding the world around them, but should be able to complete the following tasks between the ages of 0 and 6 months old:<br><br>
<b>Visual:</b>
<ul class="ticks">
<li>Eyes move independently from head</li>
<li>Looks directly at speakers face</li>
<li>Visually track an object about 12” away </li>
<li>Looks for objects in hand</li>
</ul>
<b>Fine Motor:</b>
<ul class="ticks">
<li>Keep hands either in a fist shape or open and relaxed</li>
<li>Can hold and grasp objects</li>
<li>Move the hands to the mouth</li>
<li>Bring hands towards the centre of the body</li>
</ul>
<b>Gross Motor:</b>
<ul class="ticks">
<li>Lie on tummy and turn head side to side</li>
<li>Swipe at objects</li>
<li>Lift head to 45° </li>
<li>Kick feet alternately </li>
</ul>
Following the first 6 months of life, a child should progress further between ages 6 and 12 months, demonstrating the ability to:<br><br>
<b>Visual:</b>
<ul class="ticks">
<li>Visually attend to pictures in a book</li>
<li>Looks for dropped objects</li>
<li>Explores depth by looking into containers </li>
</ul>
<b>Fine Motor:</b>
<ul class="ticks">
<li>Place objects in cups</li>
<li>Self feeds finger foods and able to hold a bottle</li>
<li>Able to spoon feed thick objects</li>
</ul>
<b>Gross Motor:</b>
<ul class="ticks">
<li>Rolls from back to belly</li>
<li>Sit without support</li>
<li>Scoot on stomach</li>
<li>Saving reflexes present (moves hands forward when falling)</li>
<li>Pulls to standing</li>
<li>Furniture walking</li>
<li>Walks with one hand held</li>
</ul>
<h2>How Occupational Therapy can help a child with delayed development</h2>
<br>
Our occupational therapists have the skills and knowledge to help further the development of children who may be finding adapting to the world around them more difficult than others. We can offer a range of treatment and services to improve the development of your child.<br><br>
If you are concerned about how your child is developing or would to find out more about how our occupational therapists can help please email office@otforkids.co.uk or call us on 0330 233 0888 <br><br>

