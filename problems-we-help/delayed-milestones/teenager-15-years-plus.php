<h1>Teenager (15 years plus)</h1>
<br>
Making the transition through teenage years and into adulthood is a very difficult time for any young persons’ life and may result in physical, emotional or behavioural difficulties that affect how they function on a day to day basis. A child of this age should be able to:<br>
<br>
<b>Visual</b>
<ul class="ticks">
<li>Teenagers should be able to navigate and perceive the moving world without difficulty</li>
</ul>
<b>Fine motor</b>
<ul class="ticks">
<li>Teenagers should be able to perform complex fine motor movements</li>
<li>Dress independently</li>
<li>Manipulate small objects </li>
</ul>
<b>Gross Motor</b>
<ul class="ticks">
<li>Able to engage in all sports </li>
<li>Able to negotiate complex environments </li>
</ul>
<b>Organisational skills</b>
<ul class="ticks">
<li>Able to begin to independently organise their work and social life</li>
<li>Organise timetables </li>
<li>Remember equipment needed for meetings</li>
</ul>
<h2>How an occupational therapist can help a child with delayed milestones</h2>
<br>
Our occupational therapists have the skills and knowledge to help further the development of teenagers who may be finding adapting to the world around them more difficult than others. We can offer a range of treatment and services to improve development.<br>
