<h1>Toddler and Early Infancy (1 to 3 years)</h1>
<br>
<h2>Toddler</h2>
<br>
As the child develops further, between the ages of 12 and 24 months, the child should be able to:<br>
<br>
<b>Visual:</b>
<ul class="ticks">
<li>Hands move according to vision</li>
<li>Looks for and identifies pictures in books</li>
<li>Explores depth of objects</li>
<li>Enjoys watching moving objects (wheels, etc.) </li>
</ul>
<b>Fine Motor:</b>
<ul class="ticks">
<li>Builds towers out of large blocks</li>
<li>Scribbles with crayons </li>
<li>Able to take socks off</li>
<li>Turns pages of a book one at a time</li>
<li>Uses thumb and index finger to hold crayon</li>
</ul>
<b>Gross Motor:</b>
<ul class="ticks">
<li>Walks independently forwards and backwards</li>
<li>Throws a ball without falling down</li>
<li>Stand up independently</li>
<li>Jumps with two feet</li>
</ul>
<h2>Early Infancy</h2>
<br>
Going into early infancy, (2 &ndash; 3 years old) the child should be able to:<br>
<br>
<b>Visual:</b>
<ul class="ticks">
<li>Visually attends to hands whilst holding objects</li>
<li>Visually inspect objects without the need to touch them<br>
</li>
</ul>
<b>Fine Motor:</b>
<ul class="ticks">
<li>Brushes teeth with help </li>
<li>Holds scissors with two hands and can open and close scissors</li>
<li>Starts unbuttoning large buttons</li>
<li>Draw vertical lines</li>
<li>Able to throw a small ball</li>
</ul>
<b>Gross Motor:</b>
<ul class="ticks">
<li>Catch a ball</li>
<li>Walk on tip toes briefly</li>
<li>Several steps taken on a balance beam</li>
<li>Walks upstairs with support</li>
<li>Able to jump forward</li>
<li>Balance on one foot </li>
</ul>
<h2>How an occupational therapist can help a child with delayed milestones</h2>
<br>
Our occupational therapists have the skills and knowledge to help further the development of children who may be finding adapting to the world around them more difficult than others. We can offer a range of treatment and services to improve the development of your child.<br>
