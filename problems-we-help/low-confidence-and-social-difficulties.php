<h1>Low Confidence and Social Difficulties</h1>
<br>
Confidence is a vital aspect of physical and social development in children, for without confidence it is likely that children will struggle to push themselves to make friends, engage in new activities and can have a detrimental effect on their overall happiness in these early years.<br><br>
Low confidence can become apparent because of many factors, including genetic, environmental and socially, for example it is likely that if a child is in an environment where those around him/her also have low confidence will be of the same mould. However that is not to say that every child will experience this and it is not to say that this cannot be improved through therapy. Confidence is such a subjective outcome, that it may take some children a short time to feel more confident, where as other children may take longer. Low confidence is usually a secondary symptom of more complex difficulties, such as poor fine or gross motor skills resulting in the child realising that they ‘can’t do it’. This is because they can see how easily other children can complete the activity in which they are struggling.<br><br>
<h2>Social implications of low confidence</h2>
<br>
The social implications of low confidence can be quite paramount, as a child with low confidence would struggle to push themselves out of their comfort zone. This could lead to the child feeling isolated and alone, as they are not comfortable in approaching children they don’t know when making friends, or asking to play a game that they may not be directly involved in. Over time, the child will start to adapt to this and find new ways to cope with the social difficulties. However it is likely that the child would prefer to have more friends but does not know how. An occupational therapist would work together with you and your child in discussing how to make friends, and what to do when anxiety is becoming too much.<br><br>
<h2>How can Occupational Therapy improve confidence? </h2>
<br>
An occupational therapist would be able to provide effective assessment and treatment to improve confidence in children, through the use of occupations and activities. The occupational therapist would evaluate the confidence level and physical ability of the child and engage the child in therapy that challenges them but is at a standard that is achievable. This sense of consistent achievement and change in ability over time is what will improve confidence.<br><br>
