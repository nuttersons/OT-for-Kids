<h2><a id="1" name="1"></a>Letter Formation</h2>
<br>
Letter formation is the ability of the child to begin to correctly form letters. A correctly formed letter has to start in the right place, curve anti-clockwise and end in the correct position. If your child is struggling to write they may use compensatory methods to overcome this difficulty such as starting in the wrong position, circling clock-wise rather than anti-clockwise or holding the pencil in an abnormal grip. <br><br>
You may find that the final letter looks similar to the d above, however if it is drawn incorrectly, this can indicate an underlying handwriting difficulty that may be affecting other letters in their writing.<br><br>
<h2><a id="2" name="2"></a>Pencil Control</h2>
<br>
Pencil control is the ability to control the pencil to produce smooth, fluid and continuous lines when writing; it requires a great deal of visual acuity, visual perception and fine motor movements to produce such accurately drawn lines. The ability to control the pencil is a fundamental building block in handwriting development, without this key aspect, children will struggle to form letters that are legible. Often resulting in letters that appear stretched, too large or jittery.<br><br>
<h2><a id="3" name="3"></a>Alignment </h2>
<br>
Alignment refers to words written in a horizontal plane, at the same angle as expected for certain letters. For example, the letter p slopes vertically downwards, if a child slopes the p to the left or right, this is incorrect alignment and often results in further difficulties relating to tall and small letters. Children who struggle to align their letters correctly often have writing that is difficult to read and can look disjointed.<br><br>
<h2><a id="4" name="4"></a>Spacing</h2>
<br>
Letter spacing is the term used to describe the correct spacing between letters and words, it is often demonstrated through letters that are squashed together or words that seem to mould into one. Poor spacing can have a dramatic effect on the legibility of a child’s handwriting and it is important that this is addressed as soon as possible.
<br><br>
<h2><a id="5" name="5"></a>Pencil Pressure</h2>
<br>
<a id="6" name="6"></a>The pressure upon which we apply to the pencil when writing determines the legibility and letter formation of handwriting, if the child is applying too much pressure, some letters may appear forced and they will most likely have difficulty completing the more complex letters such as g,y and q as this requires less pressure and a more fluent writing style. Children with poor pressure often find it difficult to convert their writing into cursive format, often due to the poor pressure control.
<br><br>
<h2><a id="7" name="7"></a>Posture</h2>
<br>
Posture is an important aspect of handwriting as poor posture takes the body out of alignment and ultimately makes the task of writing harder. Poor posture moves the shoulder blade, upper arm and forearm out of alignment which subsequently has a poor effect on handwriting.
<br><br>
<h2><a id="8" name="8"></a>Visual Perception</h2>
<br>
Visual perception is the ability to perceive the patterns on a page as letters, words and numbers. Children who have poor visual perception tend to have difficulties planning their next words or the next part of their letters and often find it hard to continue writing if their hand is in the way.<br><br>
<h2><a id="9" name="9"></a>Handwriting speed and size</h2>
<br>
As children develop they are expected to write in constrained time limits whilst keeping their work neat and legible, children with dyspraxia or developmental co-ordination disorder often struggle to do this due to the associated difficulties with these conditions. The size and speed of writing can have an effect on children in lessons and ultimately during examinations which can give a poor reflection of their true intellect.<br><br>
<h2><a id="10" name="10"></a>Pencil grips</h2>
<br>
Children with poor handwriting most often develop an abnormal grip when writing, this is to reduce the demands of the muscles in the hand in an attempt to produce more legible writing. This style can often have a detrimental effect in letter formation, size, speed and pressure. An occupational therapist can provide an array of adaptive pencils and grips aimed at promoting the correct grip needed for legible writing. As writing is a complex task, requiring fluent movements, it is often difficult for children to produce these movements unless their fingers are in the correct tripod position to start with. The therapist would be able to work with your child in promoting the correct grip to improve handwriting.<br><br>
