<h1>Poor Handwriting</h1>
<br>
Handwriting is an important aspect of childhood, incorporating a variety of complex and intricate muscle movements and visual perception skills. Handwriting is not a simple task, and if left underdeveloped can become a problem later in academic life. The necessary skills required to produce writing are usually acquired in early school years, and honed over the remainder of the school period. Children who struggle with handwriting often develop an abnormal grip to compensate for the lack of muscle development in the hands; this grip is usually the first sign in noticing a child who has poor handwriting.<br><br>
An occupational therapist would be able to assess why your child may be struggling to write, evaluate the reasons for this and provide effective treatment exercises  in clinic, school and at home. The therapist would analyse several aspects of your child’s handwriting, including pencil grip, letter formation and visual perception skills, all of which have a great impact on correct handwriting. <br>
Handwriting can be broken down into these key aspects:
