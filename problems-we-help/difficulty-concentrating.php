<h1>Difficulty Concentrating</h1>
<br>
Children of all ages often find it hard to maintain concentration, and in the majority of cases it is normal for children to experience some difficulties concentrating. However, if a child is too easily distracted or cannot follow instructions without the need to repeat several times what was said, poor concentration can cause significant issues across all areas of childhood, such as school, home and making friends.<br>
<br>
An occupational therapist can determine the factors that may be the reason behind the lack in concentration needed at vital times of learning work together and provide interventions that aim to increase the overall sustained concentration of the child.
<br><br>
<h2>What is concentration?</h2>
<br>
Concentration is the ability to focus on, maintain and attend to a particular task. Children should be able to maintain a level of concentration that is appropriate for their age, one way to tell if your child is concentrating well is to compare them with their peers. The aspects of concentration are as follows: 
<ul class="ticks">
<li>Listening </li>
<li>Following verbal and non-verbal cues, </li>
<li>Ignoring any noises or visual distractions </li>
<li>Staying quiet </li>
<li>Sustaining attention over time</li>
<li>Focusing on the relevant information</li>
</ul>
It is often those children who are constantly talking during lessons, being thrown out of classrooms or constantly interrupt others who are most likely to have difficulties concentrating.  An occupational therapist would be able to use their skills and knowledge to provide activities and games aimed at improving concentration, some examples of which are:
<ul class="ticks">
<li>Reading a paragraph of fictional text and asking the child to recall key information</li>
<li>Maintain silenced colouring/drawing on one task for a period of time</li>
<li>Discuss the importance of concentration within the context of the home and school</li>
</ul>
It may also be common for a child who has difficulty concentrating, to struggle to make real friends, as they often talk over and interrupt the other children, this can cause low confidence and the use of laughter as a compensatory method to overcome these difficulties. As they develop they may become distracted from school and become uninterested in learning. Through the use of Occupational Therapy, children can not only improve their concentration but hopefully find a particular hobby that they enjoy, further developing their learning and ability to maintain concentration.<br><br>
