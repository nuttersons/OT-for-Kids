<h1>Gross Motor Difficulties</h1>
<br>
Gross motor is a term used to describe the ability to produce smooth, fluid and accurate movements needed to compete in sport, move through space safely, navigate new layouts and engage in a range of playground activities. The term gross motor can be translated literally into large movements, referring to movements completed by the whole body, rather than fine motor movements which only concern the intricate movements of the hand.<br><br>
Children who struggle with movement can often have difficulty balancing, moving around the home and school environment safely, engaging in P.E and competing in sport. An occupational therapist would firstly assess how your child functions in activities and analyse their gross motor skills, before suggesting treatment options. During this analysis of gross motor skills, the occupational therapist would use their knowledge to unpick the components of gross motor skills and determine which aspects the child is struggling with most.<br><br>
<h2>Components of Gross Motor skills</h2>
<ul class="ticks">
<li>Balance </li>
<li>Proprioception</li>
<li>Sensory regulation of limbs and muscles</li>
<li>Hand-eye co-ordination </li>
<li>Motor planning and execution</li>
<li>Visual perception</li>
<li>Midline orientation</li>
<li>Bilateral integration</li>
<li>Spatial awareness</li>
<li>Temporal awareness</li>
</ul>
A combination of these components of gross motor skills are needed to perform activities, for example to be able to successfully catch a ball, a child must maintain balance, plan when they want to move (motor planning and execution) and orientate their hands towards the middle of the body (midline orientation and bilateral integration) in time with the flight and speed of the ball (spatial awareness and temporal awareness and hand-eye co-ordination). Therefore, what seemed to be a simple task actually requires a great deal of gross motor co-ordination and skills.<br><br>
<h2>Importance of gross motor skills</h2>
<br>
Gross motor skills are an important component of development and become a vital part of engaging in activities across many areas, such as at home, in lessons, in the playground or playing outside with friends and sport. Some of the common difficulties to be aware of are if your child is:
<ul class="ticks">
<li>Finding it hard in P.E</li>
<li>Having difficulty running/hopping/jumping/skipping etc</li>
<li>Unable to balance effectively</li>
<li>Struggling to throw or catch</li>
<li>Having difficulty moving in space (proprioception)</li>
<li>Having difficulty completing complex movements</li>
<li>Unable to stand and complete tasks successfully</li>
<li>Clumsy and un-coordinated</li>
</ul>
<h2>How Occupational Therapy can help? </h2>
<br>
An occupational therapist would analyse the child’s gross motor skills in relation to a task, determine where the deficit lies and provide treatment that aims to improve these. Treatment would include completing activities that involve the use of gross motor skills, the theory being that through practicing tasks and activities that use the components of gross motor skills, children will begin to develop and improve the aspects upon which they’re struggling with and subsequently the overall task performance of gross motor activities will improve. <br><br>
