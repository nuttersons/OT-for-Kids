<h1>Sensory Regulation Difficulties</h1>
<br>
Sensory regulation is the ability to listen to the environmental feedback your body is receiving and respond through movement accordingly. Sensory regulation is required during all aspects of life, it is how we interpret the world around us, complete everyday activities and be able to function in a safe and successful manner. If a child has sensory regulation difficulties they may have difficulty surrounding handwriting, running, jumping, tying shoe laces, concentrating and understanding  instruction in a crowded and noisy environment.<br><br>
An occupational therapist would be able to provide assessment of the specific sensory deficits that the child may be experiencing, discuss goals and ultimately provide treatment aimed at improving sensory regulation.<br><br>
It is likely that a child with Sensory regulation difficulties will often either find an environment sensory stimulating and be over-sensitive to the environment, (resulting in them acting out, finding things very difficult in school/shopping centres and other noisy environments) or they will show little or no reaction to otherwise extreme sensory input, (resulting in little or no change in emotion or state when bright lights/loud noises, heat or strong smells enter the environment).<br><br>
<h2>Importance of Sensory Regulation</h2>
<br>
Sensory regulation is vital in childhood development and the acquisition of new skills. If a child has poor sensory regulation, they will probably have difficulties both at home and at school, mainly surrounding:
<ul class="ticks">
<li>Acting out when placed in a noisy or bright environment</li>
<li>Movement difficulties</li>
<li>Fine motor difficulties</li>
<li>Responding to loud noises/bright lights or strong smells</li>
<li>Finding school/shopping centres and other noisy environments difficult to deal with</li>
<li>Little or no reaction to sensory inputs</li>
<li>Difficulty concentrating in class</li>
<li>Difficulty moving between classes/ or coping in a crowded environment</li>
</ul>
<h2>How Occupational Therapy can help</h2>
<br>
Occupational therapy is useful for children who struggle to make sense of the world around them, providing assessment and treatment aimed at improving the sensory feedback received from the world around them. The occupational therapist would be able to identify the key deficit areas and through occupation improve the connections between the senses and the brain, in order to improve the interpretation of senses and ultimately improve function.<br><br>
