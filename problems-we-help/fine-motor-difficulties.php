<h2>How can Occupational Therapy help?</h2>
<br>
Occupational therapy is a profession in which the therapist uses activities to promote the skills needed to function in everyday activities. One area which we work in is paediatrics, analysing and improving many aspects needed for a productive childhood, such as movement, concentration attention, confidence and fine motor skills. Through using activities to promote skills, the therapist can maintain the necessary motivation or engagement needed from the child in order to develop skills, for with a lack of motivation and subsequently focus to engage in therapy, research has shown that improvements will not be made as quickly. By linking the underlying components of fine motor skills to activities that children complete daily (in school and at home) such as cutting, writing, drawing, painting, dressing and playing board games, fine motor skills will subsequently be improved.<br><br>
