<h1>Movement and Co-ordination Group</h1>
<br>
OT for Kids is a private company, who only hire professional and reliable therapists. We offer a range of groups. One of which being the Movement Improvement group. 
<br>
<br>
<b> Common difficulties appropriate for a movement group:</b>
<ul class="ticks">
<li>Clumsy</li>
<li>Un-coordinated</li>
<li>Difficulty balancing</li>
<li>Difficulty running, hopping, jumping and skipping</li>
<li>Finds floor based activities difficult</li>
<li>Poor hand eye co-ordination</li>
<li>Poor visual perception</li>
<li>Difficulty perceiving the flight of objects</li>
<li>Difficulty catching and throwing</li>
</ul>
The movement group is designed for children who are having difficulty producing fluent and accurate gross motor movements resulting in children having difficulty producing smooth movements of the upper and lower body and typically find it difficult to partake in P.E and sport.
<br><br>
<h2>Goals of a movement improvement group</h2>
<br>
The two main aims of the movement improvement group are:
<ul class="ticks">
<li>Improve gross motor skills, focusing on two common aspects such as balance and co-ordination</li>
<li>Improve visual perception and motor planning skills </li>
</ul>
The occupational therapists would work together with the children in completing activities that promote the skills needed to produce fluent accurate movements. Examples activities include, balance beams, target throwing games, obstacle courses and relay races, and ball based games. An outcome measurement tool would be administered at the beginning to provide a benchmark for future sessions and at the end to show any changes.<br>
<br>
<b>Benefits of group therapy:</b>
<ul class="ticks">
<li>Build social skills</li>
<li>Tailored functional improvements for each group</li>
<li>Realise your child may not be the only one experiencing these difficulties</li>
<li>Talk to other parents with similar concerns</li>
<li>Discuss any concerns or issues you may have with therapist</li>
<li>Reduced rate per hour compared to 1:1 therapy</li>
</ul>
Following the group, you will receive a report, containing an outline of how the group has been, a summary of your child’s skills in relation to the activities completed in the group and any recommendations regarding regular therapy or equipment.<br>
