<h1>Handwriting Improvement Group</h1>
<br>
OT for Kids is a private company, who only hire professional and reliable therapists. We offer a range of groups. One of which being the handwriting group. The occupational therapists would work on improving the core aspects that underline handwriting, these aspects include:<br>
<br>
<b>Legibility:</b> How easy the child’s writing is to read<br>
<br>
<b>Spacing: </b>If the writing is correctly spaced across the page<br>
<br>
<b>Alignment: </b>The level at which writing is horizontally aligned on the page <br>
<br>
<b>Visual Perception: </b>The ability of a child to visually perceive the patterns needed to make words<br>
<br>
<b>Letter size and slope: </b>The appropriate sizing and sloping of letters<br>
<br>
<b>Letter formation: </b>The correct formation of letters, including anti-clockwise movements and cursive flicks<br>
<br>
<b>Posture: </b>An upright and correct posture is needed to facilitate writing<br>
<br>
<b>Pencil grip:</b> A dynamic tripod grip is needed to produce the fluent movements needed to write.<br>
<br>
The handwriting group is designed to improve handwriting and would be suitable for children who are having difficulty producing clear, legible and correctly formed writing.
<br><br>

<img src="/website69b/images/handwriting-example2.png" width="100%">
<h2>Goals of a handwriting improvement group</h2>
<br>
The two main aims of the handwriting improvement group are:
<ul class="ticks">
<li>Improve pencil grip and posture</li>
<li>Improve the underlying aspects of handwriting with the aim of improving legibility and fluency of writing. </li>
</ul>
The occupational therapist would, through the use of games and activities aim to promote legibility, spacing, alignment, visual perception, letter formation, size and slope. The occupational therapist would also assess the pencil grip and posture and provide adaptive grips and sloped writing boards (if required) to help correct poor pencil grip and posture.<br>
<br>
<b>Activities include:</b>
<ul class="ticks">
<li>Musical writing programmes</li>
<li>Worksheets</li>
<li>Throwing and balancing games designed to strengthen upper body to promote posture\</li>
<li>Home programmes to promote development</li>
<li>Grip correction</li>
</ul>
Following the group, you will receive a report, containing an outline of how the group has been, a summary of your child’s skills in relation to the activities completed in the group and any recommendations regarding regular therapy or equipment.<br>
