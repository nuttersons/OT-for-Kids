<h1>Concentration and Memory Group</h1>
<br>
OT for Kids is a private company, who only hire professional and reliable therapists. We offer a range of groups. One of which being the concentration and memory improvement group. <br><br>
Some of the key difficulties children who would be suitable for this group include:
<ul class="ticks">
<li>Forgetting P.E kit often</li>
<li>Difficulty listening to others</li>
<li>Often interrupts</li>
<li>Difficulty following two or more stage instructions</li>
<li>Poor ability to sustain attention</li>
<li>Poor multitasking skills</li>
<li>Difficulty following clearly given instructions</li>
<li>Finds it hard to let others talk first</li>
</ul>
The concentration and memory improvement group is designed for children who have difficulty maintaining their concentration or remembering key information needed for school or home function.
<br><br>
<h2>Goals of a concentration and memory improvement group</h2>
<br>
The two main aims of the concentration and memory improvement group are:
<ul class="ticks">
<li>Improve concentration and listening skills in relation to activities both at home and in school</li>
<li>Improve memory and information retention skills </li>
</ul>
The occupational therapists would use activities and games to promote the underlying skills needed to maintain sustained attention and remember information. These activities include a quiz based on a small section of text or concentrated work on one particular task. The children who have difficulties concentrating often get frustrated and may find home and school life challenging; a concentration group can help these children manage their frustrations.  An outcome measurement tool would be administered at the beginning to provide a benchmark for future sessions and at the end to show any changes.
<br> <br>
<h2>Benefits of group therapy:</h2>
<ul class="ticks">
<li>Clear outcome measurement across the course of the group</li>
<li>Build social skills</li>
<li>Tailored functional improvements for each group</li>
<li>Realise your child may not be the only one experiencing these difficulties</li>
<li>Talk to other parents with similar concerns</li>
<li>Discuss any concerns or issues you may have with therapist</li>
<li>Reduced rate per hour compared to 1:1 therapy</li>
</ul>
Following the group, you will receive a report, containing an outline of how the group has been, a summary of your child’s skills in relation to the activities completed in the group and any recommendations regarding regular therapy or equipment.<br><br>
