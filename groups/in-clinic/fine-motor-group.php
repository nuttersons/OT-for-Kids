<h1>Fine Motor Improvement Group</h1>
<br>
OT for Kids is a private company, who only hire professional and reliable therapists. We offer a range of groups. One of which being the fine motor improvement group. The fine motor and improvement group aims at improving the co-ordination and strength of the hands needed for fine movements. Children with fine motor difficulties often struggle with:
<ul class="ticks">
<li>Getting dressed</li>
<li>Tying shoe laces</li>
<li>Writing</li>
<li>Completing games such as Jenga and coin slot games</li>
<li>Cutting and gluing </li>
<li>Colouring and drawing </li>
</ul>
The occupational therapists would assess the specific aspects of fine motor tasks that the child is finding difficult and provide activities and therapy aimed at improving and developing both the strength and co-ordination of the muscles in the hands.<br><br>
It is not uncommon for children with fine motor difficulties to also have poor handwriting, as the muscles needed to complete fine motor tasks are also the muscles needed to produce the intricate movements needed for writing. The occupational therapists would work with your child over the course of the group aiming to meet two clear goals set out in week one.
<br><br>
<h2>Goals of a fine motor improvement group</h2>
<br>
The two main aims of the fine motor improvement group are:
<ul class="ticks">
<li>Improve cutting skills and other fine motor related school based tasks</li>
<li>Improve the underlying aspects of fine motor skills, enabling children to perform better in everyday tasks</li>
</ul>
<h2>Benefits of group therapy:</h2>
<ul class="ticks">
<li>Build social skills</li>
<li>Tailored functional improvements for each group</li>
<li>Realise your child may not be the only one experiencing these difficulties</li>
<li>Talk to other parents with similar concerns</li>
<li>Discuss any concerns or issues you may have with therapist</li>
<li>Reduced rate per hour compared to 1:1 therapy</li>
</ul>
Following the group, you will receive a report, containing an outline of how the group has been, a summary of your child’s skills in relation to the activities completed in the group and any recommendations regarding regular therapy or equipment.<br><br>
