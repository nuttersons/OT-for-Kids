<h1>Movement Group</h1>
<br>
Children develop at different rates, for some children, co-ordinating movement and being able to throw, catch and balance come naturally and develop as part of an inclusive P.E programme within school; however some children require additional help to enable them to perform complex motor movements required during everyday sport based activities.  The purpose of the movement group is to improve the necessary underlying skills needed to perform the complex gross motor movements. Complex Gross motor movements are needed in many sports and activities, such as football, basketball, throwing, catching, balancing and skipping.<br><br>
<h2>Two main aims of the Movement group are:</h2>
<ul class="ticks">
<li>To assess and improve hand eye co-ordination, targeting, throwing and catching skills.</li>
<li>To assess and improve balance, proprioception and visuo-motor planning in relation to gross motor activities. </li>
</ul>
<h2>Which children are appropriate for the Movement group in schools?</h2><br>
Children who are having difficulty understanding the how to successfully perform simple and complex movements are appropriate for the movement group. Children who often find sports difficult, appear uncoordinated or clumsy and who struggle with the following movements are suitable for the movement group:
<ul class="ticks">
<li>Throwing at a target accurately</li>
<li>Catching a beanbag, large ball or tennis ball</li>
<li>Jumping with two feet simultaneously</li>
<li>Hopping</li>
<li>Balance based activities</li>
<li>Team games</li>
<li>Bat and ball skills (Tennis, Rounder's etc)</li>
</ul>
The movement group is best suited for children of a younger age, as this age where the most improvements can be made. Children will be grouped based on age and behaviour. It is important that groups are kept with children at a similar age and ability for the groups to run effectively<br><br>
<h2>How can our Occupational Therapist improve the Movement of children at your school?</h2><br> 
The service our Occupational Therapists (OT) provide is always of a high quality, OT for Kids strive to do our best for the children in our care. The OT will be able to not only assess each child during the group but provide treatment throughout the duration of the school term to address every child's individual difficulties in relation to producing smooth continuous movements.<br> <br> 
Our Occupational Therapists will work together with teachers in providing an all round approach to improving the co-ordination skills of children within the movement group. This can include verbal advice, equipment or activities to complete.<br> <br> 
<h2>What is included in our Occupational Therapy Movement Group for Schools?</h2><br> 
Every child in the movement group will be assessed at week 1 in order to determine which specific areas of movement based activities the children are finding difficult. A treatment plan will then be completed and treatment will commence on week 2. On the last week of the groups during term, a final assessment will be completed. <br> <br> 
The results of all assessments will be compared and a final individual report will be written per child. The final report will not only contain a summary of the achievements made during the group, but will also contain a selection of further recommendations for school. By providing your school with a section on further recommendations, you can continue to develop the skills of the children, during the time the Occupational Therapist is not in school.<br> <br> 
<h2>When will the Occupational Therapy Movement Groups for Schools take place and how long will the group run for?</h2><br> 
All of our Occupational Therapy groups are run during term time only, throughout the school day. Each school can choose from a combination of five groups to be run throughout the school day (maximum of four groups per day)<br> <br> 


