<h1>Concentration Group</h1>
<br>
The concentration group is a group designed to improve the ability of children to filter out irrelevant information in order to maintain concentration on a task. Through the help of OT for Kids Concentration groups, children begin to develop an understanding of how to concentrate and how to ignore background distractions. 
<br> <br>
Each child who struggles to concentrate is often overwhelmed by the level of sensory input entering their system at one time. For example, Children with poor concentration may be very inquisitive and ask several questions about the environment, the task itself or what is required of them. This inquisitiveness is because they cannot filter out unwanted information such as displays, noise or thoughts. With the help of our Occupational Therapists during the concentration group, children learn how to improve these essential sensory regulation skills needed for learning.<br> <br>
<h2>Two main aims of the concentration group are:</h2>
<ul class="ticks">
<li>To assess and improve working memory, sensory feedback and sensory regulation</li>
<li>To assess and improve managing distractions and maintaining concentration on school based tasks</li>
</ul>
<h2>Who is appropriate for the concentration group in schools?</h2><br>
All children have a limited attention span, this is something that is grown and developed throughout their development into adulthood, however the Occupational Therapy concentration group is designed for children whose learning is significantly impacted upon by their ability to concentrate compared to the rest of the class. Children who are appropriate for the concentration group will demonstrate some of the following behaviours:
<ul class="ticks">
<li>Ask many questions</li>
<li>Interrupt lessons</li>
<li>Have trouble sharing, turn taking or team games</li>
<li>Are easily distracted by the environment</li>
<li>May get easily angry or annoyed when they cannot complete task</li>
</ul>  
Children will be grouped based on age and behaviour. It is important that groups are kept with children at a similar age and ability for the groups to run effectively.<br><br>
<h2>How can our Occupational Therapist improve the concentration of children at your school?</h2> <br>
Our Occupational Therapist has experience with working within schools and can combine this experience with the knowledge and analytical mindset gained from their training. Our Occupational Therapist will be able to analyse the underlying reasons behind poor concentration and provide effective treatment to improve these skills.<br><br>
<h2>What is included in our Occupational Therapy Concentration Group for Schools?</h2><br>
The Concentration Occupational Therapy group for primary schools will contain:
<ul class="ticks">
<li>Formal assessment carried out at the beginning and end of each term to monitor improvements made in therapy</li>
<li>Informal ongoing assessment and treatment/li&gt;
</li><li>Tailored treatment plans</li>
<li>Final individual report containing further recommendations for schools</li>
</ul>  
<h2>When will the Occupational Therapy Concentration Groups for Schools take place and how long will the group run for?</h2><br>
Our Occupational Therapy Concentration group for schools take place during the school day. The concentration group runs during term time only and can be purchased for a full year (at reduced rates) or on a term by term basis.<br><br>
