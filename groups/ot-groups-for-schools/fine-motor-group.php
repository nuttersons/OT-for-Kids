<h1>Fine Motor Group</h1>
<br>
The term fine motor describes the smaller more acute movements of the hands that are required in many common tasks. Tasks such as cutting, threading, gluing, dressing, writing and arts and crafts are some of the tasks in which refined, complex fine motor skills are needed.<br><br>
An OT for Kids fine motor group will not only assess the reasons behind poor fine motor skills (such as poor finger strength, finger isolation and visuo-motor skills) but also provide an effective treatment plan across the school term, through small group therapy (no more than 6 children per group). <br><br>
The fine motor group has two aims, these aims are listed below:<br><br>
<h2>Two main aims of the fine motor group are:</h2>
<ul class="ticks">
<li>To assess and improve hand strength, finger manipulation and other underlying components of fine motor skills</li>
<li>To assess and improve visual perception and integrate this with the developing fine motor skills to enable complex fine motor movements to occur</li>
</ul>
<h2>Which children are appropriate for the fine motor group in schools?</h2><br>
In a class, there is usually between 3 and 5 children who will have fine motor difficulties. 
<br><br>
You can spot these children as they all portray several signs that they are finding the task difficult. Whilst the class is cutting, gluing or performing similar activities that involve complex movements of the hands and fingers notice if any children are:
<ul class="ticks">
<li>Fixing their elbows into their side</li>
<li>Manipulating the items by pushing into their body</li>
<li>Squirming on their chair, kneeling or standing</li>
<li>Completing the task incorrectly</li>
<li>Cutting with one hand and paper resting on the table</li>
<li>Cutting with the spare hand crossing over the scissor hand</li>
<li>Giving in or losing motivation quickly</li>
<li>Difficulty understand terms like cross over, swap, or flip </li>
<li>Difficulty manipulating objects to match patterns / shapes completed by teacher</li>
</ul>
Any child who is displaying any of the above signs when completing tasks needs input from one of our Occupational Therapists at school. Children will be grouped based on age and behaviour. It is important that groups are kept with children at a similar age and ability for the groups to run effectively<br><br>
<h2>How can our Occupational Therapist improve the fine motor of children at your school?</h2><br>
Our Occupational Therapist (OT) are highly trained and have a specialised skill set to provide your school with effective assessment and treatment for children with poor fine motor skills. The OT will provide an inclusive, tailored and specific treatment plan for the children of that group over the duration of the term. Treatment will be adjusted according to the needs of the children within that group.
<br><br>
<h2>What is included in our Occupational Therapy Fine motor Group for Schools?</h2><br>
The Fine motor Occupational Therapy group for primary schools will contain:
<ul class="ticks">
<li>Two formal assessments completed at week 1 and in the final week</li>
<li>Ongoing informal assessment</li>
<li>Detailed, tailored and need-specific treatment plans</li>
<li>Ongoing treatment designed to make lasting improvements</li>
<li>Final individual report to summarise the group</li>
<li>Further recommendations for school and the classroom for each child</li>
</ul>
<h2>When will the Occupational Therapy Fine motor Groups for Schools take place and how long will the group run for?</h2><br>
Our Occupational Therapy Fine motor group for schools take place during the school day. The fine motor group runs during term time only and can be purchased for a full year (at reduced rates) or on a term by term basis.<br><br>
