<br><h1>OT Groups for Schools</h1>
<br>
<p>In every class in both Primary and Secondary Schools, there are on average there are 3-5 children who have difficulty writing, co-ordinating movements or concentrating. Children who find writing, movement or concentrating difficult are likely to be falling behind the rest of the class, even if they are receiving regular extra input within school. It is common that the extra input within school is providing some benefit, but with limited improvements. </p>
<br>
<p>Our Occupational Therapists have the experience and knowledge to assess and provide effective group treatment for children who are having difficulties writing, co-ordinating movements or concentrating within school. With our help, these children can develop the vital skills needed for success in school. </p>
<br>


<p>OT for Kids currently provide a growing service across schools in the North West in implementing Occupational Therapy (OT) groups to address handwriting, fine motor skills, co-ordination and concentration or listening skills.</p>

<br>
<h2>Benefits to School</h2>
<br>
<p>OT for Kids understand the pressures on modern schools, with the spending of pupil premium being examined and performance related pay, it can be difficult. By choosing to work with OT for Kids in providing effective Occupational Therapy (OT) group treatment, your school will certainly benefit. See the benefits of OT groups for children in your school below:</p>

<ul class="ticks">
<li>Justified spending of pupil premium</li>
<li>Improved writing and concentration in lessons</li>
<li>Onsite Occupational Therapist</li>
<li>Bring together health and education to ensure the best for each child</li>
<li>Understand more about the prevention and treatment of poor handwriting</li>
<li>Improved understanding of a child's sensory system and concentration</li>
<li>Individualised tailored reports for each child who attends the groups</li>
<li>Teaching Assistants are encouraged to be involved</li>
<li>Groups improve key aspects of school curriculum</li>
</ul>

There are many benefits to using OT for Kids to provide OT groups, we can help across many areas within school, and provide a better understanding and treatment for concentration, handwriting, co-ordination and fine motor skills. 
<br><br>
<h2>Groups available:</h2>
<br>
OT for Kids provide five types of groups to schools to address common difficulties. The groups available are:
<br><br>
<a href="/website69b/groups/ot-groups-for-schools/handwriting-group.php" class="bigorange">Handwriting Group </a>
<br>
The handwriting group is aimed at children who are holding their pencil in and an abnormal grip, have messy writing or are having difficulty with writing speed. Children appropriate for the handwriting group find writing difficult, form letters incorrectly and often space their work erratically.
<br><br>
<a href="/website69b/groups/ot-groups-for-schools/fine-motor-group.php" class="bigorange">Fine Motor Group </a>
<br>
The fine motor group is aimed at children who are having difficulty cutting, threading and completing tasks that involve the smaller movements of the fingers. Children appropriate for the fine motor group may present with poor posture and have slow and inaccurate cutting skills.
<br><br>
<a href="/website69b/groups/ot-groups-for-schools/movement-group.php" class="bigorange">Movement Group </a>
<br>
The movement group is aimed at children who are often clumsy, uncoordinated and are finding sports/p.e difficult. Children appropriate for the movement group have poor throwing and catching skills, poor balance and find it difficult to jump, hop or skip successfully.
<br><br>
<a href="/website69b/groups/ot-groups-for-schools/concentration-group.php" class="bigorange">Concentration Group </a>
<br>
The concentration group is aimed at children who are having difficulty following or comprehending verbal instruction, maintaining attention and managing distractions. Children who are appropriate for the concentration group will have difficulty sitting still, listening in class, tuning out from external noise or light or are finding it difficult to comprehend verbal instruction.
<br><br>

<a href="/website69b/groups/ot-groups-for-schools/organisation-group.php" class="bigorange">Organisation Group </a>
<br>
The organisation group is a group designed to improve the ability of children to organise and plan their thought processes, diary, homework schedules and everyday tasks such as packing a bag and money management.
<br><br><br>

<h2>What will be included in each group?</h2>
<br>				
Each group will be completed by an experienced Occupational Therapist, and will receive the following:
<ul class="ticks">
<li>Formal assessments at week 1 and final week</li>
<li>Hour of weekly Occupational Therapy group treatment for a full term</li>
<li>Regular informal assessments to monitor and adapt group treatment</li>
<li>Final individual reports, outlining goals, summary of improvements made and further recommendations for school</li>
</ul>

The assessments will be used to determine the underlying reasons behind the difficulties and be the basis for all future group treatment sessions. Regular informal assessments are used to monitor progress, adjust treatment accordingly in order to maintain the high standards during each group treatment session. The report will outline the purpose of the group, improvements made during treatment sessions and a detailed summary with further recommendations to schools for each child. 
<br><br>
<h2>Where and when are the OT for Kids groups completed</h2>
<br>
OT groups are available during term time for a minimum of one day of groups per school. Groups can be completed on as many days as desired. One of our Occupational Therapists would complete the groups within school and run throughout the school day.
<br><br>
<h2>Why does your school need one of our Occupational Therapists?</h2>
<br>
Our Occupational Therapists (OT's) are the people who have the knowledge and skills needed to assess and provide recommendations to your school. An OT from OT for Kids will outline the reasons why some children are having difficulty and provide treatment using purposeful and meaningful activities, combined with environmental adaptations to improve the development and increase the overall competence of these children within school. 
