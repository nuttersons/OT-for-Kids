<h1>Organisation Group</h1>
<br>
The Organisation group is a group designed to improve the ability of children to organise and plan their thought processes, diary, homework schedules and everyday tasks such as packing a bag and money management. Through the help of our Occupational Therapists at OT for Kids, children can learn the key skills needed to organise and plan their lives successfully. 
<br><br>
For children who struggle with organising and planning, it is often because they lack the key skills needed to complete these tasks. Skills that enable children (and adults) to successfully plan and organise their lives are key to development and future employment. The Organisation group will be able to teach children skills such as task analysis, information processing, listening and eliciting key information and techniques to improve memory.
<br><br>
<h2>Two main aims of the Organisation group are:</h2>
<ul class="ticks">
<li> To assess, treat and provide techniques to improve working memory </li>
<li> To assess and improve the key skills needed for successful organisation and planning of day to day life. </li>
</ul>
<br>
<h2>Who are appropriate for the Organisation group in schools?</h2>
<br>
Organisation and planning are key skills that take time to be developed, however most children have begun to utilise these skills effectively by the time they reach Secondary school. Therefore, children who are appropriate for this group are those entering Secondary school, or are currently in secondary school but are struggling with organising their day. Common difficulties that children experience who suitable for this group are:
<br>
<ul class="ticks">
<li>Forgetful and often lose things</li>
<li>Unable to pack their bags independently</li>
<li>Do not bring the right books to lessons</li>
<li>Forget P.E Kits and items for school trips</li>
<li>Difficulty processing complex instructions</li>
<li>Poor working memory </li>
<li>Cannot complete homework on time</li>
<li>Often does not write down or record key aspects of homework</li>
</ul>
<br>
Children will be grouped based on age and behaviour. It is important that groups are kept with children at a similar age and ability for the groups to run effectively.
<br><br>
<h2>How can our Occupational Therapist improve the Organisation of children at your school? </h2>
<br> 
Occupational Therapists will have extensive experience of providing therapy within schools. An Occupational Therapist at OT for Kids will be able to assess and provide effective solutions to the difficulties each child faces when attempting to organise and plan events or day to day life. The onsite Occupational Therapist will be able to provide treatment, through both active discussions and skill-specific activities to improve the key skills needed for good organisation and planning.
<br> <br>
<h2>What is included in our Occupational Therapy Organisation Group for Schools?</h2>
<br>
<b>The Organisation Occupational Therapy group for primary schools will contain:</b>
<br>
<ul class="ticks">
<li>Formal assessment carried out at the beginning and end of each term to monitor improvements made in therapy</li>
<li>Informal ongoing assessment and treatment</li>
<li>Tailored treatment plans</li>
<li>Final individual report containing further recommendations for schools</li>
</ul>
<h2>When will the Occupational Therapy Organisation Groups for Schools take place, and how long will the group run for?</h2>
<br>
Our Occupational Therapy Organisation group for schools take place during the school day. The Organisation group runs during term time only and can be purchased for a full year (at reduced rates) or on a term by term basis. 
