<h1>Handwriting Group</h1>
<br>
It is highly common for children to have difficulties developing the skills and appropriate pencil grip needed for fluent, legible handwriting. From our experience, OT for Kids have found that there are many children within the classroom who would benefit from our Occupational Therapy handwriting group. The purpose of the handwriting group is to assess the underlying reasons behind poor handwriting and provide effective treatment through small group therapy (no more than 6 children per group). The handwriting group has two aims, these aims are listed below:<br><br>
<img src="/website69b/images/handwriting-example2.png" width="100%">
<h2>Two main aims of the handwriting group are:</h2>
<ul class="ticks">
<li>To assess and improve pencil grip and posture</li>
<li>To assess and improve hand strength, manipulation and the underlying visual perception skills needed for legible handwriting</li>
</ul>
<h2>Who is appropriate for the handwriting group in schools?</h2>
<br>Pencil grip and posture are one of the tell tale signs that a child is having difficulty controlling their hands in producing neat writing. Children should be encouraged to hold a pencil in a dynamic tripod grip from an early age to encourage hand development and proper writing style. A dynamic tripod grip is with the pencil held predominantly by the index and thumb with the middle finger resting below for support and the ring and little finger tucked away into the palm.<br><br>
As well as an improper pencil grip, children who also are struggling to cut, mould, build, understand the concepts of shape, size and form and who are having pain when writing or colouring also need help from our Occupational Therapists.<br><br>
Any child who is displaying the following signs when writing needs input from one of our Occupational Therapists at school:<br>
<ul class="ticks">
<li>Forward or backward, sloped leaning posture (head on table or close to work or leaning back with arm extended)</li>
<li>Poor or abnormal pencil grip (held by more fingers than just the index and thumb, or by the whole hand in a immature grip)</li>
<li>Difficulty understanding concepts of shape, size and form</li>
<li>Struggles to paint or draw at a level similar to classmates</li>
<li>Has high pressure when writing, drawing or painting (paint brush bristles break off, writing appears dark)</li>
<li>Writing is painful, slow or looks laboured</li>
<li>Difficulty forming letters correctly</li>
<li>Difficulty copying patterns</li>
<li>Messy colouring</li>
</ul>
Children often are very good at finding ways to ensure they can complete the task and you may notice other behaviours as well as the ones above that could indicate a need for input from one of our Occupational Therapists. Children will be grouped based on age and behaviour. It is important that groups are kept with children at a similar age and ability for the groups to run effectively.<br><br>
<img src="/website69b/images/handwriting-example.png" width="100%">
<h2>How can our Occupational Therapist improve the handwriting of children at your school?</h2><br>
Our Occupational Therapists have the necessary experience of not only working with children affected by handwriting difficulties but also in the running of effective, high quality Occupational Therapy groups across a number of schools.<br><br>
Our Occupational Therapists will be able to assess the reasons behind poor writing and provide an effective treatment plan during group time and offer advice on adaptive grips and equipment to teachers in continuing the improvements made during therapy across the school week, when the Occupational therapist is not there.<br><br>
<h2>What is included in our Occupational Therapy Handwriting Group for Schools?</h2><br>
Each group will a formal assessment in week 1 and on the final session followed by a detailed individual report. During the running of the groups, each group will also receive regular informal assessments and up to an hour of high quality treatment. 
<br><br>
An individual report will be completed following the last group session and contain an outline of the group goals, the findings of the assessments, summary of improvements and a section on the further recommendations to school for each child who attended the groups. The final individual report will be a summary of what has been achieved throughout the duration of the groups and a useful tool for teachers, teaching assistants and SENCO's to draw upon in future lessons or as warm ups to current lessons for the whole class.
<br><br>
<h2>When will the Occupational Therapy Handwriting Groups for Schools take place and how long will the group run for?</h2><br>
Our Occupational Therapy Handwriting group for schools take place during the school day as part of an inclusive therapy programme of four groups a day (of any combination of types). The handwriting group runs during term time only and can be purchased for a full year (at reduced rates) or on a term by term basis. <br><br>
