<h2><a id="1" name="1"></a>Where and how is the handwriting assessment completed?</h2>
<br>
The Handwriting assessment is an assessment that can take place in clinic, at home or at school. The therapist would get the child to write a piece of writing in their ‘best’ handwriting using a sentence that contains every letter of the alphabet<br>
<br>
<b>‘The quick brown fox jumps over the lazy dog’<br>
<br>
</b>Depending on the age of your child, the therapist may also ask them to write a sentence from memory to analyse free written letters against copied letters. Generally it is easier to copy a sentence than to write letters from memory. This is because the patterns required to form the letters can be copied from sight rather than pulled from learnt memory. This can indicate a lack of understanding of letter formation, which could go unnoticed, and obviously impact in future writing development. The therapist would then analyse the writing using a handwriting checklist, this covers all aspects needed for successful handwriting, such as letter formation, uniformity of slope and size, spacing, alignment and legibility. Scores are marked from 0 to 5, with 5 being all letters are correctly formed, a total is then calculated. These scores would be used following treatment to provide you with an accurate measurement of improvement.
<br><br>
<h2><a id="2" name="2"></a>What problems may be suitable for a Handwriting assessment?</h2>
<br>
The following problems are areas some examples of areas that an occupational therapist can improve. The handwriting assessment would help elicit these values and provide the therapist with a guide for the treatment available.
<ul class="ticks">
<li>Shaky handwriting</li>
<li>Letters poorly formed</li>
<li>Writing drifting un-aligned across the page</li>
<li>Size of letters seem too large or too small</li>
<li>Writing difficulty to read</li>
<li>Teacher/School have identified difficulties compared to other children</li>
<li>Incorrect pencil grip present/ No tripod grip present</li>
<li>Hand/arm physically shaking when writing</li>
<li>Poor fine motor control</li>
</ul>
If you think your child has poor handwriting, or is behind their classmates, they will probably benefit from an Occupational Therapy assessment.
<br><br>
<h2><a id="3" name="3"></a>Conditions the Handwriting assessment is commonly used for:</h2>
<br>
Below are some of the most common conditions the handwriting assessment is used for:
<ul class="ticks">
<li>Developmental delay</li>
<li>Developmental co-ordination disorder</li>
<li>Dyslexia</li>
<li>Dysphagia</li>
<li>Dyspraxia</li>
<li>Cerebral Palsy</li>
<li>Stroke</li>
</ul>
Occupational therapists can help all kinds of children, including those that do not have an identified condition. Poor handwriting can have a detrimental effect on later life, therefore by ensuring your child is assessed and treated at a young age, you are offering them a better future.
<br><br>
<h2><a id="4" name="4"></a>What will the Handwriting assessment assess?</h2>
<br>
The occupational therapist will use the Handwriting assessment to gain a better insight into how poor handwriting is affecting your Child’s school life and academic ability. This would include a detailed discussion and practical assessment of how the symptoms of Handwriting are impacting on function, and provide reasons why this could be.<br>
<br>
<b>Impact on the school environment</b><br>
The impact of poor handwriting within school can give an unfair reflection of your child’s academic ability, as a lot of assessments of cognitive ability required to show understanding across the curriculum use hand written tests. Therefore if your child is struggling to write clearly and legibly it will have an impact on the scores they receive in tests, for example in English, understanding the alphabet, phonics and sentence structure are all demonstrated through both verbal and written word, similarly in maths timed mental assessments require the child to produce fast efficient legible writing in order to have enough time to process the question asked, calculate the answer and transcribe it on paper.<br>
<br>
<b>Impact on the home </b><br>
These difficulties can also have a negative impact upon the home, as the child may start to dislike writing, making forms, cards and other writing activities a hard and demanding process. This may also put stress on the relationships between parent and child.<br>
<br>
<b>Social/Behavioural implications</b><br>
The therapist will also use the handwriting assessment combined with their clinical expertise and reasoning to pick apart any behavioural issues your child may be experiencing which could be due to a lack of confidence in writing ability or a frustration at how their peers can write but they can’t. These types of hidden emotions can often express themselves through acting out and misbehaving. The therapist will help identify any underlying emotional connections to handwriting using this assessment and through discussing things with your child.
<br><br>
<h2><a id="5" name="5"></a>What information will I receive following a Handwriting assessment?</h2>
<br>
Following an assessment, you can request to receive a Handwriting report based on the findings of this assessment. This will contain a detailed analysis of the impact the condition is having on your life, a treatment plan and recommendations that will improve your daily living.
<br> <br>
<h2><a id="6" name="6"></a>Treatment following a Handwriting assessment</h2>
<br>
Following a handwriting assessment, an occupational therapist would offer a range of treatment options and interventions, below is some examples of the kind of treatment we provide:
<ul class="ticks">
<li>Therapy aimed at improving fine motor skills</li>
<li>Handwriting improvement programme</li>
<li>Multi-sensory approach to handwriting improvement</li>
<li>Clearly defined SMART therapy goals</li>
<li>Outcome based re-assessment following treatment</li>
</ul>
<h2><a id="7" name="7"></a>Summary</h2>
<br>
In summary if you have noticed that your child is not developing their writing as quickly and efficiently as other children in their class, there could be some underlying reasons for this. The handwriting assessment is useful tool that an occupational therapist can use to identify these reasons and use the results as the base for treatment. Handwriting is something that has an impact on the whole of a person’s life from young to old. It is important to address this issue younger rather than older as the brain is more receptive to learning at this age.<br>
