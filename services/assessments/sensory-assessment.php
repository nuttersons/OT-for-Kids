<h1>Sensory Assessment</h1>
<br>
A Sensory assessment would consist of assessing how your child copes with the ever growing load of sensory information that enters their senses on a daily basis. Through analysis of the behaviours the child demonstrates under stress the therapist can help deduce why they may be misbehaving.
<br><br>
<h2>Where and how is it completed?</h2>
<br>
The Sensory assessment is an assessment that can take place in clinic, at home or in school. It will be completed by an occupational therapist through an observation of the behaviours and movements the child shows in relation to the sensory input they are receiving.<br><br>
<h2>What problems may be suitable for a Sensory assessment?</h2>
<br>
Below is a list of some the common problems that are suitable for an Sensory assessment:
<ul class="ticks">
<li>Easily distracted by noises/smells</li>
<li>Rocks/shakes</li>
<li>Flings limbs around</li>
<li>Spinning </li>
<li>Over sensitive to light</li>
<li>Difficulty holding objects</li>
<li>Struggles to identify change in temperature</li>
</ul>
<b>Conditions associated with a sensory assessment.</b>
