<h2>What will the Sensory assessment assess?</h2>
<br>
The occupational therapist will use the Sensory assessment to gain a better insight into how your child perceives the sensory environment and how this affects your child during day to day life, both at school and at home. This would include a detailed discussion and practical assessment of how sensory difficulties are impacting on function, and provide reasons why this could be.<br>
<br>
<b>Impact on the school environment</b><br>
A school has one of the most overloaded sensory environments than any other environment. This can be extremely overwhelming for any child, the sensory assessment would look at the classroom, the atmosphere and help available at school for your child. To cope with the level of sensory information received at school, your child may have developed behaviours that are used to calm their own sensory system. These could be shouting, rocking, or finding somewhere quiet.<br>
<br>
<b>Impact on the home </b><br>
The Sensory assessment will also assess the input your child is receiving at home. For example, In a loud, busy home environment, a child may struggle to take in information, concentrate on a task or partake in family activities.<br>
<br>
<b>Social/Behavioural implications</b><br>
Children with sensory difficulties may express bad behaviour, or behaviours that are socially unacceptable in order to cope with sensory information. This assessment would help identify these and provide strategies that can be used to manage this.<br>
<br><br>
<h2>What information will I receive following an Sensory assessment?</h2>
<br>
Following an assessment, the therapist would first, explain the outcome of the assessment and how this is impacting function. If required you can request to receive a Sensory report based on the findings of this assessment. This will contain a detailed analysis of the impact the condition is having on your life, a treatment plan and recommendations that will improve your daily living.
<br><br>
<h2>Treatment following a Sensory assessment</h2>
<ul class="ticks">
<li>Sensory Integration</li>
<li>Adaptive Equipment</li>
<li>Education and advice to parents and schools</li>
<li>Sensory play</li>
</ul>
<h2>Summary</h2>
<br>
In summary the Sensory assessment would consider how the environment is impacting on your child’s behaviour across all aspects of their life, most notably, the direct effect the condition has on the difficulty, time taken and ability to complete important meaningful daily occupations.<br>
