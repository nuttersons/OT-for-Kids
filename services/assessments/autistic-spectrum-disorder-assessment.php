<h1>Autistic Spectrum Disorder Assessment</h1>
<br>
The Autistic Spectrum Disorder (ASD) assessment consists of a combination of observation, narrative and social assessment. The ASD assessment is used by occupational therapists to better understand how the autistic spectrum is impacting upon their school and home life, most notably during social situations. The occupational therapist would observe your child and by noticing how they respond to social stimuli (such as eye contact, facial expressions) calculate if your child has low level or high level functioning child on the autistic spectrum.<br>
<br>
The occupational therapist that completes the Autistic Spectrum Disorder assessment will incorporate all aspects of childhood activity from physical difficulties to social/behavioural issues that are currently having a detrimental effect on their ability to perform both in school and at home.<br>
