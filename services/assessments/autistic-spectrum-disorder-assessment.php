<h2><a id="1" name="1"></a>Where and how is the Autistic Spectrum Disorder completed?</h2>
<br>
The ASD assessment is an assessment that can take place at home, in school or at one of our clinics. The ASD assessment is completed by the occupational therapist, using their clinical expertise and training, through a combination of a checklist and observation. The checklist will contain questions relating to how your child perceives the world. For example, does the child find social situations difficult, or prefer to play alone rather than with others.
<br><br>
<h2><a id="2" name="2"></a>What problems are my child experiencing that may be suitable for a paediatric Occupational Therapy ASD assessment?</h2>

<br>
A lot of children will experience difficulties at some point in their development; however certain signs may highlight an underlying condition that may be having a poor influence on development. Some of the difficulties that children on the autistic spectrum experience are:
<ul class="ticks">
<li>Difficulty empathising with other children</li>
<li>Awkward in social situations</li>
<li>Does not engage with other children</li>
<li>Fixated on information or patterns</li>
<li>Difficulty following conversation</li>
<li>Appear rude or selfish </li>
<li>Aware they are different to others</li>
<li>Good attention to detail but struggles to see the bigger picture</li>
<li>Difficulty coping with a change of routine/ surroundings</li>
<li>Struggles with understanding the consequence of their actions</li>
<li>Over/under active sensory system
<ul>
<li>Prefers silence or makes loud noises to calm themselves</li>
<li>Doesn’t like being touched/ craves overly firm contact (rubs trousers, likes the feel of rough objects)</li>
<li>Sensitive to light\</li>
<li>Heightened sense of smell </li>
<li>Difficulty concentrating on one task in a busy environment</li>
</ul>
</li>
</ul>
If you notice that your child is experiencing any of the above symptoms, then your child will probably be suitable for a ASD assessment.
<br><br>
<h2><a id="3" name="3"></a>Conditions the ASD assessment assesses</h2>
<br>
The Autistic Spectrum Disorder Assessment is used to assess if and how your child fits onto the autistic spectrum. The autistic spectrum consists of:
<ul class="ticks">
<li>These two conditions make up the Autistic Spectrum</li>
<li>Autism</li>
<li>Asperger’s syndrome</li>
</ul>
An occupational therapist compares the results of the ASD assessment against the autistic spectrum and offer advice and treatment plans on how to improve function.
<br><br>
<h2><a id="4" name="4"></a>What will the ASD assessment assess?</h2>
<br>
The occupational therapist will use the ASD assessment to gain a better insight into how the autistic spectrum is affecting your child during day to day life, both at school and at home. This would include a detailed discussion and practical assessment of how the symptoms of ASD are impacting on function, and provide reasons why this could be.<br>
<br>
Reasons why and impact on function could include:
<ul class="ticks">
<li><a href="/website69b/#a1">Impact on the school environment</a></li>
<li><a href="/website69b/#a2">Impact on the home</a></li>
<li><a href="/website69b/#a3">Impact of ASD on function</a></li>
<li><a href="/website69b/#a4">Social behavioural implications</a></li>
</ul>
<b><a id="a1" name="a1"></a>Impact on the school environment<br>
</b>Autism can have a profound effect on how your child functions within the school environment, for example, friendships, maintaining concentration, following teacher instructions and avoiding distractions in the classroom. The ASD assessment is a vital tool in the therapist’s tool box providing them with a detailed analysis of how the uniqueness of the autistic spectrum is impacting at school. Giving you insider knowledge of what life at school for your child is really like.<br>
<br>
<b><a id="a2" name="a2"></a>Impact on the home<br>
</b>The occupational therapist will use the assessment tool with you to discuss how the condition is impacting on the home. This could include aspects such as physical/behavioural impacts on the time taken to eat dinner or difficulty in calming down enough for bed. For example, children within the autistic spectrum often struggle understanding commands or tone of voice and facial expressions resulting in enforcing boundaries and set bed times becoming difficult.  The occupational therapist would assess how any problems are influencing the dynamics and functional capabilities of the home.<br>
<br>
<b><a id="a3" name="a3"></a>Impact of ASD on function (Physical)<br>
</b>The ASD assessment would also highlight any physical implications on daily function. For example, children within the autistic spectrum could have physical difficulties such as motor planning and execution. The ASD assessment would be used to identify how these difficulties are affecting your child’s ability to complete important occupations (such as getting dressed, running, throwing and catching).<br>
<br>
<b><a id="a4" name="a4"></a>Social/Behavioural implications<br>
</b>Not only does the ASD assessment consider the implications of ASD on the home, school and its relation to physical function. The ASD assessment also considers how Autism and Asperger’s syndrome are affecting the child’s behaviour, and offers possible explanations as to why the child is behaving as they are (at home, school, social events or with others).
<br><br>
<h2><a id="5" name="5"></a>What information will I receive following an ASD assessment?</h2>
<br>
Following an assessment, you can request to receive an ASD report based on the findings of this assessment. This will contain a detailed analysis of the impact the condition is having on your life, a treatment plan and recommendations that will improve your daily living.
<br><br>
<h2><a id="6" name="6"></a>Paediatric Occupational Therapy treatment following an ASD Assessment</h2>
<br>
Possible Occupational Therapy interventions include:
<ul class="ticks">
<li>Sensory integration</li>
<li>Clear SMART goals setting</li>
<li>Therapeutic planning</li>
<li>Advice and education on managing the symptoms of the autistic spectrum</li>
<li>Aids and adaptation assessment and advice</li>
<li>Sensory play therapy</li>
<li>Concentration games</li>
<li>Treatment aimed at educating social rules and communication to your child</li>
</ul>
<h2><a id="7" name="7"></a>Summary</h2>
<br>
In summary the ASD assessment is an assessment aimed at identifying if your child is on the autistic spectrum and how the condition is impacting on your child’s ability to complete activities at home, in school or how the autistic spectrum affects the child socially. Following the ASD assessment the occupational therapist will provide interventions aimed at improving function and managing the symptoms of the autistic spectrum.<br>
