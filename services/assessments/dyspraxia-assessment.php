<h2><a id="1" name="1"></a>Where and how is the Dyspraxia assessment completed?</h2>
<br>
The Dyspraxia assessment is an assessment that can take place at school, home or in clinic. It is completed by an occupational therapist mainly through observation of the kind of activities children partake in whilst at school and around the home. For example, running or physical activity, negotiating corridors without bumping into people, writing, doing up coats or buttons, concentrating, multitasking and remembering information are all activities children do during an average day. Through the use of the DASH assessment tool the therapist will guide you on the severity of dyspraxia and how it may be affecting your child.<br>
