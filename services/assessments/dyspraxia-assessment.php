<h2><a id="4" name="4"></a>What information will I receive following an Dyspraxia assessment?</h2>

<br>
Following an assessment, the therapist will first explain the outcome of the assessment and what this means in relation to your child and their function at home. If required you can request to receive a Dyspraxia report based on the findings of this assessment. This will contain a detailed analysis of the impact the condition is having on your life, a treatment plan and recommendations that will improve your daily living. <br>
