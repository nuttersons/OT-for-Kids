<h2><a id="3" name="3"></a>What will the Dyspraxia assessment assess?</h2>
<br>
The Dyspraxia assessment will assess if your child has Dyspraxia and to gain a better insight into how Dyspraxia is affecting your child during day to day life, both at school and at home. This would include a detailed discussion and practical assessment of how the symptoms of Dyspraxia are impacting on function, and provide reasons why this could be.<br>
<br>
Reasons why your child may need a dyspraxia assessment:
<ul class="ticks">
  <li><a href="/website69b/#a1">Impact on the school environment</a></li>
  <li><a href="/website69b/#a2">Impact on the home</a></li>
  <li><a href="/website69b/#a3">Impact of dyspraxia on function</a></li>
  <li><a href="/website69b/#a4">Social or behavioural implications</a></li>
</ul>
<b><a id="a1" name="a1"></a>Impact on the school environment</b><br>
The Dyspraxia assessment also considers how the condition is impacting upon the child within the school environment. This could be the effect dyspraxia is having on movement in Physical education lessons, or in joining in with other pupils at playtime. Dyspraxia also has an effect on concentration and working memory during class, this could possibly result in the teacher having to repeat instructions directly to your child, or your child becoming lost and unsure on the task  when everyone else starts working.<br>
<br>
<b><a id="a2" name="a2"></a>Impact on the home </b><br>
The Dyspraxia assessment will evaluate the effect the condition is having on home life. This could include aspects such as physical or behavioural impacts on the time taken to complete activities. For example, playing outside with siblings can be a challenge, often resulting in low confidence in their own abilities ‘I can’t do it’ and a lack of motivation to join in, possibly leading to isolation. The occupational therapist would use the dyspraxia assessment tool to assess how these problems are influencing the dynamics and functional capabilities of the home.<br>
<br>
<b><a id="a3" name="a3"></a>Impact of Dyspraxia on function (Physical)</b><br>
The Dyspraxia assessment would highlight the physical implications on daily function. For example, children with Dyspraxia have common difficulties in areas such as coordination and planning. The Dyspraxia assessment would be used to identify how these difficulties are affecting your child’s ability to complete important occupations (such as getting dressed and gross motor skills).<br>
<br>
<b><a id="a4" name="a4"></a>Social/Behavioural implications</b><br>
Not only does the Dyspraxia assessment consider the implications of Dyspraxia on the home, school and its relation to physical function, it also considers how Dyspraxia affects the child’s behaviour. Suggesting possible explanations as to why the child is behaving as they are (at home, school, social events or with others) <br>
