<h2><a id="2" name="2"></a>What problems may be suitable for a paediatric Occupational Therapy dyspraxia assessment?</h2>
<br>
You may notice your child experiencing some of the problems listed below, if so they are likely to need a paediatric Occupational Therapy dyspraxia assessment.
<ul class="ticks">
  <li>Difficulties planning movements</li>
  <li>Bumping into objects/people frequently</li>
  <li>Clumsy</li>
  <li>Uncoordinated</li>
  <li>Unable to complete simple or complex movements (skipping, hoping jumping, running)</li>
  <li>Struggles to maintain balance </li>
  <li>Fidgeting/ unable to stay still</li>
  <li>Easily excited</li>
  <li>Avoidance of construction based toys</li>
  <li>Dominate hand not established</li>
  <li>Struggles to understand verbal instruction</li>
  <li>Concentration difficulties</li>
  <li>Struggles to multitask</li>
  <li>Struggles to remember more than a few pieces of information</li>
  <li>Poor handwriting</li>
  <li>Slow at getting dressed</li>
</ul>
