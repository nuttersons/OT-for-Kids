<h2><a id="5" name="5"></a>Treatment following a Dyspraxia assessment</h2>
<br>
Some of the possible Occupational Therapy interventions following a dyspraxia assessment are: 
<ul class="ticks">
  <li>Gross motor groups</li>
  <li>1:1 therapy</li>
  <li>Improved motor planning</li>
  <li>Reduced clumsiness</li>
  <li>Education and advice to parents and schools</li>
  <li>Social integration</li>
  <li>Concentration games</li>
  <li>Memory games</li>
  <li>Environmental  adaptations/reduction of distractions<br>
  </li>
</ul>
<a href="#top">Back to top </a><br>
<br>
The occupational therapist would assess and determine the most suitable intervention that is most likely to address your child’s individual needs.
<br><br>
<h2><a id="6" name="6"></a>Summary</h2>
<br>
In summary the Dyspraxia assessment is an assessment tool used by occupational therapists to evaluate how dyspraxia is impacting upon your child’s function, which can take place at school, home or in clinic. It is required if your child has difficulty planning movements, is clumsy or uncoordinated or struggles to remain concentrated. Following a dyspraxia assessment, the occupational therapist can offer selected treatments aimed at improving function.<br>
