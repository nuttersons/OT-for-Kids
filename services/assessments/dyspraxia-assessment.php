			  <h1>Dyspraxia Assessment</h1>
	      <br>
The Dyspraxia Assessment is an assessment tool used by occupational therapists to evaluate if the child has symptoms that relate to a condition called Dyspraxia and how dyspraxia is impacting upon your child’s function in relation to activities completed both at school and at home. <br>
<br>
Taking a holistic approach, the Dyspraxia assessment incorporates all aspects of childhood activities, from physical difficulties to social or behavioural issues that are currently having a detrimental effect on their ability to perform in school and at home.
