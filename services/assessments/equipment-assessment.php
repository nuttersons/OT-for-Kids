<h2>Impact on the environment:</h2>
<br>
<b>Impact on the school </b><br>
The Equipment assessment also considers how the condition is impacting upon the child within the school environment. This would include a detailed discussion and practical assessment of how easily it is for your child to move around school, get to lessons on time and access the facilities.<br>
<br>
<b>Impact on the home </b><br>
The therapist will also assess how your child copes around the home, in terms of independently completing activities such as eating, dressing, going up stairs and moving around the home safely. These sorts of difficulties could put stress and strain on the family, as an adult will most likely be sacrificing their time to look after the child.<br>
<br>
<b>Social/Behavioural implications</b><br>
The decision to choose to have equipment that can show a difference between your child and others is difficult, however you can be assured that our therapists will talk through any concerns you may have regarding this and offer advice on how to manage this, and provide your child with the confidence and understanding to support the use of adaptive equipment.<br><br>
<h2>What information will I receive following an Equipment assessment?</h2>
Following an assessment, the therapist will discuss the outcomes of the assessment with you and how this is impacting on home and school. If desired you can request to receive an Equipment report based on the findings of this assessment. This will contain a detailed analysis of the impact the condition is having on your life, a treatment plan and recommendations that will improve your daily living.
<br> <br>
<h2>Treatment following an Equipment assessment</h2>
Following an equipment assessment is it likely that your child may benefit from other forms of Occupational Therapy, below is a list of some of the other available interventions:
<ul class="ticks">
<li>Fine motor games</li>
<li>Gross motor groups</li>
<li>1:1 therapy</li>
<li>Aids and adaptation advice and education</li>
<li>Education</li>
</ul>
<h2>Summary</h2>
In summary, the equipment assessment would be used to determine if a condition is having or will have a serious impact on function within school and at home, alongside the social implications this may have. The therapist would be there to assess and analytically describe the most suitable piece of equipment to maximise your Childs independence and be there to discuss any concerns you may be having.<br>
