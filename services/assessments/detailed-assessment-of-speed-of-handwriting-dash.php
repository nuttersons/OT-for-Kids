<h2><a id="1" name="1"></a>Where and how is DASH completed?</h2>
<br>
The DASH assessment is an assessment that can take place in clinic, at home or in school. It is completed by the occupational therapist through interacting with the child in writing several different tasks, ranging from words per minute to the best handwriting they can master.
<br><br>
<h2><a id="2" name="2"></a>Underlying Problems to be aware of that could need a paediatric Occupational Therapy assessment:</h2>
<br>
Handwriting is a complex and challenging task, requiring a high level of fine motor skills, visual perception and pencil control. Some of the aspects that result in poor handwriting are listed below
<ul class="ticks">
<li>Difficulty identifying an object from a crowded background</li>
<li>Poor fine motor control</li>
<li>Difficulty doing up buttons/zips</li>
<li>Difficulty manipulating objects</li>
<li>Difficulty drawing/copying pictures</li>
<li>Uncontrolled pencil movements</li>
<li>Cramped or illegible handwriting</li>
</ul>
An occupational therapist is able to improve a child’s handwriting through a variety of treatment options. For treatment to occur, the occupational therapist must first identify why your child is struggling to write, this can then be used as the basis behind the treatment plan.
<br><br>
<h2><a id="3" name="3"></a>Typical conditions that require a DASH assessment:</h2>
<br>
All children may require some help with their handwriting at some point or another; however poor handwriting is more likely to occur with the following conditions:
<ul class="ticks">
<li>Dyslexia</li>
<li>Dyspraxia</li>
<li>Dysphagia</li>
<li>Stroke</li>
<li>Cerebral Palsy</li>
<li>Muscular Dystrophy </li>
</ul>
Your child may be struggling to write at the same level of their peers, with or without a recognised condition, our occupational therapists can provide assessment and treatment aimed at improving your child’s handwriting.
<br><br>
<h2><a id="4" name="4"></a>What will the DASH assessment assess?</h2>
<br>
The occupational therapist will use the DASH assessment to gain an understanding of the underlying difficulties that result in poor handwriting. This would include a detailed discussion and practical assessment of how poor handwriting is impacting on function, and provide reasons why this could be.<br>
<br>
<b>Impact on the school and home environment</b><br>
Handwriting is a huge part of the school curriculum; it not only affects your child’s grades but has an impact on how they express their academic knowledge. Poor handwriting can lead to the child feeling frustrated at not given marks for points they clearly understand but just cannot express on paper. At home, It can be difficult for the child to complete forms legibly, sign cards and other similar activities that require good handwriting. The occupational therapist would use the DASH assessment tool to determine the aspects of handwriting that your child struggles with most. Handwriting is a complex and difficult skill to perfect, and is not as simple as it seems. By using the DASH assessment the complex processes underlying correct handwriting can be elicited and a treatment plan can be formulated to address any underlying issues.<br>
<br>
<b>Social/Behavioural implications</b><br>
Not only does the DASH assessment consider the implications of poor handwriting on the home, school and its relation to physical function, it also considers how this affects the child’s behaviour and mood. Suggesting possible explanations as to why the child is behaving as they are (at home, school, social events or with others). For example, a child with poor writing can lose interest in school work, become distracted and unfocused on tasks involving writing and ultimately become unmotivated and frustrated with having to write. Through this assessment the therapist can formulate a plan to address these frustrations and improve overall behaviour regarding handwriting.
<br><br>
<h2><a id="5" name="5"></a>What information will I receive following a DASH assessment?</h2>
<br>
Following an assessment, you can request to receive a DASH report based on the findings of this assessment. This will contain a detailed analysis of the impact the condition is having on your life, a treatment plan and recommendations that will improve your child’s handwriting.
<br><br>
<h2><a id="6" name="6"></a>Treatment following a DASH assessment</h2>
<br>
Some of the available Occupational Therapy paediatric treatments are listed below:
<ul class="ticks">
<li>Pencil control</li>
<li>Letter formation </li>
<li>Spacing</li>
<li>Legibility</li>
<li>Alignment</li>
<li>Fine motor control</li>
<li>Painting</li>
<li>Finger painting</li>
<li>Practicing correct grip</li>
<li>Equipment such as pencil grips and sloped writing boards</li>
<li>Advice and education on correct posture and how this impacts on handwriting</li>
</ul>
However we do offer a wide range of treatments, if you feel that your child could benefit from Occupational Therapy aimed at improving handwriting then please call.
<br><br>
<h2><a id="7" name="7"></a>Summary</h2>
<br>
In summary the (DASH) is used to analyse the speed and legibility of a person’s handwriting. Identify the underlying issues behind poor handwriting and is suitable for a range of conditions, including, dyspraxia, dyslexia, stroke, cerebral palsy and muscular dystrophy. The DASH assessment will also evaluate the home and school environment and any social or behavioural implications. Following a DASH assessment the occupational therapist can provide treatment around improving handwriting.<br>
