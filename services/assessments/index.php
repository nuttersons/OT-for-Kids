<h1>Assessment</h1>
<br>
Assessments are a vital stage of the Occupational Therapy process; they provide the therapists with a tool to gather important information regarding your child and the difficulties they are facing. Assessments can be completed where ever you require, including at school at home or in one of our clinics.
<br><br>
<h2> The purpose of assessments in paediatric Occupational Therapy</h2><br>
There are four main reasons why occupational therapists complete assessments, they are:
<ul class="ticks">
<li>To identify any concerns or difficulties and how they impact upon daily occupations</li>
<li>To prioritise main concerns</li>
<li>To naturally observe the child completing activities in school or at home</li>
<li>To observe social relationships and how they affect activities</li>
</ul>
All of our assessments are used to analyse how your child completes daily activities, the results of which will be used to create a therapy plan. Daily activities refer to the activities an average child completes, consisting mainly of three areas: play, school and home life. Typical childhood daily activities include tying shoe laces, getting dressed, balance and play movements and handwriting.
<br><br>
