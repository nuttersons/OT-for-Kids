<br> <br>
<h2><a id="3" name="3"></a>What will the Functional assessment assess?</h2>
<br>
The occupational therapist will use the Functional assessment to gain a better insight into how any functional difficulties are affecting your child during day to day life, both at school and at home. This would include a practical assessment and detailed discussion, to aid in both understanding for yourself and deciding on the right treatment plan for your child.<br>
<br>
<b>Impact on the school environment</b><br>
The Functional assessment would not only identify any functional difficulties but determine how these are impacting on your Childs ability to perform both in school and at home. For example, poor fine motor difficulties identified during assessment could relate to your child being last at getting changed for P.E or struggling to join in construction based play with other children. <br>
<br>
<b>Impact on the home </b><br>
The impact on the home would also be considered, for example, any difficulties your child may be experiencing at school could mean they are not coming home happy, or appear frustrated at what are seemingly relatively easy tasks to complete for someone of their age. This could have a detrimental effect on the home dynamics and relationships between siblings/parents.
<br><br>
<h2><a id="4" name="4"></a>What information will I receive following a Functional assessment?</h2>
<br>
Following an assessment, the occupational therapist will discuss the outcome of the assessment with you and explain in further detail the reasons why your child may be experiencing some difficulties. If desired you can request to receive a Functional report based on the findings of this assessment. This will contain a detailed analysis of the impact the condition is having on your life, a treatment plan and recommendations that will improve your daily living.
<br><br>
<h2><a id="5" name="5"></a>Treatment following a Functional assessment</h2>
<br>
Following a functional assessment the occupational therapist can provide help and interventions. Some of these are:
<ul class="ticks">
<li>Gross motor groups</li>
<li>Movement based activities</li>
<li>1:1 Sessions</li>
<li>Fine motor games (Jenga, building blocks etc)</li>
<li>Handwriting improvement programmes</li>
<li>Dressing practice</li>
<li>Play therapy</li>
</ul>
<h2><a id="6" name="6"></a>Summary</h2>
<br>
In summary the Functional assessment would firstly identify any physical or cognitive difficulties that are having a detrimental effect on function and then apply these to the environment, incorporating how they are impacting upon both school and at home. This assessment would then form the basis for your treatment plan.<br>
