<h1>Risk Assessment</h1>
<br>
A risk assessment would be completed as a component of many assessments. Its purpose is to assess the environment (both physical and social) for any risks that could damage your child’s health. The physical environment would include aspects relating to trips or falls, or objects that may harm your child if they bumped into it, by including the social risks, such as bullying or loneliness, to your child’s mental wellbeing, the therapist is taking a holistic approach (assessing all aspects) which ensures all corners are covered.
<br><br>
<h2>Where and how is it completed?</h2>
<br>
The Risk assessment is an assessment that can take place in <a href="/website69b/about-us/our-clinics/index.php">clinic</a>, at <a href="/website69b/index.php">home</a> or in <a href="/website69b/who-we-work-with/schools.php">school</a>. It is completed by the occupational therapist, and would give you time to discuss any concerns you may have regarding your child’s safety.
<br><br>
<h2>What problems may be suitable for a Risk assessment?</h2>
<br>
Children are more inclined to take risks than adults, as this area of their brain has not developed yet. However some children may have certain problems that increase risk, some examples are below:
<ul class="ticks">
<li>Poor balance </li>
<li>Poor eye sight</li>
<li>Visual perception difficulties</li>
<li>Difficulty standing still</li>
<li>Poor social skills</li>
<li>Uncomfortable in social situations</li>
<li>Attention difficulties</li>
<li>Poor co-ordination</li>
<li>Finds it difficult to make friends</li>
<li>High risk taker</li>
</ul>
<h2>Suitable conditions for a Risk assessment</h2>
<br>
Some conditions that are suitable for a risk assessment are listed below:
<ul class="ticks">
<li><a href="/website69b/conditions/aspergers-syndrome.php">Asperger’s syndrome</a></li>
<li><a href="/website69b/conditions/autistic-spectrum-disorder-asd.php">Autism</a></li>
