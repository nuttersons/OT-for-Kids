</ul>
If your child has not been diagnosed with a recognised condition, but you feel that they could benefit from a risk assessment then we will be more than happy to help.
<br><br>
<h2>What will the Risk assessment assess?</h2>
<br>
The occupational therapist will use the Risk assessment to gain a better insight into the level of risk when your child is completing occupations, this would include an outline of the risks during school life, home life, the impact on function and any social/behavioural implications.<br>
<br>
<b>Impact on the school and home environment</b><br>
The Risk assessment also considers how the condition is impacting upon the child within both the school and home environment. This would include a detailed discussion and practical assessment of how the symptoms of the condition are resulting in these environments becoming more dangerous, and provide reasons why this could be. The main risks within school and home is the risk of falls, especially in schools as they are often cluttered making traversing across the classroom difficult for children with poor movement or visual perception skills.<br>
<br>
<b>Social/Behavioural implications</b><br>
Not only does the Risk assessment consider the implications of Risk within both the home and school environment, it also considers how risk affects the child’s behaviour. For example, a child who is constantly falling over will have low confidence in relating to activities that involve walking over tricky terrain, such as going on a school trip to an outdoor centre or the woods.
<br> <br>
<h2>What information will I receive following a Risk assessment?</h2>
<br>
Following an assessment, the therapist will discuss with you the outcome of the assessment and if required you can request to receive a Risk report based on the findings of this assessment. This will contain a detailed analysis of the impact the condition is having on your life, a treatment plan and recommendations that will improve your daily living.
<br> <br>
<h2>Treatment following a Risk assessment</h2>
<br>
The main aim of treatment following a risk assessment would be to provide effective risk management advice and education, including any necessary equipment that would further reduce risks. Alongside risk management treatment, Occupational Therapy input would also be used, the aim of which being to increase the child’s ability to perform in skills they find difficult. Through improving the underlying skills, the overall risk to the child will subsequently be reduced.
<br><br>
<h2>Summary</h2>
<br>
In summary the Risk assessment would consider how the condition is impacting across all aspects of the child’s life, most notably, the direct effect the condition has on the difficulty, time taken and ability to complete important meaningful daily occupations. <br>
