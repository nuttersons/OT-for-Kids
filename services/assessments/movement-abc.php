<h1>Movement Assessment Battery for Children (Movement ABC)</h1>
<br>
Movement ABC is an assessment tool used to identify a delay or impairment in motor development in children. The occupational therapist would use this tool to assess if any motor development delay or impairment is having a detrimental impact during activities completed both at home and in school. <br>
<br>
The Movement ABC incorporates the impact of an impairment or developmental delay on childhood activities, surrounding their ability to perform well in school and at home.
