<h2><a id="1" name="1"></a>Where and how is the Movement ABC completed?</h2>
<br>
The Movement ABC is an assessment that can take place in clinic, at home or in school. It is completed by a Movement ABC trained occupational therapist. The assessment is a recognised standardised assessment used all over the world by multiple health professionals.
<br><br>
<h2><a id="2" name="2"></a>Problems that could require a Movement ABC assessment:</h2>
<br>
Children who would be suitable for a Movement ABC assessment often display the following symptoms:
<ul class="ticks">
<li>Clumsy</li>
<li>Uncoordinated</li>
<li>Difficulty completing smooth continuous movement</li>
<li>Frequent falls/trips</li>
<li>Fine motor difficulties </li>
<li>Difficulty running, jumping, hoping or completing other gross motor activities</li>
<li>Possible reduced muscle strength or tone</li>
<li>Visual perception difficulties</li>
<li>Poor hand eye-coordination</li>
<li>Loss of function with one limb following stroke</li>
</ul>
If you have noticed that your child is struggling to perform outdoor activities or play games that involve balancing, throwing, catching or any other gross motor activity, then your child is probably suitable for an Occupational Therapy assessment.
<br><br>
<h2>What conditions does Movement ABC asses?</h2>
<br>
There are certain conditions that have symptoms which affect movement and therefore require a movement ABC assessment, these are:
<ul class="ticks">
<li>Dyspraxia</li>
<li>Cerebral palsy</li>
<li>Developmental Coordination Disorder</li>
<li>Developmental Delay</li>
<li>Head Injury</li>
<li>Multiple Sclerosis</li>
<li>Rett Syndrome</li>
<li>Stroke</li>
<li>Spina Bifida</li>
</ul>
However, if your child has not been diagnosed with a condition, but you still feel that they are struggling to complete activities that involve running, hoping, jumping balancing and a range of other movements then please do not hesitate to call us.
<br><br>
<h2><a id="3" name="3"></a>What will the Movement ABC assessment assess?</h2>
<br>
The occupational therapist will use Movement ABC to gain a better insight into how these movement difficulties are affecting your child during day to day life, both at school and at home. This would include a detailed discussion and practical assessment of how the symptoms of Movement ABC are impacting on function, and provide reasons why this could be.<br>
<br>
<b>Impact on the school environment</b><br>
Movement ABC also considers how movement impairment is impacting upon the child within the school environment. For example, during P.E or sport lessons, a child who struggles to move will have great difficulty competing at a similar level as their peers and may even feel isolated or excluded due to their physical difficulties. <br>
<br>
<b>Impact on the home </b><br>
Movement ABC will also evaluate how these movement difficulties impact upon activities at home. This could include aspects such as physical or behavioural impacts on the time taken to complete activities. For example, finding it hard to move smoothly and successfully can reduce the child’s motivation to partake in occupations that get them moving, and therefore could stay inside instead. This could lead to an unhealthy and overweight child, (if they are not receiving enough physical activity) which could also have an effect on mood and happiness. The occupational therapist would work with you in assessing how these problems are influencing the dynamics and functional capabilities of the home and provide a treatment plan to address this.<br>
<br>
<b>Impact of Movement ABC on function (Physical)</b><br>
Movement ABC would also highlight how these motor difficulties impact upon function. As your child develops, they will have to improve and develop alongside their peers; this will be difficult if your child finds new movement patterns hard to master. The Movement ABC assessment would be used to identify how these difficulties are affecting your child’s ability to complete important occupations (such as getting dressed, running and playing) and by using the results from this assessment a treatment plan can be drawn up.<br>
<br>
<b>Social/Behavioural implications</b><br>
Not only does the Movement ABC consider the implications of Movement ABC on the home, school and its relation to physical function, it also considers how Movement ABC affects the child’s behaviour. Suggesting possible explanations as to why the child is behaving as they are (at home, school, social events or with others). If your child finds movements difficult they may become frustrated and easily annoyed, this can be difficult to manage and disrupt the home environment. The Movement ABC will help identify if your child is experiencing any of these.
<br><br>
<h2><a id="4" name="4"></a>What information will I receive following an assessment?</h2>
<br>
Following an assessment, the therapist will discuss with you the outcome of the assessment and how this is impacting on your child’s ability to function at home or at school. If desired you can request to receive a Movement ABC report based on the findings of this assessment. This will contain a detailed analysis of the impact the condition is having on your life, a treatment plan and recommendations that will improve your daily living.
<br><br>
<h2>  <a id="5" name="5"></a>Treatment following an assessment</h2>
<br>
Some of the available treatments that an occupational therapist provides are listed below:
<ul class="ticks">
<li>Gross motor groups</li>
<li>1:1 Therapy</li>
<li>Movement games</li>
<li>Play therapy</li>
<li>Education on how to maximise function</li>
<li>Spatial awareness therapy</li>
</ul>
<h2><a id="6" name="6"></a>Summary</h2>
<br>
In summary Movement ABC would assess how your child moves in relation to occupations and activities. It would look across three aspects, the environment, the occupations (including the difficulty required to complete them) and the person. One of our trained paediatric occupational therapists will then provide a treatment plan following the results of this assessment. <br>
