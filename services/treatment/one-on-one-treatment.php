<h1>One on One Treatment</h1>
<br>
Occupational therapists will mainly work with children on a one to one basis during treatment. The occupational therapist can begin to develop a good working relationship with the child in order to make the most improvements during therapy.
<br><br>
<h2>What types of treatment are covered during one to one therapy?</h2>
<br>
There are many types of treatment covered during one to one therapy, below is a list of some of the treatments we provide:
