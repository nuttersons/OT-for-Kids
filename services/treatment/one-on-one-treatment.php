</ul>
<h2>What does one on one treatment involve?</h2>
<br>
Occupational therapy one to one treatment will be in-line with the treatment plan set out between you and your therapist during the assessment phase. Three main aspects of one to one treatment are:
<ul class="ticks">
<li>Chance for you to discuss any concerns</li>
<li>Individualised therapy sessions</li>
<li>Professionalism and good working relationships</li>
</ul>
<h2>Who can be present during treatment?</h2>
<br>
Anyone related to the child or are involved in the care/wellbeing of the child can attend treatment sessions. However for the therapist to get the most out the child it is advised to save all questions until the end of treatment.
<br><br>
<h2>Why choose one on one therapy?</h2>
<br>
One on one therapy is a great way to make improvements with your child that can translate through to both school and home life. They cover all ranges of treatment options and are available at home, in clinic or at school.
<ul class="ticks">
<li>Collaboratively set personalised goals</li>
<li>Regular reviews tailored and designed around your child</li>
<li>Guided treatment sessions to meet the highest needs of your child</li>
</ul>
<h2>How will a one hour treatment session benefit you and your child?</h2>
<ul class="ticks">
<li>Full hour treatment to address your concerns</li>
<li>Experienced, friendly and enthusiastic therapists</li>
<li>Functional improvements </li>
</ul>
