<br>
The purpose of fine motor  skills during childhood are to enable children to complete important school and  home based occupations such as writing, threading, dressing and tying shoe  laces. Without the ability to complete these activities children will be left  behind academically compared to class mates, especially with handwriting. These  early development of fine motor skills are honed and improved over time and  into adulthood to ensure that they are a high functioning independent adult.<br><br>
<h2>Problems that may require fine motor improvement therapy</h2>
<br>
Below is a list of common problems associated with fine motor control, that an occupational therapist can help improve.
<ul class="ticks">
<li>Unable to tie shoes</li>
<li>Poor handwriting</li>
<li>Difficulty getting dressed</li>
<li>Inability to do up buttons</li>
<li>Slow at fine motor games</li>
<li>Struggles to manipulate objects with both hands</li>
<li>Often compensating by using the body to stabilise objects</li>
<li>Struggles to undo sandwich wrappers/lunch box </li>
</ul>
This is not an exclusive list, and if you think that your child is having difficulty using their hands, manipulating objects or is often finding it hard to maintain grip on objects, then they may be suitable for fine motor treatment.
