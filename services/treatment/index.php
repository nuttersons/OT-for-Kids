<h1>Treatment We Provide</h1>
<br>
The treatment stage of the Occupational Therapy process is one of the most important stages within the Occupational Therapy process. The Occupational Therapy treatment will be where the most improvements are made. The therapist uses their knowledge and skills to provide a treatment plan with the aim of improving your child’s ability to function both at home and at school.
<br><br>
<h2>The purpose of Occupational Therapy treatment</h2>
<br>There are four main reasons behind providing Occupational Therapy treatment to children are to:
<ul class="ticks">
<li>Meet goals set following assessment phase</li>
<li>Improve function in key areas</li>
<li>Increase independence both at home and at school</li>
<li>Improve physical, social and behavioural aspects</li>
</ul>
In order to maintain the high quality services that we provide, all of our treatment sessions will be regularly reviewed and a final outcome used as a form of evidence to show the improved results through therapy.
