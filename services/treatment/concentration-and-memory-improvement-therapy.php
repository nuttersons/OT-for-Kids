<h1>Concentration and Memory Improvement Therapy</h1>
<br>
An occupational therapist can be an effective treatment to improve your child’s concentration and memory. This can be done through different treatment options with the ultimate aim of improving function. Poor concentration can impact heavily both at school and at home, therefore it is important to address this as soon as possible.
<br><br>
<h2>What does concentration and memory entail? </h2>
<br>
Concentration is the ability to focus and sustain your attention on a task at will. Memory however, is the ability to recall, retain and elicit information that has happened in the past. Both concentration and memory have many aspects, for example memory involves short term (recent) memory, long term (years) memory and working memory (used whilst completing sums), whereas concentration contains sustained attention, multitasking and focused attention (ignoring any distractions in the background like cars, chatter etc.). <br>
<br>
In terms of childhood development and the importance of the ability to concentrate and remember information, these abilities are critical to engaging in school, learning and competing at the same level of their peers. Poor concentration or attention can lead to bad behaviour in school, whereas poor memory results in the child struggling to retain information covered in previous sessions and consequently both of these result in their learning being impaired. Of course there is a huge link between concentration and memory, if you can’t concentrate long enough to focus on the information it is impossible to correctly recall this information later on.
<br><br>
<h2>What are Concentration and memory games?</h2>
<br>
Concentration and memory games are activities that require the child to use their concentration skills and/or memory skills.  Some examples include word searches, card games, colouring on one task for a timed period, scrabble and various other games. The theory is that by using the same set of skills required for school work during OT sessions that involve memory and concentration games, the child will develop and improve these particular skills; learning how to maintain focus on a task, and remember information and transferring these schools into day to day life both at home and in school.
<br><br>
<h2>Problems that may require concentration and memory games</h2>
<br>
Below is a list of some examples that may indicate poor concentration or memory:
<ul class="ticks">
<li>Often forgetting P.E kit</li>
<li>Forgets events/who’s picking them up</li>
<li>Unable to remain focused on one task</li>
<li>Struggles to cope with distractions/ noisy environments</li>
<li>Often distracted once focused on a task</li>
<li>Shouts or disrupts class</li>
<li>Struggles to understand social rules (waiting turn to speak etc)</li>
</ul>
This is not an exclusive list, and concentration and memory difficulties can go unnoticed. If you start to realise that your child is struggling to maintain a routine or concentrate during lessons, an occupational therapist will be able to help.
<br><br>
<h2>Suitable conditions for Concentration and memory games treatment</h2>
<br>
Below is a list of conditions that lend themselves to poor concentration and memory, due to the nature of the condition:
<ul class="ticks">
<li>Dyslexia</li>
<li><a href="/website69b/conditions/acquired-head-brain-injury.php">Acquired head injury</a></li>
<li><a href="/website69b/conditions/multiple-sclerosis-child.php">Multiple sclerosis (child)</a></li>
<li><a href="/website69b/conditions/attention-deficit-hyperactivity-disorder-add-adhd.php">Attention deficit disorder (ADD) or Attention deficit hyperactivity disorder (ADHD)</a></li>
<li><a href="/website69b/conditions/dyspraxia-or-developmental-co-ordination-disorder-dcd.php">Dyspraxia</a></li>
</ul>
However, most young children have not been diagnosed with a condition, and if you feel that your child is struggling with concentrating or remembering information then they will probably be suitable for an Occupational Therapy referral.
<br><br>
<h2>Benefits of Concentration and Memory games</h2>
<ul class="ticks">
<li>Improved attention span</li>
<li>Improved concentration</li>
<li>Better quality work</li>
<li>Able to follow instructions easier</li>
<li>Short term memory improvement</li>
<li>Forgetfulness reduced</li>
<li>Able to focus for longer periods</li>
<li>Retains information better</li>
</ul>
