<h1>Gross Motor Activity Training</h1>
<br>
Gross motor activity training is a type of treatment aimed at improving the perception, balance, hand eye co-ordination and many other aspects that are termed under the umbrella of gross motor function. The occupational therapist would use a variety of games and activities to improve movement.
<br><br>
<h2>What does gross motor mean?</h2>
<br>
Gross motor literally translates too large movements, or movements that require a large proportion of the body to move (such as legs, arms and trunk when running). Gross motor movements are the main skills we need to move on a daily basis, or more specifically move safely and successfully in space.
<br><br>
<h2>Why do we need gross motor activities?</h2>
<br>
Controlled movements allow us to run, hop, jump, bend, skip, and do physical activities such as star jumps and sit ups etc effectively. Only through these movements being performed correctly can we successfully compete in sport, complete physical education lessons, safely move around at home/school without bumping into things, or play with other children outside safely.
<br><br>
<h2>What are gross motor activities? </h2>
<br>
Gross motor activities vary greatly in complexity, however there are fundamental underlying skills that support and enable all of us to complete these complex activities. The term gross motor can be broken down into a series of terms used to describe the subconscious thoughts we all use every time we run/hop/jump/skip and complete other similar activities. Below is an inconclusive list of these aspects:
<ul class="ticks">
<li>Balance</li>
<li>Posture</li>
<li>Hand/foot-eye co-ordination</li>
<li>Visual and auditory figure-ground discrimination
<ul>
<li>The ability to distinguish depth of a sound/object in space</li>
</ul>
</li>
<li>Proprioception
<ul>
<li>The perception of their body/limbs in space</li>
</ul>
</li>
<li>Bilateral integration and co-ordination
<ul>
<li>The use and control of both hands/feet in relation to objects in space</li>
</ul>
</li>
<li>Spatial Awareness
<ul>
<li>Assess and move accordingly to spatial relationships between objects and the body</li>
</ul>
</li>
<li>Floor based activities
<ul>
<li>Sit ups, four point balancing, push ups, rolling over</li>
</ul>
</li>
<li>Initiate movements
<ul>
<li>Start movements on command</li>
</ul>
</li>
<li>Stabilising muscle movements
<ul>
<li>Needed for balance when bending</li>
</ul>
</li>
<li>Stop movements on command without overflow
<ul>
<li>Stop movements immediately without carrying after stop command</li>
</ul>
</li>
<li>Rhythmicity of movement
<ul>
<li>Move in a similar pattern to music/rhythm</li>
</ul>
</li>
<li>Motor planning and timing
<ul>
<li>Plan and time movements successfully in relation to the task</li>
</ul>
</li>
<li>Motor execution
<ul>
<li>The ability to control and execute smooth movements independently</li>
</ul>
</li>
<li>Midline Orientation
<ul>
<li>Bringing the hands to the middle of the body</li>
</ul>
</li>
<li>Temporal awareness
<ul>
<li>Appreciation of speed and timing</li>
</ul>
</li>
<li>Form consistency
<ul>
<li>Appreciation of the size of objects</li>
</ul>
</li>
<li>Vestibular regulation
<ul>
<li>Balance regulation using the receptors in the inner ear</li>
<li>Maintain balance in sitting, standing and moving</li>
<li>Detecting speed and direction of movements</li>
</ul>
</li>
<li>Memorising and recalling muscle movement patterns (Muscle memory)
<ul>
<li>Complete memorised motor patterns</li>
<li>Learn and remember new motor patterns </li>
</ul>
</li>
<li>Listening, understanding and responding to instruction</li>
</ul>
<h2>Problems that may require gross motor activity training</h2>
<br>

Some of the problems that may require gross motor activity training include:
<ul class="ticks">
<li>Poor balance</li>
<li>Clumsy</li>
<li>Poor Physical Education grade</li>
<li>Slow at completing movements</li>
<li>Difficulty co-ordinating more than one movement at the same time</li>
<li>Messy eater</li>
<li>Difficulty riding bike/pedalling</li>
<li>Poor swimmer</li>
</ul>
The theory behind gross motor activity training is underpinned by two main approaches in Occupational Therapy: top down and bottom up therapy. <br>
<br>
Top down therapy analyses the task and through therapy, the task is repeated, simplified and made more difficult until the child has developed the skills needed to complete this particular task. As a consequence the underling skills will also improve, but only in relation to this one particular task.<br>
<br>
However, bottom up therapy takes a more foundational approach, it focuses on improving the skills underlying the task (such as balance, core strength and hand eye co-ordination), with the aim of improving the overall task speed and success through improvement in these foundation skills. These improved foundation skills can be transferable across other tasks (not just the highlighted task).
<br><br>
