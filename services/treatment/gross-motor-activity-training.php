<h2>Benefits of gross motor activity training:</h2>
<ul class="ticks">
<li>Improved balance</li>
<li>Improved co-ordination</li>
<li>Better performance in sport P.E and swimming</li>
<li>Able to play and compete on a similar level to classmates</li>
<li>Improved confidence in independence and ability</li>
<li>Improved perception skills</li>
<li>Muscle strengthening</li>
<li>Improved instruction attention and understanding</li>
<li>Motor planning and execution optimisation</li>
<li>Improved proprioception skills </li>
</ul>
