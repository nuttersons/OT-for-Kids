<h1>Sensory Integration Therapy</h1>
<br>
Sensory Integration is a therapy approach that aims to enable children to internalise and regulate their sensory responses to the environment around them. Every activity we do requires a high tech calibration of our senses. For with poor integration of touch and proprioception (the perception of limbs in space), we struggle to identify the correct weight of an object or the correct amount of pressure needed when completing tasks such as holding objects, writing or drawing. Poor sensory integration can have a dramatic effect on a child’s handwriting, ability to do up buttons and zips or complete physical activities such as jumping/skipping. <br>
<br>
Sensory integration therapy uses activities to create or reinforce the connections between the body’s sensory system (such as the skin, nerves and hairs) and the brain. Some children do not correctly develop sensory processing (or integration) through normal development, and therefore will require therapy to enable them to reach these important developmental stages. As required by the College of Occupational Therapists, all Sensory integration practitioners must be fully qualified and trained in sensory integration, ranging from the first level of training to the fourth tier (in which therapists must complete a PhD and research project in sensory integration)<br><br>
<h2>Problems that may require Sensory Integration therapy:</h2><br>
Below is a list of some of the common issues that may require sensory integration therapy:<br>
<br>
<h2>Sensory seeking behaviour </h2>
<ul class="ticks">
<li>Swinging/Spinning</li>
<li>Jumping/throwing themselves around</li>
<li>Purposely hitting themselves against objects</li>
<li>Screaming/shouting/ making lots of noise</li>
</ul>
<h2>Sensory avoidance </h2>
<ul class="ticks">
<li>Seeks quiet corners</li>
<li>Unlikely to engage in loud activities </li>
<li>Finds noisy/bright/smelly environments overwhelming</li>
<li>Doesn’t have many friends</li>
</ul>
<div class="clear"></div>
An occupational therapist  can help improve your child’s ability to regulate their senses. This would be  done through a variety of treatment options.<br><br>
<h2>Other Problems include</h2>
<ul class="ticks">
<li>Poor balance</li>
<li>Motor difficulties</li>
<li>Different response to playground activities as other children</li>
</ul>
The theory behind Sensory  Integration is that through completing tasks that have a high sensory  involvement; the senses, body and mind will automatically form an  interconnection and thus learn how to interpret the responses it receives from  the activity. For example through enabling a child to play, swing, jump bounce  and a whole range of other sensory activities they learn, over time, how to  calm their sensory system (if they have an under-active sensory response  system), how to respond to movement in space correctly (such as catching and dodging  objects) or to learn their own strength in relation to other children and/or  objects like a pencil (if children are consistently breaking objects  unintentionally).<br><br>
