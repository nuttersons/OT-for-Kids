<h2>Benefits of Sensory Integration:</h2>
<ul class="ticks">
<li>Sensory Integration offers an explanation behind your child’s behaviours</li>
<li>Understand why your child may be acting out/throwing tantrums  frequently</li>
<li>Improved sensory processing and integration and therefore improved functional task ability</li>
<li>Improved concentration</li>
<li>Improved balance and motor skills</li>
<li>Social improvements</li>
</ul>
