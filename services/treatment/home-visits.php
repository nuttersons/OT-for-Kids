<h1>Home Visits</h1>
<br>
A home visit enables the therapist to analyse and treat the problems and difficulties your child is having performing everyday tasks at home first-hand. The occupational therapist would be able to
<ul class="ticks">
<li>Provide treatment at home</li>
<li>Risk assess</li>
<li>Discuss the use of adaptive aids and equipment</li>
<li>Wheelchair access </li>
</ul>
Home visits are designed for your convenience; the therapist can travel to you to provide an array of treatment options.
<br><br>
<h2>Safety and risk assessment</h2>
<br>
It is important to remain safe at home whilst using equipment and completing everyday tasks such as brushing teeth, getting dressed and playing outside. The occupational therapist can help you ensure your child is safe. The occupational therapist will assess how your child completes activities and provide advice on how these can be done differently to promote safety and independence. <br>
<br>
Also by assessing occupations at home, in the natural environment, the occupational therapist can observe a more realistic demonstration than if the same occupation was completed in clinic. For example, a child attempting to get dressed in clinic will have their clothes laid out, ordered and easily available for them. Where-as at home they may have to select their clothes out the wardrobe, take them out and organise the clothes prior to putting them on. This adds several steps into the activity, thus making it a much more difficult task to complete. By completing a home visit, occupational therapists can provide accurate advice on how to make the task easier, in order to enable your child to improve their function.
<br><br>
<h2>What will be involved in a home visit?</h2>
<br>
Home visits will tend to follow a set treatment plan that has been previously discussed between yourself and the therapist. The therapist can also provide assessment at home, and from there provide treatment. Some of the aspects of a home visit include:
<ul class="ticks">
<li>Observation of activities in natural environment</li>
<li>Advice and education </li>
<li>Occupational optimisation </li>
<li>Chance to discuss any concerns</li>
<li>1 hour treatment session</li>
</ul>
Occupational optimisation is a term used by occupational therapists to enhance and improve function of your child in their daily activities (such as writing, getting dressed and playing). The overall aim of a home visit is to provide treatment in an environment that is comforting for both yourself and your child, and to enhance their skills.
<br><br>
<h2>Problems that may require a home visit</h2>
<br>
There are a number of reasons why you may require a therapist to visit to your home, some of which are:
<ul class="ticks">
<li>Difficulty getting dressed</li>
<li>Difficulty using toilet or accessing facilities </li>
<li>Fatigues easily</li>
<li>Poor concentration</li>
<li>Mobility and access difficulties</li>
</ul>
An occupational therapist would be suitable for a home visit providing your child is having some difficulty in an area of activity, or if you have noticed that they are not developing at the expected level.
<br> <br>
<h2>Suitable conditions for a home visit</h2>
<br>
A condition will usually reduce the ability of your child in completing occupations, some of which are listed below:
<ul class="ticks">
<li>Cerebral Palsy</li>
<li>Stroke (Child)</li>
<li>Multiple Sclerosis </li>
<li>Muscular Dystrophy</li>
<li>Dyspraxia</li>
<li>Developmental Co-ordination Disorder\</li>
<li>Autism</li>
<li>Acquired brain/head Injury</li>
</ul>
However, your child is suitable for therapy, even without a diagnosed condition. The occupational therapist will be able to asses and provide treatment within your home, designed to make your life easier, and get the help your child needs.
<br><br>
<h2>Benefits of home visits:</h2>
<br>
Some of the benefits of home visits are:
<ul class="ticks">
<li>Improve morning routines </li>
<li>Increase child’s independent </li>
<li>Opportunity to discuss concerns at home</li>
<li>Equipment and aids adaptation advice and education</li>
</ul>
