<h1>Our Services</h1>

OT for Kids offer a range of Occupational Therapy services for children who have a wide variety of problems and conditions in convenient locations to suit you, including at home, in clinic or at school. Our experienced therapist will be able to help you and your child better understand the problem or condition and what assessment or treatment is the best next step. Services we provide are:<br>
<div class="split_left">
<h2><a href="/website69b/services/assessments/index.php">Assessment</a></h2>
We offer a wide variety of assessments including handwriting, movement, and attention based assessments, assessments of the home and equipment and assessments for children within the autistic spectrum.


<h2><a href="/website69b/services/reports/index.php">Reports</a></h2>
Following an assessment, a written report is available upon request. The purpose of the report can be for a second opinion, as a written document for future reference or as part of supportive evidence in a court-case (medico-legal).


<h2><a href="/website69b/services/treatment/index.php">Treatment</a></h2>
We are able to treat a variety of difficulties and conditions, including sensory integration, fine and gross motor improvement therapy, handwriting improvement therapy and concentration and memory improvement therapy. We can offer these treatments at home, in clinic or in school and in a one to one or group basis.


<h2><a href="/website69b/services/consultancy.php">Consultancy</a></h2>
We offer a consultancy service for insurers, schools, parents, case workers and solicitors. Our consultancy service offers options and recommendations regarding the health of an individual.


<h2><a href="/website69b/services/seminar-and-conference-talks.php">Seminar and Conference Talks</a></h2>
OT for Kids provide conference and seminar talks at events nationwide. Our experienced and well established Occupational Therapists are able to provide talks and seminars to delegates across the country.


<h2><a href="/website69b/services/sloped-writing-boards.php">Sloped Writing Boards</a></h2>
OT for Kids provide Sloped Writing Boards to children with handwriting difficulties. These can be used at home and at school. They are a useful tool for children to improve posture when writing.


</div>
<div class="split_right">
<h2><a href="/website69b/groups/ot-groups-for-schools/index.php">OT Groups for Schools</a></h2>
OT for Kids regularly work with primary and secondary schools in providing group Occupational Therapy children within your school. OT groups involve 4 groups of your choosing ran across the school day. There are a choice of 5 groups available, a handwriting, concentration, movement, organisation and fine motor skills.


<h2><a href="/website69b/services/school-consultancy.php">School Consultancy </a></h2>
OT for Kids provide in-depth assessments, follow up reports and further recommendations for individual cases in schools. Our Occupational Therapist will be able to come to your school to provide 1:1 assessments, verbal advice and support to teaching staff and follow up individual reports.


<h2><a href="/website69b/services/ot-assistant-classroom-and-home-support.php">OT Assistant for Classroom or Home Support</a></h2>
OT for Kids provide 1:1 support to children with moderate to severe learning needs due to an injury or illness for long periods. An OT assistant would provide support within a classroom or home setting. The OT assistant would be overseen and supported by a Senior Occupational Therapist.


<h2><a href="/website69b/services/experiencing-differences-day.php">Experiencing Differences Day </a></h2>
OT for Kids offer an Experiencing Differences day to schools. This day consists of a talk and activities for teachers and pupils to participate in to improve awareness and understanding of the common difficulties children face in school.


<h2><a href="/website69b/services/teacher-training.php">Teacher Training</a></h2>
OT for Kids provide detailed and Learning objective based training to teachers across a range of common difficulties for children in school, including handwriting, concentration, movement, sensory processing and co-ordination difficulties.
