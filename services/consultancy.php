<h1>Consultancy</h1>
<br>
OT for Kids offer a consultancy service, we work with insurers, solicitors and case managers in offering opinions and recommendations regarding the health and well-being of an individual.<br> 
<br>
The occupational therapist can provide a consultancy service to companies who deal with individuals following an injury, trauma or medical negligence. The occupational therapist would be able to assess the impact of this on the child’s ability to currently function at home, in school and also analyse how this could impact upon the future of the child, including development and the cost of any adaptive equipment or modifications that may need to be made to ensure a good quality of life.<br>
<br>
We also work alongside other health professionals, including <br>
<ul class="ticks">
<li>Physiotherapists</li>
<li>Speech and language therapists</li>
<li>Podiatrists</li>
<li>Orthotics<br>
</li>
</ul>
