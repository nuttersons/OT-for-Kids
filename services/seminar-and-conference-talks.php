<h1>Seminar and Conference talks</h1>
<br>
OT for Kids provide talks to conferences and seminars nationwide by Occupational Therapy specialists. Our Occupational Therapists are all highly experienced within their field and can offer evidence based advice on a wide variety of topics at many events.
<br><br>
<h2>What type of events do we work at?</h2>
<br>
OT for Kids provide talks to many individuals varying from teachers to case managers. Some examples of the people we provide talks too are: 
<ul class="ticks">
<li>Teaching staff
</li><li>Manual handling trainers 
</li><li>Special Educational Needs Co-ordinators 
</li><li>Head teachers
</li><li>Parent Teacher Associations 
</li></ul>
<br>

<h2>What type of topics do OT for Kids cover at events?</h2>
<br>
We can provide talks on the following:
<ul class="ticks">
<li>Seating and postural management
</li><li>Handwriting
</li><li>Concentration
</li><li>Movement and co-ordination difficulties
</li><li>Posture
</li><li>Child development
</li><li>Classroom environmental modifications for children with sensory difficulties 
</li><li>Sensory processing disorders
</li><li>Cerebral palsy and other neurological conditions
</li></ul>
This is not an exclusive list and if you feel that there is a topic that is not listed here, please <a href="/website69b/contact-us/index.php">contact us</a>.
