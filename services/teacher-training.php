<h1>Teacher Training</h1>
<br>

OT for Kids provide detailed teacher training for a variety of training opportunities for schools. We believe that prevention is better than cure. Our Occupational Therapy teacher training provides detailed theory and application for a range of common difficulties within school that can be adapted and applied across a variety of school environments. 
<br><br>

<h2>Benefits of Occupational Therapy teacher training: </h2>
<br>
OT for Kids provide excellent training to schools across the U.K. Each school that uses our service has found it to be extremely rewarding and engaging experience. OT for Kids training provides the following benefits to schools:
<ul class="ticks">
<li>Clearly defined learning objectives 
</li><li>Certificate upon completion
</li><li>Take-away learning pack and presentation
</li><li>Engaging and challenging activities 
</li><li>Key theory and practices for child development
</li><li>Full classroom approach
</li><li>Advice and support on warm up activities and whole class activities to improve fundamental skills
</li><li>Flexible rates 
</li><li>Rewarding and insightful training experience 
</li><li>You choose the length and topics for your training
</li></ul>
We are able to provide training across many aspects key to child development. You can choose the topics and length of the training relevant to the needs of your school. We provide detailed presentations and engaging activities to reinforce learning. 
<br><br>

<h2>Learning Objectives and Topics</h2>
<br>
OT for Kids provide a range of training opportunities to schools. With all of our training, we have specific learning objectives tailored to improved performance for all involved. The following breaks down our learning objectives for the training we can offer: 
<br><br>
<b>Handwriting and fine motor skills</b>
<ul class="ticks">
<li>To be able to identify poor pencil grip, letter formation and visual perception skills. 
</li><li>To understand normal handwriting development 
</li><li>To understand the underlying skills needed to write 
</li><li>To be able to understand the importance of posture when writing
</li><li>To practice and apple activities that you can complete in class to increase legibility of writing
</li></ul>
<b>Concentration</b>
<ul class="ticks">
<li>To be able to identify children with poor concentration
</li><li>To be able to identify sensory behaviours that indicate why a child is struggling to concentrate
</li><li>To understand and apply individualised strategies to improve concentration 
</li><li>To identify classroom activities to improve concentration
</li></ul>
<b>Movement and Co-ordination</b>
<ul class="ticks">
<li>To be able to identify children with poor co-ordination of upper and lower limbs
</li><li>To be able to understand the systems and underlying skills needed for successful co-ordination
</li><li>To practice and understand suitable activities for children with co-ordination difficulties
</li><li>To understand and apply key phrases when completing movement based activities
</li></ul>
<b>Organisation and Planning</b>
<ul class="ticks">
<li>To be able to identify children who have poor organisation and planning skills
</li><li>To be able to recognise and apply strategies to overcome organisation and planning defecits
</li><li>To practice and apply activities that can be used to improve organisation and planning skills
</li><li>To understand and apply key concepts across day to day lesson planning.
</li></ul>
<b>Autism, the sensory system and sensory processing disorders</b>
<ul class="ticks">
<li>To be able to identify children with sensory processing difficulties
</li><li>To understand the varying sensory input children receive and how this can affect function
</li><li>To practice and understand activities to improve sensory regulation
</li><li>To adapt key concepts into day to day practices
</li></ul>
<br>

<h2>What is included in our Occupational Therapy teacher training?</h2>
<br><br>
All of our training includes the following aspects:
<br><br>
<b>Identifying children who may need a referral to Occupational Therapy</b>
<br><br>
Teachers will be able to identify appropriate children for specialist intervention and understand the common difficulties can Occupational Therapy improve.
<br><br>
<b>Certificate of completion and Learning pack</b>
<br><br>
Each teacher who attends the training will receive a learning pack containing the key notes of the session and a certificate upon completion of the training.
<br><br>
<b>Hands on activities: </b>
<br><br>
Teachers will be encouraged to partake in activities in order to improve their understanding of both what it is like to experience difficulties in school and how Occupational Therapy can aid in school based skill development.
<br><br>
<b>Questions:</b>
<br><br>
Questions are encouraged throughout the training. All language and terminology used in the training will be kept at a non-clinical, adult friendly level. The allocated time for training is inclusive of any questions.
<br><br>

<h2>How long is OT for Kids Occupational Therapy teacher training?</h2>
<br>
OT for Kids provide training for a minimum of 1 hour, however, the length of the training is dependent upon the objectives of your school. We are extremely flexible and are adaptable to the needs of your school.
<br><br>

