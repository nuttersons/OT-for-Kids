<h1>Sensory Modality Assessment and Rehabilitation Technique (SMART) Report</h1>
<br>
A Sensory Modality Assessment and Rehabilitation Technique (SMART) report is an award winning assessment tool, to ascertain the potential abilities of conscious communication and understanding following severe brain injury. The SMART report would be used to provide an accurate diagnosis of the awareness in a person following such injury, and the most effective communication method for the person deemed to be in a minimal conscious state or a vegetative state. You may wish to use the SMART report as a second opinion, or to provide more information for teachers, or just for your own reference. <br>
<br>
The SMART tool offers an extensive range of sensory techniques (based on evidence-based practice) which provides the patient with the opportunity to demonstrate meaningful responses and evidence of awareness over time.
<br><br>
<h2>The treatment plan aimed to improve the functional performance</h2><br>
All of our therapists who administer SMART have been specifically trained to use this tool. The SMART consists of a ten session assessment completed within a three week period. The therapist would provide a variety of standardised sensory stimulation and record their responses accordingly. Only through this analysis can a treatment programme be offered.<br>
<br>
Each of our highly experienced therapists can to tailor the report to your needs, offering you the special personalised treatment to make your life easier. <br>
