<h1>Reports</h1>
<br>
Reports are used for a number of reasons; A report written by our OT's can be used a second opinion, for your own use or just to summarise the assessment completed previously by one of our therapists. We also produce reports for case managers and solicitors, all of our reports are outcome based and demonstrate any change in function.
<br><br>
<h2>The purpose of children's Occupational Therapy reports</h2>
<br>
Occupational therapy reports are available upon request and are personally written for the needs of your child. Some of the common reasons for receiving a report are to:
<ul class="ticks">
<li>Receive a hard copy of the assessment completed and any advised recommendations</li>
<li>Document a specific condition and its requirements across various environments</li>
<li>Aids and adaptation provision and justification</li>
<li>Provide a clear description of the recommendations for treatment</li>
<li>Receive a second opinion</li>
<li>Summarise the completed assessment</li>
</ul>
