<h1>Functional Assessment Report</h1>
<br>
A Functional Assessment report would offer an overview of the functional assessment completed by the Occupational therapist. You may wish to use the functional report as a second opinion, or to provide more information for teachers, or just for your own reference. The purpose of the functional report is decided by you, it will be written by each therapist following an assessment, and is therefore a personalised reflection based on how the condition is affecting your child. Inside the functional report would contain information regarding the following:
<br><br>
<h2>How the condition is impacting upon the daily level of independent function for your child</h2><br>
The functional report would contain information regarding how your child functions during occupations within the home and school environment. The functional report would give an indication of their functional performance with the aim of providing recommendations that would promote participation in occupations.
<br><br>
<h2>Educational recommendations within school</h2><br>
The functional report would also provide you with information relating to the school including an overview of the condition and how the condition impacts on function within the school environment. For example, some conditions have a direct effect on a child’s ability to concentrate, multitask, focus and interpret information within a distracting and crowded environment (as most class rooms are), these types of issues regarding attention and concentration are often thought to be down to a voluntary behaviour rather than a condition, the functional report can offer an alternative reason for these issues.
<br><br>
<h2>Problem identification and goal setting</h2><br>
The therapist would work collaboratively with you and your child to develop a description of and identify the key obstacles to participating and enjoyment of occupation. By identifying the main problems, therapy goals can be outlined to improve function. Only through clear goal setting can the changes made through therapy be measured on an objective level, providing you with the statistics and information alongside the visual changes seen following therapy.
<br><br>
<h2>Further Recommendations</h2><br>
The report would also contain information regarding the further recommendations the therapist advises following a functional assessment. The information including in the functional report would be the type of therapy (group or 1:1) duration, location and the aspects therapy aims to improve over the advised treatment course.<br>
<br>
Each of our highly experienced therapists can to tailor the report to your needs, offering you the special personalised treatment to make your life easier. <br>
