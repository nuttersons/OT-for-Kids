<h1>Access Audit Report</h1>
<br>
An access audit is an assessment and report based around how easy disabled children can access a building or facility. An access audit would assess the child’s capabilities to access services in a variety of environments, including:
<br><br>
<h2>Places that an access audit can take place:</h2>
<ul class="ticks">
<li>School</li>
<li>Home</li>
<li>Leisure centre</li>
<li>New builds</li>
</ul>
Occupational therapists are often asked to complete Access audits to determine if a building is suitable for a disabled child. Suitable places where an access audit may be needed would include access to the front entrance of the building, turning spaces between corridors, set dimensions for disabled toilets and other aspects that may make accessing the facilities difficult for someone with a disability. For example, building a new leisure centre may request Occupational Therapy input in the design. <br><br>
The Occupational Therapist would look at how a disabled person can enter/exit the building, if there is enough space in the changing rooms, and how to enter and exit the pool safely. Some of the recommendations include advising a hoist for hoist transfers in and out of the pool to enable universal access or wide ramps and corridors to ensure mobility throughout the complex. Another example of needing an access audit is if a child has a progressive condition, in which their health will deteriorate, it is important that any structural changes reflect this. For example, it is better to fit a ramp now that can be used in the future, rather than fitting a rail for the stairs now and needing to modify the environment again later into the development of the condition.
<br><br>
<h2>Facilities include:</h2>
<ul class="ticks">
<li>Entrance/exit</li>
<li>Toilets</li>
<li>Stars</li>
<li>Around classrooms</li>
<li>Corridors</li>
<li>Access to sports facilities (if needed)</li>
<li>Access to fire exits</li>
</ul>
The occupational therapist would visit the building or facility that you would like the audit to take place to complete the assessment. This could include your home, the school or any other buildings that your child uses on a regular basis.<br><br>
The occupatioanl would they advise you on any recommendations that they advise in order for your child to have full access to as many facilities as possible. Without similar access as their peers, children can begin to feel more isolated than they may already feel whilst having to use different methods of transport, entrance or exits into buildings as other children. This report can be used for medical reasons, as a second opinion or for personal use.<br><br>
Each of our highly experienced therapists can to tailor the report to your needs, offering you the special personalised treatment to make your life easier. <br><br>
