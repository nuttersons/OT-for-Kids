<h1>Special Education Needs Statement Report</h1>
<br>
A Special Educational Needs (SEN) statement would give details of the adaptive educational requirements your child may need to ensure they receive the correct level of support during school hours. The SEN Statement provides a recognised statement to ensure the environment is adapted to provide your child the support and education they need to succeed in school.<br>
<br>
You may wish to use the SEN Statement report as a second opinion, or to provide more information for teachers, or just for your own reference. The purpose of the SEN Statement report is decided by you, it will be written by each therapist following an assessment, and is therefore a personalised reflection of the conditions impact upon learning. Inside the SEN Statement there will be information regarding the following:
<br><br>
<h2>Details of all Special Educational Needs as identified in the assessment</h2><br>
The first section of the SEN statement reprot would contain a detailed description of the needs of your child that have been identified following an Occupational Therapy assessment. Factors such as how the condition effects concentration, or mobility around school will be considered
<br><br>
<h2>Educational recommendations within school</h2> 
<br>
An SEN Statement would then discuss how the school can provide a better suited learning environment for your child. Improving learning environments could include ensuring 1:1 support with a teaching assistant, or the provision of a writing slope within the classroom environment. The SEN statement report would also contain a medical overview of how the condition is impacting upon the child’s level of functioning, including all aspects of function such as sight, visual perception and muscle strength and control.<br>
<br>
The final section of the SEN statement report would also outline what the long-term aims are, the arrangements for setting short-term goals, regularly reviewing your child's progress towards those goals, and how your child’s progress is to be monitored<br>
<br>
Each of our highly experienced therapists can to tailor the report to your needs, offering you the special personalised treatment to make your life easier. <br>
