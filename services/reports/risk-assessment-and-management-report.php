<h1>Risk Assessment and Management Report</h1>
<br>
A risk assessment report would outline the main risks to your child’s safety. The environments covered would be the home, the school and public areas. Each environment has its own associated risks that we all manage on a daily basis, ranging from physical risks (such as tripping, falling, walking into things) to psycho-social risks (such as bullying, low confidence, over confidence, loneliness, communication difficulties). Following an assessment the therapist would analyse these risks in relation to your child’s performance and offer risk management through the means of education (for both child and teacher / adult) and adaptive equipment of either the home / school or personal equipment which can be carried from place to place.
<br><br>
<h2>Educational recommendations within school and home</h2><br>
The Risk assessment and management report would outline the condition (including symptoms) and the impact they are having personally for your child. This could include concentration difficulties, mobility issues or similar problems that will have a detrimental effect on how they are able to perform both at school and at home. For example, a child who struggles to move around the home/school without bumping into things or tripping will carry a higher risk when out on school trips, or when your attention is elsewhere at home. Through using a report to outline these risks, you can become more aware of these on a daily basis and therefore are able to provide your child with a safer environment.
<br><br>
<h2>Equipment recommendations for safety</h2> <br>
Although education is a vital part of risk management, the use of adaptive equipment will also offer permanent solutions to provide a safer environment. The type and array of adaptive equipment available will vary upon each case. However the therapist would only recommend the most suitable equipment for your child.
<br><br>
<h2>Summary of main risks and how to manage these:</h2><br>
The risk assessment and management report would provide a summary of the main risks that have been outlined in the report and the best tactics in managing these risks in order to ensure your child participates in a happy and safe environment<br>
<br>
Each of our highly experienced therapists can to tailor the report to your needs, offering you the special personalised treatment to make your life easier. <br>
