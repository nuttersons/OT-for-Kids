<h1>Case Manager Report</h1>
<br>
A Case manager report would give an overview of the assessment, treatment plan, educational recommendations and any equipment needs for the case manager of an individual patient. A case manager is collates all professional reports and opinions together to evaluate the best service of care for an individual with complex needs, who are often unable to make these decisions themselves due to trauma or a degenerative condition. The case manager would use this report, alongside other reports from healthcare professionals that constitute a MDT (multi-disciplinary team) such as physiotherapists, doctors, nurses and speech and language therapists, to evaluate the best care plan approach for this patient.
<br><br>
<h2>Assessment</h2><br>
The case manager report would provide a thorough description and analysis of the complex needs that the patient has and how these are impacting on function regarding daily occupations. Daily occupations are the activities we need to complete in order to participate in a meaningful life. For a child, these relate to being able to get dressed independently, run and play with peers, communicate needs to adults, write and verbally communicate in school and maintain a sustained attention span during lessons.
<br><br>
<h2>Educational and Equipment recommendations within school</h2> <br>
The occupational therapist would also provide their recommendations regarding any educational requirements the child may need during school, this could include any equipment that would further enhance their learning (such as posture chair, sloped writing boards, pen grips, ear defenders). The occupational therapist would give justified clinical reasoning behind the provision of any equipment or allocated therapy sessions that are required during school hours.
<br><br>
<h2>Treatment</h2> 
<br>
The treatment plan, time, date and duration would be outlined in this report. The occupational therapist would provide a detailed breakdown of the therapy needed for this complex case and why the therapist has recommended this treatment, including clear long term and short term goals of therapy and the proposed benefits it would have.<br><br>
Each of our highly experienced therapists can to tailor the report to your needs, offering you the special personalised treatment to make your life easier. <br><br>
